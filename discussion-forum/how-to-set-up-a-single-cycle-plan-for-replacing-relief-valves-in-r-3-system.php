            <p>Take a relaxed approach and set up a single cycle preventive maintenance (PM) schedule. Determine the frequency of the PM tasks - are you completing them individually or for a group of Pressure Safety Valves (PSVs)? The PM task involves removing and reinstalling a spare PSV and it is recommended to complete this work order. Additionally, create a separate work order for the refurbishment of the removed PSV by workshop personnel to ensure it is ready as a spare for future use. To streamline the process, consider breaking down the job into at least three operations within the work order: 1) Removal of existing PSV 2) Installation of spare PSV 3) Refurbishment of existing PSV for future use as a spare.</p>
