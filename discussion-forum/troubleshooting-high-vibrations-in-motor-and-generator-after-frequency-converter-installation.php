            <p>Hello Kishore, you mentioned that there is vertical vibration on the non-drive end (NDE) of the motor base. It is common for units installed on vibration pads to experience issues due to the deformability of the structure. The vibration may be caused by a coupling problem pivoting through the drive end (DE) of the motor and shaking the NDE, possibly due to a dry coupling. Many engineers overlook the fact that supporting a base on pads can lead to vibration since the base is not solid and may be warped. To prevent vibration, it is best to install equipment on a firm, properly machined base set in place on epoxy grout for a solid bond with the floor. Additionally, check the equipment's feet for trueness before installation to prevent softfoot issues. This type of installation can significantly reduce vibration in the surrounding areas. Thanks for your attention to this matter.

