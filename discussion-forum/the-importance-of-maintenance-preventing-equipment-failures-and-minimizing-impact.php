        <div class="bg-white p-3 rounded mb-3">
            <p>In RCM2 methodology, the timing of failures is more important than the cause. Failures are typically caused by various factors such as fouling, fatigue, creep, wear, corrosion, poor workmanship, or exceeding design limits. All failures have underlying causes, even though the timing of failure initiation may be unpredictable. The P-F curve is a useful tool to understand the rate of physical degradation, with the 'P' point indicating that failure has already begun. Predictive Maintenance aims to forecast the time-to-functional failure, or the 'F' point, after the initiation of the P-F curve. While the 'P' point may be unpredictable, the probability density curve provides insights into the statistical likelihood of failure at different time points. It does not disclose information about physical degradation but offers guidance on when maintenance should be performed.</p>
