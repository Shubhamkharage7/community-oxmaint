Before adding new users, it is recommended to log in as Master and change the default password to enhance security. You can also edit permissions for the fxDefault user as needed. The Edit User List dialog box allows you to configure users, set passwords, assign access levels, and define permissions for each user. 

To add a new user, write a script in Machine Edition with the EditUserList function. A dialog box will appear in Runtime for you to enter the new user's information. Similarly, to edit or delete users, follow the same process of writing a script and using the EditUserList function.

By setting up user accounts and permissions in Proficy View HMI, you can control access to graphical panels, data changes, inspectors, communication monitoring, script debugging, and more. It is important to regularly review and update user permissions to maintain a secure system.</p>
