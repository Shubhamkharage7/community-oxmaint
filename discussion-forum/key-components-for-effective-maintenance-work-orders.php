            <p>When creating a work order (WO) for preventive maintenance (PM), predictive maintenance (PdM), or corrective maintenance (CM) activities, it is important to include specific elements unique to each type of activity. Certain 'Common' elements should always be present, such as Equipment/Asset Details (Functional Location, Basic Engineering details, reference to engineering documents, etc.), Equipment/Asset Criticality, Scheduling Priority, Work Planner, and Cost Center/Work Center.

For PM/PdM activities, additional elements should be included, such as a Detailed Job Plan (including Permit Requirements, LOTO, Process Isolations, etc.), Job Code, Bill of Materials (BoM), Tool Kit, Expected man-hours, Job criticality, and Findings/Observations.

For CM activities, a Corrective Works Notification should be generated before the WO. This notification should outline the basic problem statement and include the 'Common Elements'. The WO itself should include Problem/Fault Code & Cause Code, Job Safety Analysis (JSA) or Risk Assessment results, Findings & Corrections reporting section, spares consumed, tools employed, man-hours consumed, and production loss or impact.

It is important for organizations to follow their incident & failure investigation process in addition to these reporting requirements for proper analysis and reporting for reliability studies.</p>
