            <p>Dear Sir,

The measurement of liquid level in a vessel is a crucial aspect that can be found in the design documentation. Factors influencing this measurement include the vessel's capacity, the anticipated maximum size of liquid slugs (as outlined in the HAZOP analysis), the capacity of the liquid pump-out system, and the velocity of vapors/gas in the outlet pipe and over the liquid surface.

It is important to note that higher values in these variables can lead to an increased risk of liquid carry over or, in extreme cases, "puking." Therefore, under such circumstances, it is recommended to maintain the liquid level at a lower level.

If you are unable to access the design documents, I recommend enlisting the services of a design consultant to assist you with this matter. Each situation is unique and requires specific details and analysis, so there is no one-size-fits-all solution that can be provided by forum members without a thorough investigation.

Thank you.</p>
