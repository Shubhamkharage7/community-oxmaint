            <p>For those who may come across this information later on, it is important to note that L7 and L8 processors are equipped with an energy storage module (ESM) that ensures the PLC remains powered long enough to save its program into nonvolatile memory when the main power source is disconnected or the processor is removed from the chassis. During the program-saving process, a red "ok" light will illuminate on the PLC and then turn off once the program has been successfully saved. In Studio 5000, a minor processor fault will be indicated if the ESM module is faulty, and a message will also appear on the PLC screen. The ESM in L7 processors can be easily replaced by the user while the PLC is powered on, with the part number being "1756-ESMCAP". In contrast, the ESM in L8 processors is not user-replaceable, requiring the entire processor to be sent to Rockwell for repair if the module is faulty.</p>
