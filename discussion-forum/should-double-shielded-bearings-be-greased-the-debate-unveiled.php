            <p>I'm excited to share my insights on the topic at hand! I have always been a big admirer of Electricpete and the valuable knowledge he provides to many, myself included. However, I must respectfully clarify a point regarding the lubrication of double shielded bearings versus sealed bearings. In the realm of electric motors, it is the sealed bearings that are designed to be lubricated for life, not the double shielded bearings.

When it comes to motors, the use of double shielded bearings is declining in repair shops. Typically, if a motor comes in with double shields, it will likely leave with only the outboard shield intact. In my experience dealing with various motor manufacturers, none have advised against lubricating double shielded bearings. If a motor has a grease zerk, regardless of its horsepower, it is recommended to apply grease. Conversely, if there is a plug where the zerk should be, this indicates that the motor has sealed bearings and does not require greasing.

In the case of double shielded bearings, a small gap can be observed between the inner bearing race and the shield. This is where new grease will penetrate to replenish the old oil. Despite its size, this gap is sufficient for the lubrication process. I hope this information proves helpful in understanding the maintenance of electric motor bearings.</p>
