            <p>Andy Rodes, the VP of Operations at VibeCloud Reliability, discusses the comparison between the costs of online wireless vibration monitoring and route-based vibration monitoring. He has been in the industry for nearly two decades and has extensive experience in analyzing various approaches when it comes to condition monitoring. According to Rodes, there is no fixed industry standard cost per point for vibe routes from third-party service providers like his company.

When running a vibration program internally, Rodes suggests that having two certified vibration analysts, with one gathering data and the other analyzing it, can handle around 4-6k points with monthly readings and analysis. Factors such as the location of the facility, number of components monitored, and sampling frequency play a crucial role in determining the scope and cost of monitoring solutions.

Rodes emphasizes the importance of using wireless sensors with a minimum of 10khz velocity spectrum range to provide analysts with the most data for accurate analysis. While sensor pricing can be complex, Rodes mentions that their preferred sensor starts at $26 per month and includes everything from sensors to full analysis by certified analysts.

In conclusion, Rodes points out that for facilities looking to monitor 75 components or more, monthly walking route vibration analysis is usually more cost-effective compared to online monitoring solutions with wireless sensors. He also mentions working on a white paper to further explore the pros and cons of wireless sensors versus walking route monitoring. For more information, readers can reach out to Rodes for a copy of the white paper once it is completed.</p>
