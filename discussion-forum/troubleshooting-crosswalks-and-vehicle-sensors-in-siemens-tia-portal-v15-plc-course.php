PLC programming revolves around time, with the scan cycle acting as the clock. During each scan cycle, the PLC reads inputs, assesses the system's state, determines if a change is necessary based on the combination of previous and current input states, and writes the new state. This process may involve activating external outputs like lights or altering internal state information stored in PLC memory, such as booleans and timers.

