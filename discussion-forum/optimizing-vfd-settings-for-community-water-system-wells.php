            <p>Your post lacks important details. Pump control can be direct-acting, where increasing control output (speed) raises the process variable (pressure), which is usually the desired outcome. On the other hand, pump control can be reverse-acting, where increasing control output (speed) lowers the process variable (such as the level in a sump). It is crucial to ensure that your drive is properly configured, including checking settings like minimum speed and max amps, as these settings can restrict speed adjustments. Minimum speed is especially important for systems with high static head, as running too slowly will not provide sufficient head.</p>
