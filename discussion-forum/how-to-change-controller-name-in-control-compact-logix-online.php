            <p>One user on a forum mentioned that when you check the controller tags section, the name assigned by the user is displayed next to each configuration, input, or output assembly. Therefore, altering the module name in online mode will impact communication. However, this is not entirely accurate as the names are actually displayed with a "local" prefix, not the controller name.

It is indeed possible to change the controller name online using CIP messages. Yet, if you are running version 24 or higher, going online with the controller after renaming it can be a bit complicated. Additionally, this practice is not endorsed by Rockwell. Renaming online will also modify the display on the LCD screen.

It is advisable not to include the program version in the controller name. The ACD file does not necessarily have to match the controller name. Instead, consider placing the version information in the controller description, which can be adjusted online via Studio.

To clarify further, the controller name is not the same as the controller's hostname, which can be changed whenever necessary. The controller name does not impact communication but is visible in Linx.</p>
