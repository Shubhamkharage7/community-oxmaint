Nonetheless, projects utilizing a 5370 CompactLogix controller may still encounter the "Battery" minor fault, which actually refers to an ESM fault rather than a Battery fault. This fault can be monitored through the FaultLog Object using a GSV to the MinorFaultBits attribute, bit 10. Since the ESM is not replaceable, encountering this fault would require controller replacement.

In the event of a Major Fault Type 1 Code 40 in a Logix 5000 controller, it indicates a failure of the ESM to store the project to the internal NAND flash memory upon power up. This typically points to a hardware issue with the ESM not charging or retaining its stored energy. Minor Faults such as Type 10 Code 12, 13, 14, 15 also trigger the Battery Minor Fault and suggest ESM issues.

Major Faults will halt the processor, while Minor Faults will not interfere with its operations. For further assistance, please refer to TechConnect. Thank you, George.</p>
