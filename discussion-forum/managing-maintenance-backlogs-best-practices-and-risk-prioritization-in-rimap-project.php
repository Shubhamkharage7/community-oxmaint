I have achieved significant success by employing risk matrices for ongoing projects integrated within the Work Order priority system. Building on Vee's insights, Work Order priorities are structured to reflect both the potential severity of consequences if no action is taken and the acceptable timeframe before failure becomes "intolerable". While this may sound complex, in reality, it is quite straightforward and easy to navigate. This approach allows for a systematic and logical prioritization of work orders, providing a level of control over ongoing projects.

For instance, in the case of detective tasks, the frequency of these tasks is determined by the acceptable level of risk tolerance. By executing these tasks within a specified timeframe, we can effectively manage our risk within the predefined tolerance levels. If a task exceeds its designated timeframe, it indicates that we are surpassing our acceptable risk threshold, putting us in a position of unnecessary risk.

Similarly, prioritization methods like these, when rigorously implemented and monitored through graphical reports like "Priority versus time," have proven to be successful. These reports not only track the number of work orders but also identify those that have pushed the organization beyond its acceptable risk limits. This approach also aids in managing general backlog, albeit a broader aspect.

