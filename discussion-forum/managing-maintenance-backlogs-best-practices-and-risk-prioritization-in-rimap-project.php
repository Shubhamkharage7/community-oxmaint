            <p>After implementing a concept from an old maintenance management book by Herbaty, we have seen significant success over the past 9-10 months. According to our system, a deferred job is essentially a task that needs to be scheduled in. We have introduced a priority system for these deferred jobs, consisting of three levels. Our scheduler always prioritizes filling in higher priority deferred jobs, as long as the estimated duration of the task aligns with the schedule. In cases where a higher priority deferred job cannot fit in, any suitable deferred job can be utilized to fill the gap. It is possible for lower priority deferred jobs to get overlooked and never make it onto the schedule. Regular audits, initially set at three months, assist in identifying such cases. It is crucial to make a decision about these tasks, whether adjusting the priority, outsourcing the job, or dismissing it altogether. Proper assessment is key in managing these deferred jobs effectively.</p>
