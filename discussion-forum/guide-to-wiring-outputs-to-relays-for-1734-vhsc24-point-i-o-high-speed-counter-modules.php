        <div class="bg-white p-3 rounded mb-3">
            <p>Improving the friendliness of the documentation is crucial, as even the diagrams on the modules may not be very clear. The 1734-VHSCx module features two fast 24V solid-state sourcing outputs, with a 25us ON and 150us OFF timing. These outputs draw power from the Field Power Bus of the POINT I/O assembly next to them and can handle a load of up to 0.5A. Additionally, they come equipped with open-circuit and short-circuit detection diagnostics.

The "RET" terminals on the module are linked to the DC Common of the field power bus. By connecting an output load between Output 0 or Output 1 and one of the RET terminals, it will be energized by +24V when the output is activated. Essentially, these terminals serve as an additional connection point for wires intended for the DC Common of the Field Power Bus.

On the other hand, the Vaux terminals are independent of the POINT I/O field power bus and are designated for providing power to the encoder and terminating its power wire. The two VAux+ terminals and two VAux- terminals are internally connected, making them suitable for encoders that require 5, 9, 12, or 15 volt power instead of 24 volts.

If desired, a small relay can be controlled with the VHSCx output for increased power capability, albeit at a slightly slower speed.</p>
