            <p>Apologies for the delay, Mike. The synchronization of transducers is automatically averaged for accurate results without any effort on your part. Individual amplitudes are also averaged, with the main focus being on maintaining the position of the roving transducer in the same relationship. In cases where adjustments are needed, such as when taking cross channel readings, a simple change in position notation or manual adjustment can rectify any discrepancies. The data exported from RBMWare to ME'scope is expected to be precise, but any errors can be easily corrected by inputting a minus sign in the spreadsheet. Cross Channel phase readings are best recorded on a bubble chart for reference. As long as coherence levels are above 90%, the readings should be reliable, with a preference for readings above 95%. Coherence levels indicate the relationship between signals, with low coherence possibly pointing towards structural issues such as loose joints or cracks. Performing spectral readings can help identify such problems, as changes in coherence when varying frequencies can indicate potential structural issues that require attention.</p>
