        <div class="bg-white p-3 rounded mb-3">
            <p>Several years ago, I was responsible for organizing the participation of refineries in the Solomon studies for a global oil company. Solomon Associates (SAI) conducts these studies annually, alternating between the USA in even years and the rest of the world in odd years. Each study includes around 150-200 refineries, with the specific locations kept confidential by SAI. As a result, only SAI can provide information on the participants in each study, including details on who ranks in the top quartile or is considered a pacesetter. However, what is available is the performance data for each refinery, including metrics such as energy consumption, manpower utilization, maintenance and operating costs, and conversion efficiency.

SAI employs a normalization process in their analysis, measuring all process units in equivalent distillation capacity (EDC) to allow for comparisons between simple and complex refineries. They also make adjustments for shutdown cycles and maintenance cost allocations to ensure consistency across different accounting practices. This approach creates a level playing field for all participants in the study.</p>
