        <div class="bg-white p-3 rounded mb-3">
            <p>I once had a customer with around 20 nodes connected, which I initially thought was too many. The number of nodes depends on the amount of data being passed through. DH+ works by transmitting data in a single puck from node to node. To optimize performance, I recommend splitting the data into 3 pucks - one for each production line. If budget allows, consider adding a bridge to divert some traffic to Ethernet.

DH+ is known for its industrial strength and reliability. If you encounter issues, ensure that terminal resistors are in place at the end nodes. If problems persist, it may be due to an excessive number of nodes or data overload.

For additional information, refer to this document: [link to document].

My customer had a significant amount of data, with new recipes being loaded every few hours. These recipes included various temperature, pressure, and vacuum ramps, which could potentially cause performance issues.</p>
