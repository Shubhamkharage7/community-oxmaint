        <div class="bg-white p-3 rounded mb-3">
            <p>In most cases, a fork type sensor is used to detect the trailing edge of a label. If this sensor is misaligned or not detecting the gap between labels, it could be causing the issue. Additionally, check if the bottle sensor is accurately detecting a bottle or if it remains on even when no bottle is present. The label sensor model is Leuze GS 61/6D.2 (without knob volume) and the bottle sensor is a Panasonic FX100. Despite only one glass bottle being detected, the label continues to feed. A video demonstrating this issue with the sensor detection is available. The label sensor is functioning properly, successfully detecting every label and gap.</p>
