        <div class="bg-white p-3 rounded mb-3">
            <p>A common issue in labeling machines is the misalignment or lack of detection of the gap between labels by the fork type sensor that detects the label's trailing edge. Additionally, it is important to ensure the bottle sensor accurately detects the presence of a bottle and does not stay on when no bottle is present.Troubleshooting these sensors is essential for optimal machine performance.</p>
