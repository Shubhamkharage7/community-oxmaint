1. The controller must not be in RUN mode, but in "Rem Run", "Rem Prog", or "Program" mode.
2. The controller should not be locked by another user online.
3. The edits should not be left in "Test Edits" mode from a previous editing session.

If any of the above conditions are not met, the "Accept Edits" function may be prevented. If edits are in "Test Edits" mode, switching back to "Untest Edits" temporarily undoes the edits, prompting the user to assess their impact and decide on further action. If the reason for edits being in test mode is unclear, it is recommended to "Assemble" them to make them a permanent part of the code.</p>
