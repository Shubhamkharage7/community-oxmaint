                    <h3>Question:</h3>
                    <p>In March 2007, I had the opportunity to speak at a Reliability Conference in my country. I was asked to step in for Vee Narayan as the resource speaker due to his busy schedule. Despite the original plan for a 2-hour session on Root Cause Failure Analysis, my speaking time was cut short to 1.5 hours. During the conference, another presenter discussed Lean Manufacturing and its impact on their plant, specifically highlighting the reduction of maintenance staff from 36 to 6 due to multi-skilling. This led to questions about the fate of the remaining employees, with the presenter jokingly saying they were retired.

This raised concerns for me about the true intentions of Lean Manufacturing â€“ is it designed to push people into retirement? I believe there are two key factors that Lean Manufacturing overlooks. Firstly, multi-skilling may not be suitable if anthropometric factors come into play, as highlighted in John Moubray's RCM book. Secondly, the broad spectrum of roles within maintenance, including spare parts management, CMMS, reliability groups, and various technical roles, should be considered before downsizing.

Rather than forcing employees into retirement, why not reassign them to other areas within maintenance? This approach aligns more with my belief in continuous improvement without sacrificing people's livelihoods. It is important to understand the diverse nature of maintenance roles and the value each individual brings to the team. Let's aim for efficiency without compromising our workforce.</p>
