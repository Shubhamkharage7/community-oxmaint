            <p>Hello Rolly, so it seems that the workforce has been reduced from 36 to 6 people. Achieving a 6x reduction in staff cannot be solely attributed to adopting a multi-skill approach. Let's break down the numbers for a moment... Going from 36 to 6 is a significant change. Assuming the 6 multi-skilled individuals are fully utilized, each carrying a 100% work load, we can see that each single-skilled worker would have had a work load of 16.67%. This raises questions about the previous work efficiency and how the workload was adjusted to accomodate the smaller team. It is difficult to imagine that a company would operate with such a low work load percentage consistently. 

As someone who has experience in both single-craft and multi-craft roles, I believe that the reduction in workforce was not solely due to implementing a multi-skill strategy. The depth of skill in each craft was compromised as individuals were no longer able to focus solely on their chosen area of expertise. Personally, I found value in returning to a single-skill role as an electrician to continue honing my skills. There is a lot of industry jargon floating around, and sometimes these buzzwords are taken on by corporate leaders without a full understanding of their implications. Consultants selling various products like Lean Maintenance, FMEA, and RCM may contribute to the confusion surrounding these concepts.

Ultimately, as Reliability Engineers working in maintenance roles, it is our responsibility to ensure efficient operations and dispel any misconceptions that may arise.</p>
