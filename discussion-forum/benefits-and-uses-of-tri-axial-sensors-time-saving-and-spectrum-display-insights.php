            <p>When using a triaxial sensor, time savings are only achievable with an analyzer capable of processing all three signals simultaneously. If your analyzer only has one channel, you can use a three-position switch to capture each axis one at a time after installing the triax, reducing the time needed to switch between axes. I developed a data acquisition system for my service centers that captures all six axes of an electric motor at once, significantly saving time. Proper positioning of the triax is crucial to ensure the accuracy of the three reference positions. For example, with the PCB/IMI 604B model, positioning the triax with the connector aligned with the motor shaft ensures the X, Y, and Z axes are correct. Placing the triax close to the centerline of the shaft is also important to ensure accurate measurements. Avoid mounting the triax in a way that doesn't accurately represent the shaft centerline on one or more axes. I hope this information is helpful. Ron Brook ------------------------------ Ron Brook ------------------------------ Original Message Original Message: Sent: 09-27-2023 08:50 From: Anonymous Member Subject: Tri-axial sensor This message was posted by a user wishing to remain anonymous Dear all, A supplier is recommending a tri-axial sensor to me, and I have some questions: 1) How does using a tri-axial sensor save time? 2) Can a single-channel analyzer be used on site with a triaxial sensor, and does it require a special mounting? 3) What does the spectrum display look like with a triaxial sensor? Would the measurement result in a single spectrum or three separate spectrums? I have attached a picture of a mount I found online - can this be used instead of a triaxial sensor if installed on one axis of equipment?</p>
