            <p>Inquiry by Josh: How should equipment and instruments like block valves or hand valves, interconnecting power cables, and DCS cards be registered as assets in the industry? Are there any guidelines or best practices for asset registration? 

Dear Josh, I will address your question in parts. 

1. Many Operation & Maintenance personnel tend to overlook asset IDs and rarely use them for work orders or requests in the CMMS system. Instead, they rely on plant tags from P&IDs or other drawings. Although there is a field in the CMMS system for Asset #, it is often not mandatory for data entry. However, affixing asset ID tags is a requirement, and routine asset verification is recommended. When retiring or upgrading an asset, certain procedures, such as partial or complete retirement forms, should be followed. 

2. The approach to identifying equipment like block valves and DCS modules in the CMMS system varies between sites. This discrepancy may stem from the absence of unique equipment tag numbers in project/vendor drawings or the lack of maintenance history requirements for certain equipment. Nevertheless, these entities can be added if the need arises later on. 

3. Asset IDs are typically assigned by Assets Accounting personnel for capital equipment, which meets specific criteria outlined in company guidelines (GI 22.008). This includes items that are physically identifiable, used in productive capacity, provide benefits for over a year, and have certain direct material cost thresholds.</p>
