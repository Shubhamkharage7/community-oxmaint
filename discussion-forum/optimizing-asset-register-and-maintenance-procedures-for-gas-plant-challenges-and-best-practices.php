                    <h3>Question:</h3>
                    <p>Hello Members, 

We are currently undergoing a PM optimization process for a 25-year-old gas plant by reviewing existing maintenance procedures. During this process, we have identified minor discrepancies between assets in Maximo and assets in the field due to various upgrades over the years. As part of our PMO process, we have initiated a validation of the Asset Register and Functional Hierarchy in Maximo by comparing them with field data, P&IDs, instrumentation, and electrical documents. 

While conducting this validation, we have encountered certain challenges due to the absence of specific policies. These challenges include: 
1. Asset Registration - determining which equipment/instruments should be added as assets (e.g., block valves, hand valves, interconnecting power cables, DCS cards). Are there any industry guidelines or best practices for asset registration in this sector? 
2. Maintainable Equipment - establishing selection criteria for items subject to maintenance. Are there any industry-specific guidelines or best practices in the oil and gas field? Different industries may have varying approaches to selection and registration. Are there any value-added criteria or best practices available for consideration?

We appreciate any valuable insights or inputs you can provide on these matters. 

Thank you, 
KJAP</p>
