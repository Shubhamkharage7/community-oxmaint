            <p>When it comes to electric motors, the choice between sleeve and rolling bearings is crucial. Journal bearings are typically less tolerant of misalignment compared to roller bearings, unless the bearing has a spherical seat between the bearing and housing. This feature allows the bearing to swivel, providing self-alignment for angle misalignment of the bearing. Among rolling bearings, cylindrical roller bearings are known to be very intolerant of angle misalignment, while ball bearings offer more tolerance. For the highest level of tolerance to misalignment, consider using spherical roller bearings. Learn more about this topic in Siemens' article on sleeve vs rolling bearings for electric motors.</p>
