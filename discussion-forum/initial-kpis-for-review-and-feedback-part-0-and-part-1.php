        <div class="bg-white p-3 rounded mb-3">
            <p>Josh, here are some recommendations for optimizing business performance and focusing on key performance indicators (KPIs): 

1. Begin by conducting a thorough gap analysis to determine where your organization currently stands and where you aim to be in 2, 3, or 5 years. 
2. Remember that a high number of KPIs does not necessarily equate to progress; it can lead to confusion among team members. 
3. Avoid creating KPIs simply because your CMMS can generate them. 
4. Prioritize 10-12 key areas for immediate improvement over the next 12-18 months. Focus on these areas diligently to drive attention and effort towards them. 
5. Once you start seeing sustained improvements, consider revising or replacing existing KPIs with new ones. Maintain a manageable number of KPIs to keep track of progress effectively. 
6. Ensure that your KPIs cover all phases of planning, scheduling, execution, and analysis to prevent overlooking critical stages. 
7. Implement improvements in stages to give your team time to adapt to new processes gradually. 
8. Eliminate any KPIs that serve only to boost individual egos, and focus on areas like safety, environment, uptime, and cost to align with organizational goals. 

When selecting KPIs, refer to established resources on the subject rather than inventing new metrics. It is recommended to have no more than 12 KPIs at the Maintenance Manager level, which should be reviewed every 2-3 months. Ensure that lower-level performance indicators support each KPI and are readily accessible to relevant personnel. Display all KPIs prominently for easy visibility and tracking. By following these guidelines, you can enhance operational efficiency and drive sustainable improvement within your organization.</p>
