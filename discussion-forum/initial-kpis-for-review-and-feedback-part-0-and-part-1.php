The MSC is a method for creating and implementing strategies in asset-centric industries through the use of measurement systems. It delves into cascading measurements and indicators, starting from the corporate level and aligning indicators with the organization's goals and objectives.

I developed the MSC around 2000 after realizing that existing measurement systems were either too shallow or unsuitable for modern asset management needs. Asset management is crucial for large businesses, especially in industries like paper, mining, electricity, oil & gas, and water. The MSC has been implemented for various purposes such as performance improvement, strategic planning, CMMS system requirements, performance management systems for outsourced contracts, and data requirements for asset information portfolios.

