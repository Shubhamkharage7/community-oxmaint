                    <p>Hello readers, I want to elaborate on the discussions initiated in Comparing RCM with PMO2000. I pointed out various activities that appeared questionable. In this thread, I shared my thoughts. I am interested in hearing about more examples—specifically 'what' and not 'who'. There are two areas where actions can seem foolish. Firstly, conducting risky tests to uncover potential failures that may not be evident. Secondly, performing intrusive maintenance on equipment that is in good working condition. Let's delve into some examples of dangerous tests. One incident that deeply affected me involved the loss of a Boeing 707 aircraft and its crew. A concerning test involved shutting down two engines on one wing simultaneously, causing the aircraft to spin. A tragic outcome occurred during a training session when the student pilot failed to recover from this scenario, resulting in a fatal crash. Another noteworthy example is the blackout of controlled airspace at Sydney's airport due to a test on the UPS system during peak hours. This disruption caused chaos in the skies, leading to potential risks for incoming flights. Additionally, I have observed alarming testing procedures for lifts in underground mines, which could potentially damage the equipment and endanger workers. Moving on to intrusive maintenance practices, mandatory inspections can sometimes be excessive, leading to unnecessary risks and potential failures. For instance, dismantling and reassembling components like engines, even when they show no signs of wear or corrosion, can introduce new issues and compromise safety. These practices highlight the importance of conducting thorough risk assessments and exploring alternative testing methods to avoid destructive outcomes. Have you encountered similar instances worth sharing? Feel free to contribute to the discussion.</p>
