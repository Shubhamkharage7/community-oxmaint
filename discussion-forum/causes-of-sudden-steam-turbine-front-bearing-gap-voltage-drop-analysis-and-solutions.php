                    <h3>Question:</h3>
                    <p>Dear Experts,

We are managing a high-pressure steam turbine system that drives a synthesis gas compressor train. The steam pressure is 101Kg/cm2, steam temperature is 500degC, turbine RPM is 13200, and KS flow is 320T/hr. The turbine rotates clockwise when viewed from the steam inlet with a bearing design of tilting pad (05 pads load on pivot configuration). For further details on the machine layout, please refer to Annexure-1. The governing system was originally PGPL Governor with extraction control, but it was changed to an electronic governor Woodward505E in 2013.

In August 2016, the machine was operating at low load operation with HP at 65%, LP at 67%, and steam flow at 222 ton/hr. There was a sudden decrease in gap voltage at the turbine front end bearing VT-209A, along with a rise in bearing temperature to 151degC. The shaft vibration levels were monitored throughout the event, with a jump to 136 microns at the time of tripping.

Unfortunately, ADRE was not installed at the time of tripping, but vibration data was recorded from the Bentley Navada 3500 display panel. Upon dismantling the front bearing (VT-209), it was observed that the babbit of the pad at the 10o’ clock position had been wiped off.

The main question arising from this situation is: What could have caused the sharp decrease in the gap voltage from 8.44 to 7.1 in just 1 minute?

For more information and visual references, please see the attached annexures.</p>
