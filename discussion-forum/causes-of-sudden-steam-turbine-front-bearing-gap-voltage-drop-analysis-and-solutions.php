Thank you for your response. It was really informative and helpful. In our analysis, we found that there are two thermocouples in place: one on the bottom pad and the other at the 7 o'clock position (TI_18). TI_18 is referenced in Annexure-IV of my previous post. 

Prior to the load reduction at 12:12 PM, the bearing temperature (TI_18) was recorded at 98.7°C. After the load reduction began, we observed an increase in the bearing temperature to 109.4°C, remaining at this level for approximately 4 hours until 4:10 PM. 

Within the next 27 minutes until 4:37 PM, the temperature slowly rose to 117°C, with a gap voltage of 8.46 at that time. By 4:38 PM, the temperature had spiked to 151°C. Subsequently, at 4:39 PM, the gap voltage reached a minimum of 7.1V and the bearing temperature dropped to 146°C.

Attached is a table with detailed data for your reference. The decrease in gap voltage and the discovery of bearing wipe-off at the 10 o'clock position suggest an abnormal rotor position during low load operation. This sudden drop in gap voltage is unprecedented.

