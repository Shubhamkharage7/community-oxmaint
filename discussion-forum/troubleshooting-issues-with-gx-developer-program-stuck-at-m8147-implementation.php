            <p>The GXDeveloper software (standard ladder) lacks the ability to display the number of registers used for each data type. For instance, an integer is represented by one word such as D200. A double integer is represented by two words like D200 + D201. Similarly, a single precision float is represented by two words (D200 + D201) while a double precision float is represented by four words (D200 + D201 + D202 + D203, not available in FX processors).

In contrast, GXWorks2 offers a more informative approach by using FBD (Function Block & Ladder). It displays symbols along with their corresponding addresses, providing a clearer representation. For example, the symbol My_Integer indicates a single D register (D300) while My_Float represents a float by displaying a range of addresses (D200-1, composed of D200 & D201).

In some HMIs like Bejiers, an integer may appear with a decimal point on the screen (e.g. 100.2). However, it is defined as a whole number (1002) without the decimal point. This feature can be useful when a value is meant to be interpreted as being 10 times larger than the displayed value.

It's important to take caution when using different data types in your program. For instance, if D200 and D201 are defined as integers but are used as a 32-bit Dint, the values from both registers are combined. In the example shown, moving integers 1234 into D100 and 345 into D101, then using D100 for comparison as a Dint, results in an unexpected value (22611154) due to both registers being considered in the comparison. This clarification aims to address any confusion regarding data type usage in programming.</p>
