I recently stumbled upon a book on preventive maintenance that argues PM should not make up 15-20% of the total workload. According to Nowlan and Heap, only 14% of failures are random, nowhere near the 80% figure mentioned by Sam. It appears that some consultants may be misinterpreting the work of Nowlan and Heap, as their study found that 68% of failures were due to infant mortality, which is quite different from random failures. 

