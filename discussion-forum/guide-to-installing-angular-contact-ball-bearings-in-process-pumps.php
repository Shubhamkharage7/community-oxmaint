            <p>I have come across discussions regarding the importance of properly tightening back-to-back angle-contact bearings on overhung pumps. It is recommended to tighten them just enough so that the outer races can be moved against each other with some resistance, but not to the point of being bound up. This method serves as an alternative to using a torque wrench, as the effectiveness of the wrench check largely depends on the friction between the inner ring and the shaft.

Your question about whether to adjust the bearings before or after they cool down raises some interesting points. It is worth considering that once the bearing cools, it may be challenging to move the inner ring on the shaft. By adjusting the bearings before they cool, you are establishing a preload that may shift as the bearing returns to ambient temperature (and possibly tightens again if it warms up during operation).

Adjusting the bearings before they cool down may be more practical for a couple of reasons. Firstly, the operating temperature of the bearing falls somewhere between ambient and the temperature at which it was installed, so it is challenging to recreate the exact operating temperature with either approach. Additionally, adjusting before cooling allows for the opportunity to double-check the tightness of the outer races against each other after the bearing has cooled. If they feel too loose, adjustments can be made accordingly.

