        <div class="bg-white p-3 rounded mb-3">
            <p>In an ideal world, I would suggest storing the rotors vertically to prevent any bowing. While this method requires a significant amount of space and caution during handling, it eliminates the risk of damage. It is important to ensure the rotors are clean and properly preserved during storage. Another option is to invest in custom-made, sealed containers for each rotor, which can be purged with nitrogen for added protection. These containers can be stored either horizontally or vertically, providing a more expensive but secure storage solution.</p>
