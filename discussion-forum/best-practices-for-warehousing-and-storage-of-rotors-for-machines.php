        <div class="bg-white p-3 rounded mb-3">
            <p>Following my previous suggestion, it is imperative to check the balance of any rotor that has been stored, whether horizontally or vertically. For rotors stored horizontally, it is crucial to perform a thorough slow-roll with runout checks to ensure any bow has been eliminated before the balancing process. Any workshop that does not automatically check rotor runouts before balancing should be avoided at all costs.</p>
