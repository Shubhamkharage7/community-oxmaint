        <div class="bg-white p-3 rounded mb-3">
            <p>In response to caveman-cheddar's comments, in Philadelphia PA there are numerous refineries that used to store spare rotors horizontally, resulting in balance issues. After sending the spare rotors to a balance shop for verification, corrections were often necessary due to a bow caused by horizontal storage. This led to vibrations increasing over time once the machine was in use. It took years to convince the refineries to store rotors vertically, following API recommendations. Alternatively, if storing horizontally, it is important to balance the rotor before storage and address any bow. When replacing the rotor, trust the previous balance and allow any residual bow to work itself out by slow rolling the unit. Additionally, when storing horizontally, be mindful of protecting proximity probe target areas and prevent them from becoming load-bearing while in storage.</p>
            <ul class="post-meta mb-0">
