        <div class="bg-white p-3 rounded mb-3">
            <p>When storing motors, it is common practice to have lubricant in the bearings to prevent corrosion. Regularly rotating the bearings manually can ensure proper coating and prevent excessive bowing of horizontal rotors. While it may not be feasible for all types of rotors, periodic rotation can be beneficial in reducing bowing when stored horizontally.</p>
            <ul class="post-meta mb-0">
