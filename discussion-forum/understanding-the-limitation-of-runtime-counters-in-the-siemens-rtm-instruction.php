            <p>Previously, the runtime meter information was easily accessible in the technical specifications of S7-300 CPUs, such as the CPU 312C which had only one runtime meter. Strangely, this information seems to be elusive when it comes to the S7-1200. The only snippet I managed to uncover states that the CPU can operate up to 10 runtime hour meters to monitor critical control subsystems. Each timer needs to be initiated with an RTM execution, with all hour meters halting when the CPU transitions from running to stopped. Additionally, individual timers can be stopped using RTM execution mode 2. For more details, refer to page 344 of the S7-1200 system manual at the provided link: https://cache.industry.siemens.com/...95681/v1/s71200_system_manual_en-US_en-US.pdf</p>
