            <p>Bearing failure frequencies are typically determined based on the bearing's geometry, following established standards for bearing sizes. These frequencies, including inner race, outer race, cage, and ball frequencies, are crucial indicators of failure, especially in advanced stages. They are calculated using machine RPM (or CPM) values and the rotating shaft associated with the bearing. 

During the initial stages of failure, material tends to fracture and delaminate, leading to defects on the surfaces that can be detected by high frequency vibration equipment as 'metal to metal' contact. The progression of bearing failure often follows four stages, starting with increased metal to metal contact, followed by peaks surrounding bearing frequencies, a raised noise floor around the frequencies, and finally a drop in metal to metal contact value. 

In some cases, bearing issues manifest as harmonics of calculated values, such as in current signature analysis where it typically occurs at the 4th harmonic. If specific information is unavailable, bearings may exhibit non-integer multiples (or sub-multiples for cage frequencies) of the shaft RPM. Overall, bearing fault detection through vibration analysis is a highly reliable method. Howard W Penrose, Ph.D., CMRP at MotorDoc.com.</p>
