            <p>Are you familiar with the type of bearing and its designation? I have discovered that the calculations provided by the CSI library can be helpful for diagnosing bearing issues, as most faults with bearings are asynchronous. For instance, if I am dealing with a 6300 series bearing, I know to inspect the area in the signature form between 3.3x and 7.5x for spikes in ball pass frequency. It becomes challenging when a calculated frequency or its multiple is close to a harmonic of the running speed. I have encountered instances where the frequency falls around 121 to 122Hz, appearing as 2x line frequency when resolution is not sufficient.</p>
