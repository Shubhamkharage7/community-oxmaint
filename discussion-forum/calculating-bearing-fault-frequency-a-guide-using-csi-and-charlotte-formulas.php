            <p>When accessing your CSI database, input a popular bearing number such as 6313, without specifying a manufacturer, and view the frequency results. You will find a comprehensive list of 6313 bearings from various OEMs in the database. By analyzing the BSF, FTF, BPFO, and BPFI values across different bearing OEMs, a consistent pattern emerges. Typically, BPFO is 3-5 times shaft speed, BPFI is 5-8 times shaft speed, FTF/cage is .4 times shaft speed, and BSF is slightly above 2 times shaft speed. However, outliers with different ball numbers do not adhere to this pattern. Repeat the process with a different bearing style like 22223, and you will observe similar multiplier trends among different OEMs. The sum of BPFO and BPFI multipliers should correspond to the number of balls/rollers in the bearing. Remember, this is a general rule of thumb to follow. - Dave</p>
