            <p>Neil Bloom completely agrees with Vee's response regarding the importance of design parameters and functions. Let's delve deeper into how a function should be defined. Functions outline the tasks a component needs to achieve, explaining why it was installed in the first place. Preserving these functions is crucial in any maintenance program. Components often have multiple functions, including both regular operations and emergency tasks. Defining functions helps identify potential failure modes and their consequences. It's essential to clearly define what constitutes a functional failure based on the specific concerns of your facility.

For instance, a common pump's function may be described as providing a flow of 200 gal/min at 285 psig and 87F. But what happens if it only delivers 199 gal/min at 284 psig and 86F? Is this truly a failure? In most cases, it isn't unless critical circumstances like in the space shuttle require precise specifications. For the majority of pumps in typical facilities, a more general function like "maintaining tank inventory at a consistent level" may be more suitable.

In conclusion, without specific regulatory requirements, you should define functions based on performance standards set by you as the facility owner and RCM program manager. Regards, Neil Bloom, Author of "Reliability Centered Maintenance – Implementation Made Simple" by McGraw-Hill.</p>
