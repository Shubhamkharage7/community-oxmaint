            <p>Error Code 0018: Thermistor Failure
The temperature of the drive has exceeded the specified operating level of the thermistor. It is essential to ensure that the ambient temperature is within the acceptable range to avoid this issue. This error indicates an internal fault in the drive, possibly due to a malfunctioning thermistor used for internal temperature measurements. Consider replacing the drive to resolve this issue. 

Error Code 0001: Overcurrent Issue
This error points to an internal fault within the drive. Even without the motor connected, the drive triggers an overcurrent fault after receiving the start command (consider using scalar control as a workaround). To address this problem, replacing the drive may be necessary. These issues are detailed in the manual and suggest a potential fault in the drive system.</p>
