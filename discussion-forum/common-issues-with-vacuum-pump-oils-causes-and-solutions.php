            <p>The Castrol Tection Extra 15W-40 oil is experiencing issues due to its high-detergent multi-viscosity properties. The detergents in the oil are causing clogs in the oil coalescing filters, leading to oil passing through the filters and potentially carrying over lighter base oil fractions. This results in an increase in oil viscosity over time, leading to higher operating temperatures and wear levels. 

To address this issue, considering switching to a non-detergent, single viscosity motor oil may be beneficial. However, it is important to understand the root cause of the solidification of the oil and choose a product specifically formulated for optimal performance in these conditions. 

Analyzing the sludge in the oil is proving to be a complex task as it involves a mixture of various organic compounds that have oxidized or reacted chemically. Switching to a full synthetic oil, such as a polyalphaolefin (PAO), may improve performance by reducing the presence of residual refining byproducts that could be contributing to the solidifying effect. 

Overall, seeking input from others who have experienced similar issues and their solutions, as well as conducting further analysis in the lab, will help in making an informed decision on selecting the right oil for optimal performance. Your thoughts and suggestions are welcome. - Rich Wurzbach Maintenance Reliability Group.</p>
