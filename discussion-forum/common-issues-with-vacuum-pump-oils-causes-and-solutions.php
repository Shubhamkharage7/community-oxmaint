                    <h3>Question:</h3>
                    <p>In our biomedical research application, we rely on Busch R5-400 vacuum pumps. Initially, our mechanics used Castol Tection 15W-40, a diesel engine oil, in the pumps which proved to be unsuitable due to its high-detergent multi-viscosity properties. Following our recommendation, they switched to the suggested oil, Busch R-590. Despite claims that the oil had turned to sludge in the past, Busch assured us this was not possible. However, upon reverting to Busch R-590, the oil thickened and eventually solidified, leading to pump failure.

Suspecting a reaction between a solvent in the labs and the oil, we transitioned to CPI Engineering CP-9016-100 oil, a hydrotreated parrafin without additives. While initially performing better than the Busch oil, it too is now exhibiting signs of solidification after just 2 months of use. Has anyone encountered this issue before or have insights on which solvent may be causing it? Any recommendations for an alternative oil?

Regular oil analysis has revealed peaks in both the Busch and CPI oils before solidification. These pumps operate continuously for approximately 12 hours daily before being shut down until the following day.

- Rich Wurzbach, Maintenance Reliability Group</p>
