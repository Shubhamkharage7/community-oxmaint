            <p>Eric, I apologize for being away for a bit. Eric Nelson inquired about the sensor's polarity - whether it is PNP or NPN. In other words, does pin 2 need to be pulled high (pin 1) or low (pin 3) to activate the trigger input? Print trigger 1 is utilized as an input for a product detector sensor to initiate the print head during normal printing. The polarity can be configured as either PNP or NPN, with the default being NPN. Switching between the two options can be done by adjusting 2 jumpers on the PCB. Print trigger 2 serves as the reverse print input, although it is not commonly used. It is described in the manual as a bi-directional input offering both NPN and PNP connections. A change in state results in a reversal of the print direction. There is no indication of a polarity selection for trigger 2, suggesting that the opto driving the "Sensor Output" (pin 2) can toggle it between active high and active low. Typically, pin 2 is low unless +15VDC (pin 1) triggers it to go high. I have consolidated all relevant information on both triggers from the manual into a Word document for your convenience. Additionally, I will be sending you the complete Service Manual to the address provided in our private message. I will be unavailable for the rest of the night as I am going to see a band, but I will be back in the late morning. Best of luck, G.</p>
