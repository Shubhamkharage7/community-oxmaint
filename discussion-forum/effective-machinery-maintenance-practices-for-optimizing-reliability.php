            <p>1) When developing a preventive maintenance (PM) plan, it is crucial to determine the specific lubricant needed for each piece of equipment, whether it be grease, mineral oil, or synthetic oil. This should be clearly identified by part number or a suitable equivalent. For example, instead of vague terms like "cooking oil," the PM should specify if it requires virgin olive oil, sunflower seed oil, or soy oil. 2) It is important to carefully consider the type of lubricant required prior to conducting the first PM service. 3) The PM instructions should also include details on the amount of lubricant to be applied:

- Should the oil be refilled until it reaches the top mark, 3/4 mark, or completely drained and replaced?
- For greasing, should the grease be pumped until it emerges from the orifice?</p>
