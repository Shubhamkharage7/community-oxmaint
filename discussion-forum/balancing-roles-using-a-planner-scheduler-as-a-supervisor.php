            <p>It sounds like you are making significant progress and implementing positive changes. Moving your crafts from night shifts to day shifts is a major step towards achieving the benefits of a proactive maintenance approach. Despite concerns about the feasibility of running the plant without night coverage, you have proven that it is indeed possible.

In order to effectively plan and schedule maintenance work, it is essential to have well-trained planners operating within a structured system with defined processes and tools. Ideally, planners should be able to work on a ratio of 1:15, provided they have access to a functional work order system, procurement system, backlog of work, PM system, and a comprehensive job plan library.

As you embark on this journey towards more efficient maintenance planning, it is crucial to establish a dedicated Planner/Scheduler role. It may not be feasible for a supervisor to effectively manage both planning and supervision tasks simultaneously, especially during a CMMS implementation. Building a job plan library and training a potential candidate from your trades pool for the Planner/Scheduler role would be a more effective approach to ensuring successful maintenance planning and coordination.</p>
