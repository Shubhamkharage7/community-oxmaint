            <p>Welcome to the forum! You don't need a special cable, but it's important to use a good quality cable that is screened and follows the USB 2.0 specifications. Next, download the project to the panel using Vijeo. You will then see the boot firmware version of the panel displayed. If the version is lower than your Vijeo version, you should be able to download the project. However, downgrading the panel version can be challenging at times.

For USB flash disks, ensure that it is in the FAT32 format. In the past, it needed to be under 2GB in size, but this is no longer the case with newer panels. Only load the Vijeo project created by the designer onto the flash drive. When connecting the USB flash drive, the panel should recognize it. Check out this video for more information: https://www.youtube.com/watch?v=rL6zshRv7Yo</p>
