The CMMS runs a maintenance orders system, where clients can create and approve maintenance requests. Planners then analyze these requests and create orders for necessary repairs, while also managing spare parts in the stockroom and creating purchase requisitions for required parts, equipment, and services. Maintenance Supervisors oversee assigned orders and distribute them to technicians for execution in the field. Technicians input cost and time data into the CMMS for equipment history, KPIs, and reports, before closing out the order in the system.

Furthermore, the CMMS should offer reporting functionality for analyzing the Maintenance Organization's performance, including costs reports, labor statistics, order status, compliance with schedules, MTBF, backlogs, and inventory control. Training for the CMMS will vary depending on the chosen system, with options for training from the software manufacturer or representative. It is also beneficial to set up a testing environment within the system for self-training purposes.

