                    <h3>Question:</h3>
                    <p>Many MP2 users opt to incorporate the last two digits of the year into their Work Order numbers, such as with an example like 14-0012345. However, resetting this numbering system on January 1st is a task that is often overlooked. If you currently use the year as a prefix for your work order numbers, now is the ideal time to plan for a reset. To do so, navigate to Tools > Setup > Work Orders. A dialog box will open, allowing you to adjust the prefix for your Work Order numbers. It is highly recommended to test this change by creating a few work orders to ensure compatibility with the existing formats in the Work Order and Work Order History modules. Additionally, this is an opportunity to streamline data entry by removing leading zeros from the work order number. For instance, you could update the format to 15-12345. It is also worth considering removing the year reference altogether (e.g. 012345) and using the Request date to track when a Work Order was issued. Remember to test any changes you make by creating test work orders. Wishing you a successful and prosperous new year. Regards, Donal Fleming, Fleming Technical, Inc. Discover more at www.flemingtechnical.com, based in Atlanta, Georgia.</p>
