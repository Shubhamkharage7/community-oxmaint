                    <h3>Question:</h3>
                    <p>Our plant is equipped with a network repeater station situated in an open field, featuring two ProSoft RLX2-IHG network radios that connect our plant network to a nearby well water pumphouse. Powering these radios are two 12VDC marine duty batteries connected in series, along with a 100W solar panel with a charge controller to ensure the batteries stay charged. However, during the winter months, the batteries often experience a drop in voltage due to extended periods of heavy clouds and cold temperatures. This results in the radios shutting down when the voltage reaches around 10V, requiring a hard reboot to power them back up.

We are seeking advice on potential solutions to eliminate the need for a hard reboot. This could involve implementing a device to shut down power to the radios in a more orderly manner or finding a way to prolong the battery life by either maintaining their charge or preventing voltage drain in cold weather conditions. We understand that this issue may not be unique, but it is a challenge we are currently facing. Any insights or recommendations on resolving this issue would be greatly appreciated. Thank you in advance for your assistance!</p>
