                    <h3>Question:</h3>
                    <p>Dear experts,

Recently, a bearing replacement and the installation of a gas seal were carried out on a centrifugal compressor. However, it has been noted that as the machine reaches a constant speed, there is a noticeable increase in vibration levels on both the free and coupling sides. This increase in vibration appears to be directly linked to the temperature of the bearings, with the vibration levels stabilizing once the bearing temperature reaches a steady state. The bearing shoes are currently operating within the acceptable temperature range (85°C).

The bearing type used is tilting pad, and laser alignment was conducted taking into account the thermal expansion specified by the manufacturer. Despite changing the entire axis of the machine's interconnection, the power turbine shows low vibration values. The centrifugal compressor in question is a Solar Turbines C4021TAU-0155 model.

I would greatly appreciate your input on this matter, and have attached additional information and graphics from two separate machine starts for your reference. Apologies for any errors in the English translation.</p>
