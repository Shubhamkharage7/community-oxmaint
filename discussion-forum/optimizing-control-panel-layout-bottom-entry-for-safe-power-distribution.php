<div class='faq-item bg-light p-4 rounded shadow-sm mb-3'>
<h4 class='text-dark'>FAQ: 1. Why should we consider bottom entry for safe power distribution in a control panel layout?</h4>
<p class='text-muted'><strong>Answer:</strong> - By opting for bottom entry, you can ensure a safer design by avoiding potential risks associated with top or side penetrations. This layout allows for better organization of components and efficient power distribution within the panel.</p>
</div>
<div class='faq-item bg-light p-4 rounded shadow-sm mb-3'>
<h4 class='text-dark'>FAQ: 2. How does using a horizontal field terminal strip benefit the control panel layout?</h4>
<p class='text-muted'><strong>Answer:</strong> - A horizontal field terminal strip simplifies wiring connections within the control panel. By connecting components within the cabinet to the top side and field components to the bottom side, it streamlines the layout and improves accessibility during maintenance or troubleshooting.</p>
</div>
<div class='faq-item bg-light p-4 rounded shadow-sm mb-3'>
<h4 class='text-dark'>FAQ: 3. Why is it important to protect the 120-volt power source with an isolator before distribution in the control panel?</h4>
<p class='text-muted'><strong>Answer:</strong> - Installing an isolator for the power source helps prevent electrical hazards and ensures the safety of the components and operators. It acts as a barrier to control the flow of electricity and minimizes the risk of accidents or damage within the panel.</p>
</div>
<div class='faq-item bg-light p-4 rounded shadow-sm mb-3'>
<h4 class='text-dark'>FAQ: 4. What precautions should be taken when connecting the 120-volt source wires in the control panel layout?</h4>
<p class='text-muted'><strong>Answer:</strong> - It is advisable not to connect the 120-volt source wires directly to the bottom of the field terminal strip to avoid potential risks. Instead, consider routing them through a separate terminal strip or clearly marking the bottom terminals for incoming power to maintain a safe and organized wiring arrangement.</p>
