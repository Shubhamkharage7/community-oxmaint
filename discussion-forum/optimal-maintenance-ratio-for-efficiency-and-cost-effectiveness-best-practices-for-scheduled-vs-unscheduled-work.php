            <p>Even plants with the highest uptime goals encounter systems that are designated as RTF. When focusing solely on an RTF system, the planned/unplanned ratio typically hovers around 2% planned and 98% unplanned. It is important to note that some maintenance work may be carried out preemptively before an issue escalates into a breakdown. RTF is not a binary concept, as all systems receive varying levels of planned maintenance depending on their criticality to the overall operation.

Hence, it is reasonable to expect that the ideal planned/unplanned ratio will differ based on the prioritization set by management for individual systems, even in high-uptime plants. While consolidating all work order data into a broad percentage may seem convenient, it may oversimplify the insights and steer decision-making off course.

One common challenge lies in accurately categorizing work orders based on work types. Over time, work types may evolve, leading to shifts in how maintenance tasks are classified. For example, the removal of investigative and condition-based work types from the CMMS resulted in the reclassification of such tasks as corrective maintenance (CM).

The recent introduction of sub-work types provides a more granular view that is essential for distinguishing between planned and unplanned maintenance activities. However, comparing this new data to historical records may prove challenging due to the differences in data categorization, necessitating a fresh start in trend analysis.

While consultants may suggest ideal ratios, these benchmarks may vary and may not necessarily align with your specific circumstances. It is crucial to focus on improving your own data accuracy and trend analysis, as the key to success lies in getting your maintenance data right.</p>
