        <div class="bg-white p-3 rounded mb-3">
            <p>Operating with a strategic approach is crucial for effective maintenance planning. Strategic planning involves considering various aspects in advance, such as scope, sequence, resources, and spare parts. Planned work entails tasks that have been carefully thought out in advance. It's important to determine whether we are in control of the decision-making process or if equipment autonomy is influencing the outcomes. Planned work encompasses scheduled activities like preventive maintenance (PM), predictive maintenance (PdM), and run-to-failure (RTF) tasks conducted based on reliability-centered maintenance (RCM) analysis. This includes detective tasks (failure finding) and corrective actions driven by PdM/CBM insights. Proper organization ensures that a comprehensive work package is delivered by the computerized maintenance management system (CMMS). While most planned work is scheduled in advance, some tasks like RTF or condition-based maintenance (CM) arising from PdM analysis may not be. Efforts should still be made to plan these tasks effectively.  
Conversely, there are also unplanned activities for which we have no predetermined plan or schedule. Examples include unexpected breakdowns, trips, or rework post-PM activities. These unplanned tasks are characterized by their spontaneous nature and lack of preparation. The distinction between planned and unplanned work is crucial for optimizing work efficiency. Planned work can be carried out with reduced downtime, manpower, and cost compared to unplanned work, leading to minimal losses. It's essential to focus on reducing total downtime and combined costs of PM and CM activities to make informed decisions. While the CM/PM ratio is a valuable metric, a sole emphasis on this ratio may obscure the overall cost implications. Striving to meet benchmark targets for CM activities without proper evaluation of costs could be counterproductive. Prioritizing CM based on actual need and strategic planning is key to maximizing equipment lifespan and minimizing unnecessary expenses.</p>
