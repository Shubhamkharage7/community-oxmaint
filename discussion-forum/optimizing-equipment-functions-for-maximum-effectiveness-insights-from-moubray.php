                    <h3>Question:</h3>
                    <p>In John Moubray's RCM2 book, page 302 emphasizes the importance of considering all functions, not just primary ones, when assessing maintenance effectiveness. Secondary functions, although seemingly trivial, can pose significant threats to an organization if overlooked. Therefore, when establishing maintenance effectiveness measures and targets, every function must be taken into account.

On page 304, the book discusses how OEE only pertains to the primary function of an asset, neglecting the fact that assets, including machine tools, have multiple functions each with their own performance standards. This means that OEE does not provide a comprehensive view of overall effectiveness, focusing only on the primary function being fulfilled.

As a practitioner of TPM for over a decade, I disagree with the idea that OEE is the primary measure of TPM. TPM activities ensure that both primary and secondary functions are addressed through pillars like Autonomous Maintenance and Planned Maintenance. By addressing secondary functions, TPM aims to achieve its OEE goal by reducing function loss and function reduction loss.

In conclusion, while TPM has its own terminology for primary and secondary functions, the focus remains on optimizing equipment functions to achieve maximum effectiveness.</p>
