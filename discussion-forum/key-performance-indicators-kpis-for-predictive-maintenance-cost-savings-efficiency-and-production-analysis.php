            <p>Dear Mr. Hatfield,

In my recent book, "The Maintenance Scorecard," I delve into the importance of understanding the reasons behind establishing a predictive maintenance program before attempting to measure its effectiveness. Many maintenance professionals are quick to adopt traditional metrics without considering how these activities contribute to overall company performance.

When assessing the justification for a maintenance program, two key factors come into play. Firstly, cost-effectiveness is crucial in determining whether it is more beneficial in the long run to perform the activity rather than not. This includes considering effects on productivity and other indirect costs. Secondly, cost/benefit analysis is essential, especially in areas related to safety, environmental impact, and capital spending.

To ensure the ongoing effectiveness of a maintenance program, it is important to measure its impact on overall costs and benefits. Additionally, monitoring failure modes that are not predicted in time to take action is another valuable metric to consider.

It is imperative to avoid relying solely on traditional metrics and instead create customized measures that align with the specific goals of your organization. While the term "KPI" is commonly used, it is often misinterpreted as simply referring to measures or metrics. A Key Performance Indicator should accurately represent the overall performance within a specific strategic theme, supported by a range of other indicators.

In conclusion, adopting a tailored approach to measuring maintenance performance will yield more meaningful insights and drive strategic decision-making for your organization.

