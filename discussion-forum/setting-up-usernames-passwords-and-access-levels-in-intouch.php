In the realm of InTouch security, it is crucial for operators to log in by entering their username and password, setting an access level. It is important to note that Microsoft operating system security does not have any influence on InTouch security.

Upon creating a new application, the default username is "Administrator" with an access level of 9999, granting access to all security commands. However, adding a new username to the security list and restarting WindowMaker or WindowViewer will reset the default username to "None" with an access level of "0", restricting access to certain commands.

To grant access to protected functions, the operator's password and access level must match the values specified for the internal security tagname linked to that function. For example, access to a window can be controlled by ensuring the operator's access level is above 2000.

Operators can log in by using the "Log on" command under the "Security" section in the "Special" menu, or by creating a custom login window with touch-sensitive input objects linked to internal security tagnames.

To set up security on your application, utilize the security commands under the "Security" section in the "Special" menu of both WindowMaker and WindowViewer. These commands allow for logging in and out, changing passwords, and managing the list of valid usernames, passwords, and access levels.</p>
