There could be a couple of reasons why you are facing issues. Firstly, Quick Panels require a special serial cable for upload/download as the port on the HMI is not standard RS232. You may need a cable with a built-in converter to ensure compatibility. Additionally, having a real serial port on your computer instead of a USB/serial converter could also make a difference.

Another reason for difficulty in extracting the project file from the HMI could be that the original programmer did not include the project files when transferring the compiled runtime file from Quick Designer to the HMI. According to PME Help, QuickDesigner applications can be imported to QuickPanel targets from a QuickDesigner project file or directly from the QuickPanel target itself. It's important to ensure compatibility with applications generated with QuickDesigner 3.20 or later when importing from a project file (*.PRJ). When uploading directly from a QuickPanel target, make sure that the original application files were stored on the QuickPanel unit with the "Store Project in Panel" option enabled during compilation and download from QuickDesigner.</p>
