            <p>I apologize for the delay in my response as I have been recovering from back surgery and working from home. Each department at our facility has two classifications of maintenance employees: Maintenance Technician Mechanical (MTM) and Maintenance Technician Electrical (MTE). All employees in the department receive on-the-job training (OJT) from senior employees and undergo refresher courses on specific equipment at scheduled intervals. This training is documented in the Human Resource file through the Timecard system using "Special" or Training numbers.

Equipment at our facility is considered ISO-Critical if it directly impacts the production process. Spare parts for ISO-Critical equipment are considered critical spares, as are parts with a lead time longer than the repair time. We keep long lead time spares based on reliability history and have agreements with key suppliers to ensure timely delivery.

Critical spares are not limited to ISO-Critical equipment in our system. For instance, a hydraulic pump for a non-ISO Critical piece of equipment like a Slab Hauler could still halt production if it fails. To prevent downtime, we keep enough spare parts on hand or have just-in-time agreements with suppliers.

Our ISO documents are managed through our Quality Document Management System (QDMS) and Computerized Maintenance Management System (CMMS). The CMMS allows for easy retrieval of ISO procedures by type (ISO, Lock-Out, Gas Committee) for the entire plant or specific departments. Additionally, ISO documents in the CMMS can be attached to work orders or PM procedures, providing documentation of completed work.

