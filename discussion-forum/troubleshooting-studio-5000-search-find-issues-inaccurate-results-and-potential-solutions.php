            <p>If your Programmable Logic Controller (PLC) is still running following a computer crash, there may be a solution to the issue in versions 31 and 32 according to a Tech Note. This known issue may be related to DPI scaling on your display. If you have a Tech Connect account, you can access the note. If not, the first suggested solution involves editing a registry setting. In Windows 10 or 11, you can do this by accessing the Registry Editor and deleting the Logix Designer key under HKEY_CURRENT_USER\Software\Rockwell Software\Logix Designer. Before deleting, it is recommended to export and save the key for future use.

Another solution is to adjust your scale and layout settings to 100% by accessing Display Settings on the desktop. For users of version 31, another solution involves navigating to the LogixDesigner.exe file location and adjusting high DPI settings in the Compatibility tab to System (enhanced). As version 31 is the latest, there are no updates available.

These suggestions may help resolve the issue you are experiencing with your PLC connectivity.</p>
