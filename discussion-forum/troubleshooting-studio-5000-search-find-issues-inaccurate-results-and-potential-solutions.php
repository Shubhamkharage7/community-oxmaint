                    <p>Have you encountered any issues with the Search > Find feature in Studio 5000 being unreliable? Despite having used Rockwell software for almost a decade, I do not recall experiencing difficulties in locating instruction tags within routines using this feature in the past. When searching with one or more routines open, the search results may not display all the instruction tags used in the program. The number of tags found each time varies - sometimes it returns zero, other times two or three, and occasionally it misses an instruction on a branch even though it found the same instruction on the same rung of the same routine. To ensure accuracy in tag search results, I have discovered that closing all open routines, highlighting the main routine (without opening it), and then conducting the search yields the best outcomes. It is unclear whether this issue is a bug in Rockwell software or if there is a configuration error on my end, but troubleshooting our refrigeration system was quite challenging due to this. The provided screenshots illustrate the different search results obtained using various methods. Perhaps the search direction setting needs to be adjusted to scan through all routines from bottom to top for more comprehensive results. Have you also encountered this issue, or is there something I should address on my end before reaching out to Rockwell for assistance?</p>
