        <div class="bg-white p-3 rounded mb-3">
            <p>When it comes to gearbox design, the type of bearings used plays a crucial role. Angular contact rolling element bearings are typically found in this type of gearbox. Improperly set bearings can lead to both radial and axial movement, with radial movement being particularly damaging and resulting in gear failure. For instance, if one rotor's bearing is loosened, the gears may no longer be parallel, leading to excessive loading on the teeth and potential damage such as pitting or scoring.

Moisture content in the oil of gearboxes can be caused by various factors. One common cause is air being drawn in through a breather when the gearbox is hot and then cools down, causing condensation inside. Using synthetic lubricants can help mitigate this issue. It is recommended to have experienced individuals rebuild the gearboxes, paying close attention to bearing settings, as it requires skill. Consider switching to synthetic lubricants for better performance and longevity.</p>
