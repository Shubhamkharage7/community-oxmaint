                    <p><noscript>Posted on December 05, 2017 at 08:06 AM</noscript> Hello everyone, we are facing an issue with our furnace that has seven overhung fans attached to the side wall. In an effort to reduce beat frequency, we have set every other fan to operate at different speeds - 1080 and 980 RPM. The problem arises when the fans change direction from clockwise to counterclockwise during operation. This switch in direction results in a significant increase in vibration levels, reaching up to 1.04 ips in the axial direction and 0.8 ips in one of the radial directions. Additionally, there are impacts observed in both axial and radial directions, with a Crest Factor of up to 7.4 Gs.

The fan vendor suggests that the issue is structural, possibly due to a resonance problem, based on past experiences. However, no additional testing has been conducted to support this claim. Unfortunately, we are unable to perform further testing until January when the furnace will be taken down. I am puzzled by the vendor's explanation of a resonance problem as the high 1X vibration and Crest Factor trends suggest otherwise.

Details about the bearings being Link Belt P363 and belt driven are also worth mentioning. My queries are as follows:

- Can resonance cause an impact at 1X running speed in the axial direction?
- Why does the vibration spike significantly when only three or four fans change rotational direction?
- Is this issue primarily structural or related to balance/mechanical factors?

Thank you for your insights and assistance. Have a great day, Charlie</p>
