            <p>The issue at hand likely stems from the use of vane axial fans, which generate thrust in the opposite direction of airflow. This means that when the fan reverses, the axial force also reverses. It's possible that the locating bearing permits some axial movement to accommodate thermal expansion. If there is axial clearance in the bearing, the shaft may experience slight axial motion, leading to components rubbing and causing increased vibration and high crest factor in the acceleration waveform. To identify the problem areas, it's advisable to conduct further investigation by analyzing acceleration readings at various locations. Utilizing specialized bearing fault detection parameters like PeakVue, SpikeEnergy, and Shock Pulse can be particularly helpful in pinpointing the source of impacting amplitude. Additionally, mounting a dial indicator at the end of the fan shaft to test for axial motion during reverse rotation can provide valuable insights. By placing a bearing ball in the shaft center bore and using wax to secure it, a precise indication of any axial movement can be obtained.</p>
