            <p>Are you familiar with Function Block Diagram (FBD) programming in Logix5k? As a newcomer to this feature, I am currently exploring its functionalities. 

In ladder logic (LAD), resetting a Timer On Delay (TON) can be done using a RESET instruction or by deactivating the rung-in condition. On the other hand, a Timer Off Delay (TOF) requires a RESET instruction for its Reset Timer On (RTO) operation. 

To customize the sequence of lights turning on and off, you can manipulate the LAD code. For instance, removing a specific light from a rung and implementing a Virtual Relay to control the timing sequence. 

If you wish for the timers to reset automatically, consider tweaking the logic by incorporating a certain instruction. 

In FBD programming, further experimentation is necessary. A potential approach involves pairing each light with a LES instruction to compare its timer's values. Additionally, incorporating the signal from a specific timer and modifying it using a conditional statement.</p>
