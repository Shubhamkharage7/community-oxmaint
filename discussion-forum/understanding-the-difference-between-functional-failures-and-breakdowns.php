            <p>In the realm of Reliability Centered Maintenance (RCM), the concept of breakdowns is not commonly used. The driving force behind maintenance decisions in RCM is the potential impact on safety or production. Performance standards are set, such as a target of 10 kg/hr, and failure to meet this standard is the key measure of failure. Unlike traditional maintenance approaches, RCM defines a performance band rather than a single level, allowing for a range of acceptable performance. While some level of performance reduction is tolerated, it should not go beyond the predetermined acceptance standard. In traditional maintenance, breakdowns are seen as a complete loss of function, while RCM allows for certain breakdowns that do not significantly impact operations, such as duty pumps with backup equipment. It is important to address these issues before they escalate, even if the equipment is still operational. For those interested in learning more about RCM, AsiaEdge Singapore is hosting a 5-day workshop in Kuala Lumpur this month. For more information, please contact Easwaran@asiaedge.net.</p>
