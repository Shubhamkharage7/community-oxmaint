<p class='text-muted'><strong>Answer:</strong> 1. What is the difference between functional failures and breakdowns in asset management?
- Functional failures refer to a deviation from the intended function of an asset, such as a drop in production capacity below the specified level. Breakdowns, on the other hand, typically involve a complete cessation of the asset's function.</p>
