            <p>In times of emergency, my approach is to prioritize repair over temporary fixes. Applying any material to an impeller can throw it off balance, unless the material is harder than the impeller itself. It's important to consider the long-term costs of addressing cavitation issues through piping or liquid level adjustments versus quick fixes. Sometimes, a change in construction materials, such as switching from bronze to stainless steel, is necessary to combat cavitation problems. Stainless steel typically lasts longer, but can still show signs of cavitation damage over time due to the pump's performance. Identifying and fixing issues on the suction side of the pump is crucial for optimal operation. Dave.</p>
