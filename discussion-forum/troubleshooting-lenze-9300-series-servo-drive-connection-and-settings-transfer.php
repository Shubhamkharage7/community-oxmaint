        <div class="bg-white p-3 rounded mb-3">
            <p>Apologies for the delayed response. The ETV907 version features a servodrive with an integrated PLC. It is not possible to simply copy the entire program using the keypad. Instead, to transfer the program from an old device to a new one, you will need a card module from Lenze. This module, which is attached in the same location as the keypad interface, allows you to save a bin file to an SD card. You can then use the same card module to download the program to the new device.

Ideally, it is recommended to have access to a backup file and download it using the GDC + PLC software. However, in many cases, the backup file from the machine builder may not be available.</p>
