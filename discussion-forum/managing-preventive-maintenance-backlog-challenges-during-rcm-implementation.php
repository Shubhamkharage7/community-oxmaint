                    <h3>Question:</h3>
                    <p>Greetings! I am currently employed at POSCO, a leading steel manufacturing company located in South Korea. I have recently embarked on the journey of implementing Reliability Centered Maintenance (RCM) at my plant, which specializes in producing hot coils (hot strips). While the RCM concept is promising, I am aware of the challenges faced by the maintenance department at POSCO back in 2000 when the Maximo system was first introduced.

Presently, our maintenance approach at POSCO primarily involves Preventive Maintenance, Condition-Based Maintenance, and a Condition Monitoring System tailored to our specific needs. However, one issue that plagues me is the size of our contracted maintenance crew. With a fixed crew size of 1400 men spread across various mechanical sections such as hot rolling mill, cold rolling mill, iron making, steel making, and stainless steel making, it becomes challenging to meet the demands of each section.

Despite efforts to streamline Preventive Maintenance tasks, such as reducing the number of PM items to align with our ideal plan, many tasks end up being canceled or delayed due to manpower constraints. This hinders our ability to proactively address maintenance tasks as recommended by RCM. The crew scheduling restrictions imposed by the administration, influenced by the partner company's employment policy, further exacerbate the situation.

In my section, the hot rolling mill plant, maintenance schedules revolve around two main types: Weekly Change Down (CD) requiring 130M/D on Mondays and Monthly Shutdown (SD) requiring 180M/D on Mondays. The rest of the weekdays typically require 40M/D. The administration justifies the constraints on crew size by citing the financial risks associated with employing a larger workforce, especially during periods when their services are not fully utilized.

I am eager to learn from other companies facing similar challenges and how they have managed to navigate through such circumstances. The struggle to implement RCM in my company amidst manpower limitations is weighing heavily on me, particularly with 14 hours of production line stoppage (failure) recorded in my two plants. The administration emphasizes the importance of avoiding failures, adding to the pressure of finding a sustainable solution.</p>
