            <p>To ensure optimal performance, it is recommended that the contactor be positioned on the line side of the VFD as instructed. The issue you are experiencing could be related to the safety circuit cutting off power to the VFD when the doors are opened, causing the VFD to power down. If power is restored before the VFD has completely shut down, it may cause confusion when trying to restart the system.

To resolve this issue, it is suggested to implement the following measures:
1. When the safety doors are opened, cut off power to the VFD contactor(s) and activate a time delay timer (TOF) to prevent resetting the safety circuit until the VFD has fully powered down (refer to the manufacturer's recommended time for restart).
