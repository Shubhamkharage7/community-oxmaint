            <p>Your point is valid and raises an interesting question about the advantages and disadvantages of focusing on roving the acceleration versus roving the impact. While reciprocity theory suggests that it may not make a difference, practical considerations in certain structures and situations may prove otherwise. If minimal energy is expected in one direction while impact occurs in another, can the test provide accurate results? In your specific scenario, it is likely that the vertical direction is of most interest. An Operational Deflection Shape (ODS) analysis may be a good starting point to identify any problematic motions. Best regards, Jim P.</p>
