            <p>Hello Nicolas, I found Jim's response to your question very informative, especially regarding the use of 400 lines of resolution. This level of resolution not only saves time but also provides more than enough detail. When it comes to testing baseplates and larger surfaces, I prefer using a small fractional HP variable RPM DC motor. I attach a flywheel type rotor with a specific unbalance weight to create vibrations. By securing the motor in place and adjusting the RPM through different ranges using a rheostat controller, I am able to measure vibrations in various locations and axes. It's common to discover that only one direction is problematic at times.

I track the motor's RPM (forcing frequency) using a photo tach or strobe light. The flywheel attachment I have created includes drilled and tapped holes evenly spaced around the diameter for the option to add extra weight if needed. This customization depends on the mass involved and the need to excite any natural frequencies. In some cases, using a "dead blow" sledgehammer may be necessary. By utilizing this method, I consistently apply force without relying on a hammer or other techniques to excite natural frequencies.

I have successfully used this technique to identify natural frequencies of objects and perform nodal testing on structures and piping. It is important to securely mount the unit to ensure accurate testing, especially for piping. If there are doubts about the safety of the mounting, reverting back to the "dead-blow" hammer process is recommended. If you are interested, I can share a picture of the unit I assembled some time ago.</p>
