            <p>How old is that product? Initially introduced in 1990, it was initially programmed using Logicmaster, a DOS application. This was later replaced by VersaPro and eventually Proficy Machine Edition. After GE and Fanuc ended their joint venture, GE sold its PLC product line to Emerson. The current software offered by Emerson is known as PAC Machine Edition, which allows you to upload programs from the PLC. Your local distributor may provide a temporary license for the software.

I have uploaded Logicmaster to my Google Drive. If the PLC was programmed with Logicmaster, you will need to run it on a 32-bit OS to communicate with the PLC. Additionally, the communications port on the power supply is RS485, so you will need a converter such as USB/RS485 or RS232/RS485 if your computer has a real serial port and a 32-bit OS.

I recommend reaching out to your local Emerson distributor for assistance. Many former GE Supply outlets, now operating as Gexpro, still offer support for the product under Emerson. This would be a good starting point to seek local assistance. Do you have a backup of the program? If so, I can determine the software package used to create it.</p>
