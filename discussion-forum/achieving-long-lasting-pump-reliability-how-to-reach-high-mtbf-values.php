        <div class="bg-white p-3 rounded mb-3">
            <p>Richard, let me further explain the point you brought up: Condition-based maintenance (CBM) is focused on identifying items that are already showing signs of failure. This means that the failure is inevitable, and the signs of degradation are becoming unacceptable, whether it be through vibration levels, TAN numbers, flow reductions, or any other parameter. By recognizing these indicators, we can estimate when the item will eventually fail. As a result, all CBM-based condition monitoring activities are aimed at addressing failures that are already in progress.

On the other hand, preventive maintenance (PM) actions are taken proactively, either requiring the machine to be stopped or not. These actions are taken before the actual failure occurs, sometimes well in advance or just before it happens. PM is based on predictions of an item's lifespan, typically determined by historical data or expert judgment. This approach is generally conservative, ensuring that the item is unlikely to be unusable at the time of the PM activity. The decision to perform PM is economic, aimed at minimizing production losses by replacing or repairing the item early, even if it means sacrificing some remaining useful life. In these scenarios, the items have not yet failed and may not do so for some time. Therefore, all PM activities can be excluded from the list of failures.

It is important to differentiate between CMs carried out post-breakdown or following rework after a PM, as these instances can be considered clear failures. Additionally, modifications or temporary fixes implemented to aid operations may be categorized as CMs in your system, but these should be recognized as breakdowns and should not be included in the failure analysis.</p>
