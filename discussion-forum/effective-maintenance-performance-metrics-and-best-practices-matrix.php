            <p>When comparing reactionary work orders to planned maintenance tasks (such as PM and PdM), the objective is to ensure that the number of reactive work orders is kept below 20%. Reactive work orders refer to jobs that are typically carried out without prior planning, usually within a week's time frame. This type of work often leads to unexpected overtime and the need for on-the-spot planning.

For example, let's consider a scenario where the burner fuel oil pressure is gradually dropping over time. The minimum pressure required for proper fuel atomization is 150 psi, with the normal range being 300-350 psi. At around 250 psi, maintenance is alerted and a shutdown is scheduled. Possible causes are identified (such as issues with the burner tip, fuel pump, or controller), preliminary checks are conducted, and the situation is monitored closely (planned scenario).

On the other hand, imagine a situation where maintenance staff arrives on a Monday morning to discover that an emergency shutdown occurred over the weekend, resulting in the furnace being full of oil (reactive scenario). This type of situation does not fall under the category of Preventive Maintenance, yet it is a common occurrence in many facilities.</p>
