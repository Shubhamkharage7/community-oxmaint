                    <h3>Question:</h3>
                    <p>Hello forum members, we are currently dealing with the issue of cracks in the liners of our 2-stage, 4-cylinder reciprocating compressors used for compressing Boil Off Gas. This problem was discovered during a recent overhaul, with cracks being found in two out of three machines. Has anyone else encountered similar issues before? What could be the possible reasons behind this issue? 

The operating parameters for the compressors are as follows:
- 1st stage suction pressure: 0.15 kg/cm2, temperature: -155 deg. C
- 1st stage discharge pressure: 1.7 kg/cm2, temperature: -65 deg. C
- 2nd stage suction pressure: 1.7 kg/cm2, temperature: -65 deg. C
- 2nd stage discharge pressure: 7 kg/cm2, temperature: 45 deg. C

These compressors have non-lubricated cylinders and pressure packings, with no jacket cooling provided. The liners are made of Carbon steel. If anyone has any insights or solutions to share, it would be greatly appreciated. Thank you.

Attachment(s): Copy_of_14.07.2010.rar (1.66 MB) - 1 version.</p>
