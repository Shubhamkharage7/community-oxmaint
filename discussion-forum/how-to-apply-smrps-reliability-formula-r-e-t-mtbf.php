        <div class="bg-white p-3 rounded mb-3">
            <p>David asks, "How is the reliability formula from SMRP utilized?" The formula, R = e-(t/MTBF), is specific to the exponential distribution with a constant hazard rate. It determines the probability of an item surviving at time 't'. There are various other distributions for equipment/component failures like log-normal, normal, gamma, and beta, each with their unique R(t) definition. The formula aids in calculating reliability at any point in an item's life. Maintenance is necessary when reliability decreases, aiming to restore it to a higher value of 1. Maintenance decisions are based on the consequences of failure, with higher consequences requiring higher reliability values. As reliability decreases over time, maintenance is performed sooner for high-consequence failures and delayed for low-consequence ones. This logical approach guides maintenance scheduling.</p>
