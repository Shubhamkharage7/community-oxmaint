            <p>A decrease in voltage results in a decrease in no-load current. This is due to the fact that, in equivalent circuit terms, as we approach a no-load condition where the synchronous reactance (s) approaches zero, the rotor branch R2/S essentially disappears (as if it were an open circuit). At this point, only the stator leakage reactance and magnetizing reactance remain, creating a constant impedance below saturation levels. Therefore, reducing the voltage across this constant impedance will lead to a decrease in current flow.</p>
