            <p>The relationship between voltage and current in an electric motor is complex, with various factors influencing the results. Higher voltages can lead to increased current due to excitation of the stator and rotor cores, as well as circuit impedance. On the other hand, lower voltages may cause the motor to generate a weaker magnetic field, resulting in more slip and increased losses.

During motor starting, there is a significant spike in current, sometimes reaching up to 21 times the nameplate current. This is known as the 'half-cycle peak.' The initial high current is a result of the locked rotor condition, with both the stator and rotor experiencing full line frequency.

As the motor reaches higher loads, losses play a smaller role, while at lower loads, losses become more significant. This is reflected in a standard NEMA chart showing higher running currents at lower than nameplate voltages.

Issues such as broken or damaged rotor bars can also lead to higher currents, as the motor struggles to generate enough torque, causing increased slip frequency and pulsating current.

The question of where a specific idle current value of 25 Amps comes from is raised, noting that different motor types may have varying idle currents. Rule of thumb estimations are common, but it is important to consider the specific characteristics of the motor in question. In the USA, similar 6-pole machines often have idle currents around half or just over half of the full load current.</p>
