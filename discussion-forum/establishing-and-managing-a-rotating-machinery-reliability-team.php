        <div class="bg-white p-3 rounded mb-3">
            <p>At Electricpete, we have a structured approach to ensuring reliability within our organization. Our process involves separating reliability initiatives into two key areas: Engineering and cost/scheduling, with Engineering focusing on reliability and cost/scheduling falling under maintenance. Any costs related to reliability initiatives must be approved by the respective area PMT (Process, Maintenance, Technical) management team. Generally, these approvals are more of a formality as our requests are typically granted based on our track record of successful initiatives. However, if the total costs exceed the budget, we need to prioritize accordingly.
Our reliability group is also heavily involved in managing spare parts. This includes working closely with vendors to make material changes, upgrading to higher quality products (such as new mechanical seal technology), requesting and approving additional spare parts to be stocked, and addressing any issues with incorrect parts. The ownership of these spare parts lies with the materials and services department, but we are responsible for requesting and approving any changes, additions, deletions, delivery times, and even vendor repair and return quotes through the same department.</p>
