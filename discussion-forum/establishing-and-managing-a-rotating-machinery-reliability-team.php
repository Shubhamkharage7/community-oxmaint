        <div class="bg-white p-3 rounded mb-3">
            <p>Our website features a large 2-unit nuclear site with approximately 150 MW of motor-driven machines and an additional 30 MW of turbine-driven pumps. There are around 1000 machine trains in the vibration program, as well as numerous smaller machines not included in the program. The component/predictive group consists of three individuals specializing in rotating machinery: a pump engineer, a vibration and lube oil analysis engineer, and a motor engineer who also handles infrared thermography and partial discharge monitoring. These engineers are responsible for PM schedules/scopes, refurbishment schedules/scopes, component health monitoring, failure analysis, and overseeing vendor repairs. Additionally, as PDM program engineers, they maintain program procedures, analyze abnormal findings, communicate findings in reports, and prioritize repairs with plant stakeholders.

While there are four other component engineers in the group and a supervisor, none of them work on rotating equipment or predictive maintenance. The workload may seem overwhelming for the three engineers, but they are supported by eight multi-skilled technicians who collect PDM data and specialize in various predictive technologies. The organization has a collaborative approach, where expertise is applied where needed, regardless of the organizational structure. The three engineers work closely together on rotating equipment issues and fill in for each other when needed. They also collaborate with maintenance and other departments to address significant problems.

In fostering teamwork and achieving common goals, it is essential for all parties to take ownership of both reliability and cost/schedule issues. In the past, responsibility for reliability was often assigned to engineering and cost/schedule to maintenance, but a more integrated approach is now favored. Spare parts management is also a critical aspect that plays a role in overall reliability and cost effectiveness.</p>
