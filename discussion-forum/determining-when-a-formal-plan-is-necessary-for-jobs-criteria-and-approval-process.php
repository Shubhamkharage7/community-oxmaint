                    <p>Is your company or maintenance department clear on when a formal plan is necessary for a job? I am considering implementing the following criteria: "If the job is not performed more than twice a year." Approval: Supervisor unless the job exceeds 16 man hours, costs over $5,000 in parts, or involves a system design change. In such cases, department manager approval is required. This heuristic is simple and adaptable for jobs of any size. A straightforward rule is crucial to ensure that employees understand when it is applicable. For instance, if supervisors allow electricians to work without a required plan, the policy becomes ineffective. If a procedure is already in place for a rare job (e.g. on the PM form), following that procedure is sufficient - no additional plan is needed. For jobs involving equipment modifications, an engineering change permit is necessary for review and documentation. It is important to differentiate between an engineering change permit and a job plan. Do you have similar experiences or thoughts on this topic? I welcome feedback on any potential loopholes or shortcomings in my criteria before officially implementing it as policy.</p>
