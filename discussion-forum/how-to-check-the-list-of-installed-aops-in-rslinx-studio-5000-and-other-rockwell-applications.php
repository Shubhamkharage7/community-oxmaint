            <p>When selecting "Add Module" to a backplane or network, the module selection dialog displays a comprehensive list of Module Profiles installed on the computer. This list is dynamic and shows only the available modules based on the selected system architecture. For example, when adding a module to a ControlLogix I/O Configuration or chassis, only 1756 modules will be displayed. Similarly, adding a 1756-ENBT to the chassis will only show Ethernet-based AOP modules. To view currently installed RA Add On Profiles, navigate to the following directories based on your operating system:
- OS x86: C:\Program Files\Rockwell Software\RSLogix 5000\Module Profiles
- OS x64: C:\Program Files(x86)\Rockwell Software\RSLogix 5000\Module Profiles
For Third Party Add On Profiles, browse to:
- OS x86: C:\Program Files\RSLogix 5000 Module Profiles
- OS x64: C:\Program Files(x86)\RSLogix 5000 Module Profiles
Hopefully, this information helps users avoid building unsupported hardware configurations and streamline their module selection process.</p>
