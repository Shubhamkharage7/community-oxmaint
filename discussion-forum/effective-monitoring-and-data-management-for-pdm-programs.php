            <p>Below is my response to the questions posed by liver:

Regarding the responsibility for tracking PDM programs, I believe that the best person for the job would be the respective owners of the PDM. In terms of tracking, it is essential to monitor work orders, time spent, and the total cost expended on all PDM work completed by contractors, repair shops, and consultants. Evaluating the costs of PDM is crucial for assessing effectiveness, identifying trends, failure modes, and other important factors. The criticality of machines should be determined upfront during maintenance development programs. Weight should be placed on critical machine findings compared to simple small motor bearings, categorized into Criticality 1 for equipment with HSE and/or production loss, Criticality 2 for equipment with potential HSE and/or production loss, and Criticality 3 for equipment with no HSE or production loss implications.

It is advisable to compare the performance of one factory to another within the company to identify areas of improvement or decline. To effectively answer these questions, a robust CMMS system with proper coding and expert knowledge is necessary to extrapolate the data accurately. It is essential to have a good understanding of the subject matter before computerizing processes to ensure accurate results and avoid the "garbage in, garbage out" scenario.</p>
