            <p>Hello Guerrec, I have conducted a Reliability Centered Maintenance (RCM) analysis on the electrical system. In my assessment, the Molded Case Circuit Breaker (MCCB) seems to be prone to failure. This may be attributed to the oversized capacity designed by the manufacturer, resulting in non-moving parts condition except for tripping off. I have encountered instances of breaker failure caused by an insensitive tripping curve shift, a hidden failure resulting from sensing element failure. The MCCB manufacturer does not specify a design lifespan or keep track of their installed base, leading to a lack of customer feedback. I have even observed a MCCB from 1965 still functioning normally. To mitigate risks, it is essential to monitor the MCCB's history and conduct root cause analysis for tripping incidents. The most common cause of MCCB failure appears to be hidden spring failures, where the spring's elasticity wears off over time. Thank you.</p>
