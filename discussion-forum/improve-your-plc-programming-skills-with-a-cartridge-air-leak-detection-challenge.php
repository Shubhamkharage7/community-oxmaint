                    <p>Are you looking to improve your PLC programming skills? I have a challenge for you that involves creating a device to detect air leaks in cartridges. This project does not involve any financial gain; I am simply assisting a friend. If you are interested in collaborating, I would appreciate it and highly recommend it as a valuable PLC problem-solving exercise.

The project uses the outdated Panasonic FP0R-C16T PLC model, which has been repurposed for this task at no cost. The device includes various sensors and pneumatic valves to test the integrity of the cartridges.

Key components include:
- X1: Cartridge sensor
- X2: Seal sensor
- X3: Pressure sensor
- Y0: Pneumatic valve for sealing
- Y1: Pneumatic valve for generating vacuum
- Y2: Green light for indicating a good cartridge
- Y3: Red light for indicating a bad cartridge

The logic of the system involves the sensors detecting the presence of the cartridge, sealing it, generating vacuum, monitoring pressure, and ultimately determining the quality of the cartridge. While a press transmitter with analog input would be ideal for more accurate readings, the project currently uses a basic pressure sensor.

