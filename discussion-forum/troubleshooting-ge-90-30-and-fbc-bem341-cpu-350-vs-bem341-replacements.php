                    <h3>Question:</h3>
                    <p>Hello everyone, I am currently facing challenges with the GE 90-30 and FBC BEM341 FIP bus controller. My objective is to replace either the CPU 350 or BEM341. I have a copy of CC90 for uploading and downloading, but it seems there is an issue with the association. Is there anyone who has access to suitable GE software tools that could help with this situation?

According to the guidelines, the FIP Bus Controller release requires the Hand–Held Programmer (HHP), windows programming software IC641CTLxxx Release 2.0 or later, or the FBCLOAD configuration utility for proper network operation. While we have an HHP and memory card, the memory card is unfortunately not supported by the CPU 350.</p>
