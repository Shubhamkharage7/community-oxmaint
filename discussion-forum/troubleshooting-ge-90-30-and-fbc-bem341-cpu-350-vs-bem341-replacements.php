            <p>While rifling through a collection of old CDs, I stumbled upon one labeled Cimplicity Control (CC90) version 2.10 Service Pack 3 from November 1998. Interestingly, the README document on the CD does not mention compatibility with either the BEM340 or BEM341 FIP bus controllers. According to the GFK-1213 manual section on configuration using a handheld device, it seems that the module must be physically installed in the rack for configuration. Surprisingly, this manual does not provide any information on configuring using CC90 software.

On the other hand, the GFK-1295D manual dated 1998 states, "This manual and the online help have been updated to showcase the new features introduced in Version 2.10 Service Pack 3," and it includes detailed instructions on configuring the FIP bus controller. The CD containing this software is relatively small, around 35 Megabytes. If interested, feel free to send me a message, and we can work out a way to share a copy with you. Please note that it will be an unlicensed copy, but it will operate for a 4-day demo period without any restrictions. Embracing outdated technology has always been my forte.</p>
