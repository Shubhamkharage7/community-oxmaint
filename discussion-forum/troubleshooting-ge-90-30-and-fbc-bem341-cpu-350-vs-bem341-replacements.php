        <div class="bg-white p-3 rounded mb-3">
            <p>Facing this challenge is certainly going to be a test of your skills. Throughout my ten years working for a distributor of GE PLCs, I never encountered a customer requesting a quote for a PLC with a FIP bus. Adding to the complexity, it seems that Control90 was the only software that supported hardware configuration for the FIP bus controller, and it was a short-lived offering. However, the VersaPro software that followed didn't include the FIP controller in its hardware catalog. In addition to these obstacles, the handheld programmer is unable to configure systems with CPUs higher than CPU340.

What exactly is the issue at hand? Has the PLC's memory been lost? Is your version of CC90 outdated for compatibility with the BEM341? Can the ladder logic from the CPU350 (with 120K memory) be transferred to a CPU331 (8K memory), CPU340 (16K memory), or CPU341 (40K memory)? All of these CPU models are compatible with the handheld.

It's important to note that the ladder logic program and the hardware configuration are stored separately in the PLC's memory. With CC90, it may be possible to extract the program from the CPU350, but not the hardware configuration. If you have a CPU331 available, you could potentially use the HHP to configure the system with that module. Afterwards, you can connect to the PLC using Proficy Machine Edition to extract the hardware configuration. Utilizing Proficy, import the CC90 ladder logic, switch the CPU model back to a CPU350, and attempt to send that to the CPU350.</p>
