- When asked about the extent of procedure-based maintenance within their maintenance workforce, respondents reported the following:
    - 9% documented 100% of their maintenance procedures
    - 35% documented over 50% of their maintenance procedures
    - 48% documented less than 50% of their maintenance procedures
    - 8% did not have any maintenance procedures documented
*Please note that the question did not assess the quality or value of these documented procedures.*
- When asked about knowledge capture within their maintenance workforce, respondents provided the following responses:
    - 9% had a completed plan for knowledge capture
    - 11% had a budget and plan for knowledge capture
    - 21% were considering a plan for knowledge capture
    - 56% did not have a plan for knowledge capture
    - 3% provided an "other" response
Our research suggests that a potential brain drain may begin within the next five years at some companies, underscoring the urgency to capture valuable maintenance knowledge and experience. We extend our gratitude to the maintenance and reliability professionals who participated in this project, and anticipate sending you an email with advance download links for the report. Stay tuned for further updates. Terry O.</p>
