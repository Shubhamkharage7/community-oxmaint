            <p>Hello Jaz, I want to clarify my previous point. While I believe Reliability Centered Maintenance (RCM) is crucial, it may not be applicable to every situation. Therefore, some level of targeted effort is necessary. This is not a shortcut, but a practical approach. Instead of ranking equipment based on criticality, it may be more beneficial to prioritize based on their alignment with current corporate objectives. To learn more about prioritization techniques like the Analytic Hierarchy Process (AHP), you can visit the following links: [ExpertChoice AHP software](http://www.expertchoice.com/customerservice/ahp.htm) or [Wikipedia page on AHP](http://en.wikipedia.org/wiki/Analytic_Hierarchy_Process). In summary, the AHP process involves listing corporate objectives, having decision makers rank them, analyzing their responses for consistency, and aligning project benefits with these objectives to determine their priority. This method has proven effective, particularly in industries like nuclear where court support has been obtained. Typically, focusing on 20% of assets can yield 60%-80% of benefits, while the next 30% may provide 10-15% more. This concept aligns with the Pareto Principle. Hope this helps!</p>
