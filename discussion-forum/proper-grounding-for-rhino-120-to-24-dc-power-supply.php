            <p>It is important to note that while some DC power supplies are typically left to "float" without grounding the low side, it is recommended to have a ground reference for all supplies. During a compliance test exam in the 90s, it was highlighted that every supply should have a ground reference to prevent any potential risks associated with floating supplies.

Not only does grounding the low side of a DC supply provide convenience, but it also helps in minimizing ground currents or noise interference that could impact sensitive analog signals. Leaving a power supply floating can lead to a buildup of high voltage levels in comparison to ground, especially when near cables with high dv/dt like those found in the output of a VFD.

As a precaution, establishing a bare commons bar in the main electrical cabinet, isolated from the mounting plate but connected to a single point to ground, can serve as a reliable grounding solution. This setup not only ensures safety but also helps in saving space by reducing the need for multiple DIN guide terminals.</p>
