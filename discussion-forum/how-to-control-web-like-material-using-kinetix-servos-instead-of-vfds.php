            <p>I partially agree with the recommendation to utilize "MAJ/MAS instructions" for controlling the run/stop functions. Another crucial element to consider is the MCD (Motion Change Dynamics). The MAJ instruction directs the servo to operate at a specific velocity and acceleration, serving as a one-time directive that sets the speed and acceleration parameters when triggered. The servo will continue to run at these set values until a new command is issued. On the other hand, the MCD command is used to adjust the velocity or acceleration of the servo. If there are changes in motor speed due to variations in line speed or feedback from load cells or dancers, continuous MCD commands will be necessary. 
When operating VFDs in torque control mode, utilizing servos can present added complexity. While it is achievable, servos are typically more focused on velocity and position control. Therefore, adjustments must be made to efficiently run a servo in torque mode without encountering errors related to position or velocity.</p>
