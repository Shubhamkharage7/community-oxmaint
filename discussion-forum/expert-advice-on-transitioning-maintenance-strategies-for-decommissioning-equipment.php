        <div class="bg-white p-3 rounded mb-3">
            <p>Proper maintenance is essential for equipment that is still in operation. However, it is crucial to consider the future plans for the equipment after decommissioning. 

Option A: If the equipment will no longer be used, it is wise to cut costs by ceasing all maintenance activities. Consider salvaging any useful parts or selling the equipment as a whole. Alternatively, you could donate it to a mechanical engineering school or decide whether it is cheaper to abandon it on-site or demolish and dispose of it.

Option B: If the equipment will be decommissioned for a short period, it is recommended to perform some preventive maintenance tasks to prevent damage from corrosion or dust buildup. Consult the equipment manufacturer for additional recommendations, such as operating the equipment for a certain number of hours per month. If spare parts replenishment or training is not necessary for maintenance, it can be postponed until the equipment is to be put back into operation.

Option C: If there are no definite plans for using the equipment again after decommissioning, consider the options in A or B and make a decision to either retire the equipment permanently or keep it maintained for a potential future use.</p>
