            <p>I firmly believe in the saying "use it or lose it." When it comes to spares versus backup equipment, I distinguish between items kept in the warehouse as spares and those installed as backup or stand-by equipment to replace the lead machine when needed. This brings me to the concept of lead and lag equipment. I have multiple pieces of backup equipment, such as four pumps where typically only one is required, but occasionally two pumps are needed. To ensure proper functioning and prevent issues like electrical problems and false brinelling, I have a rotation system in place. By programming each pump to run for a specific percentage of time, we can avoid any simultaneous failures and ensure compliance. This proactive approach is important because a backup unit that is not regularly used may fail prematurely. I have witnessed instances where bearings deteriorated from lack of use, rendering the machine inoperable after being on stand-by for just a year. It is crucial to rotate and maintain backup equipment to prevent such failures.</p>
