                    <h3>Question:</h3>
                    <p>We have encountered numerous failures in a vacuum pump application involving a C-mount motor/pump alignment and a Woods #9 SureFlex Coupling with EPDM inserts. Out of 4 similar pumps, only one continuously experiences issues such as stripped teeth or torn inserts. Despite low vibration levels with all vibration at 1X, no laser alignment can be performed due to limited space. To simplify changes, we have started using a two-piece coupling insert. Strobing the coupling reveals distortion, with the metal center retaining ring beginning to bend. Evaluating the coupling size, it appears borderline between #9 and #10, potentially causing the repeated failures. Other pumps with #9 couplings do not exhibit similar problems. Any suggestions on measurements or further steps to identify the issue with this specific pump would be appreciated. Kindly share your thoughts with Rich Wurzbach at rwurzbach@mrgcorp.com.</p>
