            <p>I agree with OkiePC that using the click function is likely your best option, even if you opt for one of the cheaper clones like mitsubishi; the software isn't free. One suggestion for improving the steps is to use an integer variable in increments of 10 instead of relying on internal memory bits to control the step sequence. This allows for easier modification and addition of steps when needed. For example, setting Step 0 as IDLE (nothing running), Step 10 as Start process (startup routine), Step 20 as opening valve 1, Step 30 as running pump 1, Step 40 as counting pulses and comparing to the required value, Step 50 to stop the pump when the value is reached, Step 60 to close the valve, Step 70 to wait for a set time period, and Step 80 to return to Step 20 once the time is up.

By implementing this system, valve 1 will open if the step tag is between 20 and 60, and the pump will run if the step tag is between 30 and 50. To add an additional step, simply insert it between existing steps. For instance, if you want to add a trickle valve across Valve 1 to slow the pulses when it nears the required value, insert new steps between 30 and 40: Step 35 to open valve 2 and close valve 1, then adjust the comparison at Step 60 to refer to valve 1 as Step 35 and add valve 2 opening between Steps 30 and 40.</p>
