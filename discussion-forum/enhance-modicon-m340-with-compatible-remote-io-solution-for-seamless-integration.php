            <p>If you are considering using read_var / write_var function blocks with the processor port, I would advise against it. While it is technically possible, the time spent programming and debugging these blocks outweighs any potential benefits. In my experience, using IOScanner is a much faster and more efficient option. Here are some alternatives to consider:

1. Utilize read_var/write_var blocks with the processor port, although this may require a significant amount of time for programming and debugging.
2. Install a NOE module and utilize the IOScanner service for seamless communication with ModbusTCP slaves.
3. Install a NOC module for flexibility in utilizing scanner services for ModbusTCP or EtherNet/IP Class 1 communications, or both simultaneously.
4. If your processor supports CANOpen, consider using CANOpen IO modules.

Personally, I prefer option (3) as it offers versatility in handling different protocols and is considered the future-proof choice by Schneider. When it comes to IO, I tend to avoid the OTB series from Schneider due to personal preference, despite their good performance. I find other vendors' IO devices to be easier to work with. 

