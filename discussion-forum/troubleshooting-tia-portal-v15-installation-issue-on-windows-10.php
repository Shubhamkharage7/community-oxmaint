            <p>Problem solved! I want to express my gratitude for the helpful comments from everyone. It's important to share how I resolved the issue because it's a common occurrence that can affect many users. The reason the installer keeps launching after each PC reboot is due to an entry created in the Windows Scheduler by the TIA Portal installer. This entry should typically be removed once the installation is complete. If the installation is interrupted, the entry remains in the scheduler.

One possible reason for the installation not completing successfully was highlighted in a forum post on the Siemens website. The recommendation was to uncheck the Project Migration option for WinCC flexible 2008 SP2/SP3/SP5 SQL installation. By following this advice and excluding that option during installation, the process was able to finish without issues.

For a visual reference, I have included screenshots below. Thank you once again to everyone for your assistance.</p>
