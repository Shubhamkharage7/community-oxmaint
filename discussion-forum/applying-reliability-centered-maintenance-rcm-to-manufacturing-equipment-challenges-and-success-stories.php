                    <p>Hello everyone, I have a burning question that has left me with little to no answers. I am reaching out specifically to experts or individuals with experience in this matter to gather your insights. Can reliability-centered maintenance (RCM) be applied to manufacturing equipment? Has anyone attempted RCM and is willing to share their experiences? I have successfully implemented RCM in manufacturing facilities but have faced challenges with equipment used by operators. These equipment include AHU, sub-stations, and gensets. The reason for my inquiry is that the consequences of failure differ within a production plant. In the decision diagram, the original consequences for RCM are environmental, safety, operational, and non-operational. When it comes to equipment used by operators, environmental consequences are not a concern and there are additional consequences beyond operational. Therefore, potential consequences could include safety, operational, quality issues (previously under operational consequences in RCM), customer dissatisfaction with forgiveness, customer dissatisfaction leading to loss of business, and non-operational consequences. I appreciate any contributions and insights, and I also invite you to participate in my previous post on the PM Survey. Warm regards,</p>
