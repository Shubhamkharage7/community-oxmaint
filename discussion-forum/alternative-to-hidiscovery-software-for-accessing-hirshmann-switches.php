            <p>Upon conducting a brief search, I did not come across a Hirschman-specific NSE script. However, references to HiDiscovery V1 and V2 were found. HiDiscovery V1 operates at the L2 protocol level, while HiDiscovery V2 functions on the L3 protocol utilizing SNMP. As V1 is L2-based and non-routable, an arp scan may be sufficient for discovering switches. Tools like arp-scan-windows send ARP requests to specific LAN segments, providing MAC addresses without further context. Alternatively, utilizing nmap with a command like nmap -sn -n --send-eth 192.168.1.1-254 can reveal the manufacturer associated with MAC addresses, helping identify Hirschman devices. For V2, leveraging SNMP tools may be effective. The differences between scans on HiView's "Discovery" tab and the standalone "HiDiscovery" application are worth exploring for a more comprehensive understanding. It is worth noting that Hirschmann has introduced a new software, HiView, as an alternative to the previously mentioned tools. For more information, refer to the provided links.</p>
