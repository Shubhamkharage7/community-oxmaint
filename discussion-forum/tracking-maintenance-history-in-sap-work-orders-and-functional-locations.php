If you're looking for more information, you can visit the SAP Help Portal site for resources on this topic. Simply do a search on the internet for "SAP Help Portal." Regarding your technicians recording daily activities, consider having them record these activities against the relevant SAP Work Order in SAP instead of on an Excel spreadsheet. You can place this information in the Work Order text box, although retrieving this information in a report may be a challenge. Others may have found creative solutions to this problem.</p>
