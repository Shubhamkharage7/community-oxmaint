        <div class="bg-white p-3 rounded mb-3">
            <p>Hello Colin, while I cannot provide a specific recipe, there are some key principles to keep in mind when storing rotor shafts. 

1. For slender shafts stored horizontally, it is crucial to turn the rotors to prevent any potential issues. If vertical storage is an option, that is the preferred method. Ideally, a 270-degree rotation is recommended, with the frequency depending on the shaft's slenderness. Typically, shafts should be turned every 3 to 6 months.

2. When dealing with large rotors like those found in steam and gas turbines, vertical storage is best. If vertical storage is not possible, consider rotating the rotors every 2 months. Additionally, storing them in a container filled with dry nitrogen at a specific water column pressure can help maintain their condition.

3. Motor shafts are usually quite sturdy and may not require frequent turning. However, if there are concerns about their stiffness or weight, it is advisable to turn them over every 6 months.

4. It is important to incorporate these maintenance practices into your CMMS to ensure they are consistently implemented and not forgotten.

5. When storing rotors vertically, it is essential to ensure they are truly vertical, either by hanging freely or placing them in a designated container. Avoid resting the shaft end at an angle against a rack or wall.

These tips will help maintain the quality and longevity of your rotor shafts. Thank you, V. Narayan.</p>
