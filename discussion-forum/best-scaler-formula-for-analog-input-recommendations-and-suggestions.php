To find the offset, consider that an increase of 4000 in the raw value is equivalent to twenty-five percent of the way from the minimum to maximum, resulting in a scaled value of 25. Therefore, a decrease of 4000 corresponds to a scaled value of -25. This leads to the final formula: Scaled value = (0.00625 * Raw Value) - 25.

To validate this formula, let's test it with a couple of sample calculations. First, for Raw Value = 4000, Scaled Value = (4000 * 0.00625) - 25 = 0. Second, for Raw Value = 20000, Scaled Value = (20000 * 0.00625) - 25 = 100. Lastly, for Raw Value = 12000, Scaled Value = (12000 * 0.00625) - 25 = 50. This method helps ensure accuracy and consistency in the calculations.</p>
