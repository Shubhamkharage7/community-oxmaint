            <p>1- To clear a faulted SLC, try slowly cycling the key from program, to remote, to run (and back to remote if needed) in an attempt to resolve any recoverable faults. You can also set a system bit (S:1/8) to automatically reset a recoverable fault upon power cycle. Changes to this bit must be made offline and then downloaded. If the fault persists, it may be unrecoverable. Remember to cycle through all positions twice.2- The 5/03 PLC features two serial ports, requiring one to be bridged to Ethernet for remote connections with a VPN Router. Consider using the Digi One IAP for DF1 to Ethernet gateway or the Stridelinx VPN router from Automation Direct. Upgrading to a 5/05 PLC could eliminate the need for the gateway, but may require modifications to hardware connected to the 5/03's DH485 port.</p>
