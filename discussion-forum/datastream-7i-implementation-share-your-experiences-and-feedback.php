After implementation, the benefits of this hard work and investment can be enjoyed, especially when utilizing information systems for Key Performance Indicators (KPIs), which should be configured properly. Setting up proper KPIs in the information systems should be done by subject matter experts like Operations and Maintenance (O&M) users, rather than IT personnel. It is crucial to have the users' knowledge levels evaluated before implementing any software to ensure they can effectively utilize it. Bringing all users to a minimum level of understanding is necessary for successful implementation. 

To sum up, the key lies in how well users understand and use the software, rather than the software itself. This perspective is based on personal experience with SAP.</p>
