            <p>In my experience working with SAP and similar software, it is evident that there are more stories of failure than success when it comes to maintenance implementations. Often, SAP is utilized at the corporate level for finance, while another CMMS handles maintenance tasks. The decision to implement SAP typically lacks input from the operational and maintenance teams, as SAP's primary focus is not on maintenance. It is uncommon to hear of a SAP implementation costing less than $1,000,000, excluding hardware expenses. SAP is generally considered too costly and complex for small to medium-sized businesses with fewer than 100 employees. Datastream, which we use alongside Oracle for our database needs, is also not inexpensive but more affordable compared to SAP. From my 11 years of experience working with the same CMMS system, I find that SAP is viewed more as accounting software with a maintenance module.</p>
