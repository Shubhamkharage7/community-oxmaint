            <p>The introduction of the newer Plus 7 Performance model Series B brought an updated operating system, leading to changes in the Out-Of-Box-Experience (OOBE). These units currently do not support the initial assignment of an IP address during OOBE setup. While the screen for this feature is available, it is not yet supported. It is unclear if or when this functionality will be added in the future. Additionally, these units no longer come with the latest firmware pre-installed. Similar to Logix controllers, users must flash the firmware to their desired version using a USB thumb drive. ControlFlash cannot be used during the OOBE process, but can be utilized afterward. After the firmware upgrade, users can set the IP address through the Configuration Screen as usual. In my experience with updating the PVP7, I have always had the network cable connected without noticing any difference if the ethernet cable is unplugged. I have primarily worked with Series B units, with Series C simply being a stainless steel version of the same. There have been no reported firmware issues that differentiate the Series C from Series B. Currently, only the Standard Edition is available in Series D, which still uses the old Windows operating system and does not feature the new OOBE unless there have been recent updates that I am not aware of. It is important to note that technology evolves quickly, so information may change over time.</p>
