            <p>Matt, you are facing an intriguing challenge ahead. Allow me to provide some initial suggestions, with more contributions likely to follow. In regards to your Propulsion System (including gas turbines, large gearboxes, hydraulic and fuel pumps, and babbitt shaft bearings), consider spading off the compressor inlet and flue gas to stack flanges and maintaining the rotors in a low-pressure dry air or nitrogen atmosphere with a dewpoint of -10 to -20F. It is also recommended to turn the shaft 270 degrees every month and regularly clean and lubricate all fuel injection nozzles and piping, especially when using heavy fuels. Additionally, make sure the large gearboxes are filled with lubricating oil, adding nitrogen purge and rotating the rotor assembly monthly if seals are likely to leak. If feasible, replace the babbit bearings with brass sleeve rings or lubricate shafts at babbit with graphite paste if not. When it comes to Compressed air systems (piping and compressors), implement a low dewpoint air/nitrogen purge to maintain it under 2" water pressure. For Potable water systems (piping, pumps, and tanks), consider using a low-pressure dry nitrogen blanket after draining the system to reduce costs. Sewage systems (piping, treatment system, holding tanks, and pumps) require individual attention, as do Salt Water Cooling (pumps, piping) systems that may need sea water drained and replaced with fresh water. Applying a nitrogen blanket or permissible bactericides may also be necessary. Fresh Water Cooling (pumps, piping) systems should either be kept running or drained and blanketed with nitrogen, with cooling tower internals painted accordingly. Hydraulic systems (pumps, piping, valve blocks, and rams) should be kept filled with hydraulic oil, with system running occasionally if possible. Electrical equipment (diesel engines, generators, switchboards, transformers, motors, automation systems) will need occasional operation and maintenance. Salt Water Ballast systems (pumps, piping, tanks) should be treated like other salt water systems, while Miscellaneous gearboxes (winches, etc.) should follow similar maintenance procedures. It is important to note that exceptions may apply depending on the specific circumstances. Also, consider addressing Hull corrosion and barnacle issues, as well as giving attention to the rudder and its actuator.</p>
