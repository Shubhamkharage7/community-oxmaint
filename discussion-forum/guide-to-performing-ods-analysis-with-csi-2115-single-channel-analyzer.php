            <p>The CSI-2115 analyzer is capable of measuring shaft speed and phase. For more information on using the 2115 for balancing, refer to this link: http://www.osti.gov/scitech/biblio/552750/. In order to conduct an ODS test with the 2115 for a vibration frequency of 1x shaft speed, connect a tachometer to the BNC on the analyzer and measure vibration amplitude filtered at 1x shaft speed. Utilize Excel Scatter Plot to create scaled X-Y plots of vibration amplitude and linear dimensions to measurement points, ensuring all points are in the same plane and have the same sensor orientation. Points with a phase angle less than +/- 90-deg should be plotted in-phase, while those with a phase angle greater than +/- 90-deg should be plotted out-of-phase. Make sure to have a tachometer (or strobe with signal output) and a coaxial cable with a BNC connector on hand. - Walt</p>
