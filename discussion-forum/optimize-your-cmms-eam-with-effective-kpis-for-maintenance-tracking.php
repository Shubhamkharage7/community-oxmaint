            <p>A few years back, I established a set of indicators to create a Key Performance Indicator (KPI) known as "Work Order Content Evaluation." The main purpose of this KPI was to ensure that failure codes were consistently added to all demand maintenance work orders, including Emergency (ER) and Corrective (CM) orders. The criteria for evaluation included Labor Hours, Failure Class Code, System Code, and Asset# & Location.

With our implementation of MAXIMO 6.0 handling over 30,000 work orders monthly, we initially implemented a multi-level screening process involving Tech Writing leads, Group Planners, and Supervisors. Unfortunately, this manual process resulted in a high percentage of work orders with missing data, around 30-40% of the time.

To improve this situation, I developed a web-based interface utilizing Oracle views to monitor and display the current percentage of work orders meeting the four criteria. This resulted in a significant improvement, with the Content Evaluation reaching over 95%. This success led to a positive shift in the maintenance organization's data collection strategy, supported by upper management.

Over time, I expanded the criteria from 4 to 10, incorporating additional factors such as Sub-System Code, Remedy Code, Component Code, and more. Despite a temporary drop in Content levels after the expansion, they quickly rebounded and consistently remained above 95%, driven by management's adoption of the metric as a performance goal.

Lessons learned from this experience emphasize the importance of automating the monitoring process and developing measurements within the application to ensure adherence to documented processes. While an EAM system is a valuable tool for monitoring maintenance performance, customization can be both an advantage and a disadvantage. Additionally, the importance of proper failure coding examples and data normalization cannot be understated.

I have successfully achieved low failure segregation levels by utilizing a structured data collection approach. Those interested can refer to the attached word document containing metrics tracked for Work Order content. Ultimately, the goal was to establish disciplined recording practices to enable the development of additional metrics and tools to enhance maintenance efficiency.</p>
