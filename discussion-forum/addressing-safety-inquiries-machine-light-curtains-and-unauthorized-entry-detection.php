            <p>Our system features two reset buttons for each door to ensure operator safety. The inside button is pushed, the door is closed, and then the outside button is pressed, all within a specified time frame. This process serves as a reminder for operators to confirm that the area is clear. The light curtains, which operators reach through, require manual resets. Some machines are divided into zones, and if moving parts breach these zones, they are automatically reset unless the operator's light curtain is also breached simultaneously, in which case a manual reset is necessary. Additionally, small areas within the light curtains are safeguarded with horizontal light curtains that are linked to the ones operators reach through, necessitating a manual reset.

In our latest systems, we have implemented area scanners to ensure that no one is inside before the doors can be reset. In my personal opinion, it is advisable to mandate manual resets for light curtains and other presence sensing devices such as horizontal light curtains or area scanners to confirm that the space is clear. If manual resets are not feasible, the dual reset button approach (inside and outside the protected area) can be considered along with mandatory manual resets for every operation.</p>
