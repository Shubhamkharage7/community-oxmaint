2) When a light curtain protects a large area, the entire area must be deactivated in the event of a hazard. If the hazard involves a small opening, the entire safety zone should be shutdown unless additional light curtains are used to segment the area. However, this scenario can pose a potential nightmare if someone resets the machine while someone is in the crawlspace.</p>
