                    <h3>Question:</h3>
                    <p>In the past and present, it has been challenging to ensure that millwrights provide useful historical data in the CMMS. Previously, we would only receive information such as "replaced mechanical seal" as history. To improve the quality of our reporting, we have outlined a list of criteria for what constitutes good quality history, although not all jobs may require all criteria to be met.

While there has been some improvement in reporting, there is still work to be done. To address this, we have introduced a template with nine specific steps that millwrights are required to follow when providing history. However, our CMMS system is not currently set up to accept templates in the work history section, resulting in a cluttered history when information is copied and pasted from a Word document.

We are interested in learning about how other companies handle the following aspects: what constitutes useful or adequate history, where this information is stored, how to ensure millwrights are on board with providing history as part of their job, and feedback on our nine-step history requirements outlined below:

1. Initial complaint
2. As found deficiencies
3. Parts replaced, modified, or adjusted
4. As left condition (deficiencies corrected)
5. Deviation from specifications - indicate if data/spec sheet was filled out and if the SJP was updated
6. Possible root cause of failure (process, maintenance, design, end of life)
7. Recommendations for improvement
8. PM frequency change required (yes/no and why)
9. Technicians who worked on the job

We are eager to gather information and insights on these topics.</p>
