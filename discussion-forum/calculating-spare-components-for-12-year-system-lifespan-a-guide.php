            <p>When managing spare parts requirements, it is essential to categorize them into two groups: 1) "just in case" parts needed for unplanned maintenance due to random failures, and 2) "just in time" parts needed for scheduled maintenance due to time-related failures. The "just in case" parts should be kept in inventory as their demand follows a Poisson discrete probability distribution. For forecasting, the quantity needed for a specific time period should be considered. On the other hand, "just in time" parts are ordered based on preventive maintenance schedules determined by Materials Requirements Planning (MRP). These parts include replacements based on condition monitoring or failures discovered during maintenance checks. In some cases, challenges may arise such as parts with multiple failure modes, repairable parts, or parts failing before their scheduled replacement time. Despite these complexities, strategic planning and accurate forecasting can help streamline spare parts management processes.</p>
