            <p>Josh, after revisiting my paper from the SMRP conference, I, like you, was surprised by the lack of change in corrective maintenance despite a significant decrease in preventive maintenance activities. One would expect a reduction in the number of preventive maintenance tasks to result in a proportional decrease in corrective maintenance, as less equipment would be prone to failures. Fortunately, I still have access to the original data used in the paper, which provided some insights. 

During the initial PM optimization project, we eliminated 686 PM tasks and introduced 109 new ones. Additionally, we removed 80 equipment items from the PM program and reclassified them as Run-To-Failure. This shift resulted in an increase in corrective work orders, as noted in the reliability manager's report. 

The fact that some equipment items were later added back to the PM program due to failures or other reasons suggests that the overall number of corrective work orders remained constant. This could be attributed to a decrease in infant mortality failures, indicating less frequent intrusive maintenance.

Moving on to your second question about the role of a Reliability Manager, it is crucial for all engineering, operations, and maintenance personnel to have a solid understanding of equipment reliability. Companies are now implementing strict guidelines, such as requiring documentation of the equipment's failure mechanism and justification for any changes based on RCM principles. 

Traditionally, maintenance responsibilities were divided among departments, but a reliability program aims to integrate these functions. RCM analysis helps create a well-balanced equipment strategy focused on preventing failures with minimal resources. Communication between departments is key to maintaining equipment effectively, as even minor changes to PM activities can impact the overall strategy. To ensure controlled changes to the PM program, all requests must go through the Reliability Manager for evaluation and approval.

