            <p>I completely agree with Just's suggestion of conducting a cost/benefit analysis. One valuable tool for this analysis is APT spares, which calculates the cost of not having a spare part by multiplying the risk and consequence. The risk includes the rate of failure and potential catastrophic scenarios. The consequence is influenced by factors such as twinning, market conditions, and goodwill. It is also important to consider mitigating factors, such as implementing a vibration monitoring program to increase procurement time. Visit http://www.aptools.co.uk/papers/stockholding.pdf for a detailed discussion on factors to consider when deciding on stockholding.</p>
