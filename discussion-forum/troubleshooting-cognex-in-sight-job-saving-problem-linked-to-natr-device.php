                    <h3>Question:</h3>
                    <p>Hello, I am encountering an issue with my Cognex In-Sight jobs not saving due to a network error. I suspect this issue is linked to the connection with a 1783-NATR and a missing custom rule. Despite searching for the custom rule without success, I have tried utilizing custom rules from the In-Sight Explorer Help pages that outline the camera's ports. Additionally, I attempted using ports known to work with a Dataman scanner over NATR, but none of these solutions have resolved the problem. The error occurs when attempting to save a job while connecting to the In-Sight Sensors and selecting the specific sensor in question.

Has anyone successfully saved a job through a NATR device? I have been able to save job files on a camera directly connected to the network or by physically connecting to the camera subnet. This led me to believe that the issue may be related to the custom rules of the NATR. Any experiences or insights would be greatly appreciated.</p>
