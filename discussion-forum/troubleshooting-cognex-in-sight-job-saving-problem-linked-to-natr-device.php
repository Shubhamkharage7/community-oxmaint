            <p>I encountered a similar problem while using the In-Sight Vision Suite for my D902 series cameras. Despite my efforts to create enough rules, I found that there was a limit of 5 rules that could be created, which was not sufficient for optimal functionality. Resorting to WireShark, I identified the necessary ports for different functions, such as TCP 443 for HTTPS, TCP 8084 for Web HMI HTTP, UDP 1069 for In-Sight Discovery, TCP 50000 for the data channel, and TCP/UDP 123 for NTP. While this allowed me to connect online and view programs, I encountered difficulties with editing and saving programs. These tasks resulted in issues with the cameras, including increased processing times, network errors, and malfunctioning web HMI until the cameras were power-cycled. This experience was based on using In-Sight Vision Suite, which was a relatively new software at the time. For potentially better results, consider utilizing In-Sight Explorer. If you discover a solution to this problem, please share it for the benefit of others.</p>
