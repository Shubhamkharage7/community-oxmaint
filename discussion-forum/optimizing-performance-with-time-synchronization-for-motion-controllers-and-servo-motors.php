        <div class="bg-white p-3 rounded mb-3">
            <p>To ensure proper synchronization, check the "Enable Time Synchronization" box for all controllers on the Date/Time tab of the Controller Properties dialog. Press the Advanced button in the same dialog and adjust the "Master Override" setting on the 320ERM controller with the most drives to a value lower than the others, such as 100 if the default is 128. Confirm the grandmaster clock controller using this dialog. If necessary, bring the clocks of the other controllers into closer alignment for synchronization. Consider utilizing a network switch with PTP capabilities as the grandmaster clock for improved synchronization, as switches can sync to NTP whereas PLCs cannot (although there is an AOI available).</p>
