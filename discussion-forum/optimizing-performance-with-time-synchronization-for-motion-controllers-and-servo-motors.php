                    <h3>Question:</h3>
                    <p>Hello there, our equipment features motion controllers, servo motors, and Power Flex 527, all requiring time synchronization for optimal performance. Our company plans to incorporate Factory Talk Asset Center in the coming months, necessitating all equipment to be connected to a single network. While most machines are already networked in preparation for this transition, we are experiencing time synchronization alarms on three machines. These alarms, such as Clock Sync Alarm and Clock Jitter Alarm, are affecting two machines running on 5069-L310ERM and one on 5069-L320ERM controllers. To address this issue, it is crucial to have only one system time master on the network, ensuring consistency across all machines.
For example, Machine 1 has its first port set to 192.168.0.10 connected to the main network, with the second port linked to Kinetix 5500 at 192.168.1.1. Similarly, Machine 2 and Machine 3 are configured in a similar manner. One potential solution could be using NAT routers on each machine to help maintain the controllers on the same network while ensuring the drives recognize the connected controller as the time master.
Additionally, there seems to be a performance lag when online with any of these controllers, possibly linked to the time synchronization issues. This lag is evident when the mouse cursor intermittently becomes the blue processing circle, indicating a delay in execution. Addressing the time synchronization problem among the three controllers may improve overall system performance.</p>
