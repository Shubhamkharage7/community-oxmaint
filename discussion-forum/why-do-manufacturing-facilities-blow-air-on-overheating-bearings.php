        <div class="bg-white p-3 rounded mb-3">
            <p>Frequently, Operations will try to address nuisance alarms in the control room by using unconventional methods. I once witnessed a plant where they believed it was beneficial to use a water hose to cool down a BFP sleeve bearing. However, this practice inadvertently created a thermal signature with a temperature disparity between the top and load zone of the bearing. This could potentially lead to unforeseen issues in the long run.</p>
            <ul class="post-meta mb-0">
                <li class="post-date"><i class="uil uil-calendar-alt"></i><span>25-09-2024</span></li>
                <li class="post-author"><i class="uil uil-user"></i><span>Olivia Brown</span></li>
            </ul>
        </div>
        
        <div class="bg-white p-3 rounded mb-3">
            <p>Throughout my career, I have witnessed various instances of a common operational misconception - the belief that if something is hot, it must be cooled using water or air. Another problematic practice I have observed is when maintenance personnel come across a hot bearing over the weekend and simply apply grease without proper evaluation. This neglectful behavior can result in serious consequences for the equipment.</p>
            <ul class="post-meta mb-0">
                <li class="post-date"><i class="uil uil-calendar-alt"></i><span>25-09-2024</span></li>
                <li class="post-author"><i class="uil uil-user"></i><span>Wesley Jenkins</span></li>
            </ul>
        </div>
        
        <div class="bg-white p-3 rounded mb-3">
            <p>During my time at a paper mill, we utilized large pillow block bearings on certain drive shafts that were connected to a shaft with U-joints. Unfortunately, the automatic greaser would often over-grease the bearings, causing them to run hot for a period of time. When operations noticed the heat, they would use an air hose to cool it down. Despite the overheating, we never experienced a bearing failure, so it's unclear if the air hose intervention was effective. Interestingly, once the air hose was applied, there was hesitance to remove it even after the bearing had cooled off.</p>
            <ul class="post-meta mb-0">
                <li class="post-date"><i class="uil uil-calendar-alt"></i><span>25-09-2024</span></li>
