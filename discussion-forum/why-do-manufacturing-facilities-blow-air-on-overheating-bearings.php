        <div class="bg-white p-3 rounded mb-3">
            <p>I have observed this issue in numerous locations. The issue arises when blowing air on the bearing housing, as it cools it from the outside in, leading to a decrease in clearance. If necessary, direct the airflow onto the shaft to cool the bearing from within. Moreover, excessive greasing can generate enough heat to also impact the clearance. An example of this occurred at a newly commissioned plant last January, where attempts to manipulate controls led to a major malfunction. The compressor, positioned next to a louvred vent in a room with two other compressors, received cold air because all fresh air entered through the louvre. To combat this, they wrapped the air end with heat tape and tricked the control system into allowing the compressor to start below the recommended temperature. Ultimately, the compressor failed when it started at a temperature lower than recommended.</p>
