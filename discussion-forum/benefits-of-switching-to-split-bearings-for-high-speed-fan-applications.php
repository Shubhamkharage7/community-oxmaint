            <p>Solid ring bearings are generally expected to offer better long-term reliability compared to split bearings. However, the trade-off between this reliability and the extra installation time required is a complex consideration. One type of split bearing, circumferentially split inner ring bearings, is commonly used to enhance bidirectional thrust capabilities in bearings like "four-point contact" or "gothic arch" designs. These bearings have lower radial ratings compared to deep groove bearings, possibly due to the split design. Axially-split bearings, which are split to ease installation, may also have a load derating compared to deep groove bearings. This information can usually be found in the manufacturer's catalogue.

There are three main disadvantages associated with split bearings. Firstly, the seam may visually resemble a race defect, potentially leading to false calls by vibration analysts. Secondly, the seam serves as a focal point for stress and may be where fatigue spalling begins. Lastly, the seam can create additional stress on the rolling elements. Design modifications, such as adding chamfers or radii, can help address some of these concerns, but they may introduce other complications. Proper installation of split bearings is crucial due to the increased complexity of their design.</p>
