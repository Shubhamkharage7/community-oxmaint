            <p>Hello everyone, I wanted to clarify that failure mode and root cause are distinct concepts in the field of Root Cause Failure Analysis (RCFA). When discussing failure mode, we are referring to the potential cause or the initial stage of the RCFA process. In RCFA, failure mode is often seen as a hypothesis. Following a failure incident, the plant is now back up and running. It is important to note that Root Cause analysis is typically not conducted when the plant is not operational. Thank you.</p>
