            <p>Is this compressor equipped with an integrally geared design (IGC)? The operational speed seems to indicate that it is. The unique overhung impeller layout of IGCs can result in significant vibration levels at the seals, even though the vibrations at the bearings remain minimal. I once had a conversation with a senior engineer from a leading dry gas seal manufacturer who mentioned that their seals could handle up to 50 mils of vibration at 40,000 rpm continuously. Fiftly mils! IGCs are particularly prone to imbalance issues caused by fouling. For instance, one of our refineries has an IGC in hydrogen recycle service that requires frequent shutdowns for cleaning. Consider conducting a lateral rotordynamic analysis to gain insights into shaft displacements near the seals due to impeller imbalance.</p>
