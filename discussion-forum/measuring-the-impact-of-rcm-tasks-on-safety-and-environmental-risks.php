            <p>Measuring or estimating the probability reduction can be done by calculating the ratio of "number of times a device fails and causes a problem (that the failure finding task did not catch first)" to "number of times the device fails". Gathering data for these calculations may take some time, so it is often necessary to estimate. Another method to estimate is by subtracting the likelihood of the failure finding task detecting the issue before it impacts operations from 100%. This information is typically obtained during RCM analysis.</p>
