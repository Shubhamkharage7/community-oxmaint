            <p>Bob O described it as "That's a feature, not a bug." This choice was made by the Allen-Bradley (A-B) IDE programmers in the past. It's important to note that not all PLC IDEs follow the same convention. By searching for "otl otu bit display green" on Google or this site, you can find past discussions on this topic. A-B IDEs display relay logic based on whether the relay instruction is an input or an output. Inputs show the result of a contact instruction's output rung, while outputs show the operand bit's value for output coils. The display reflects the operand bit value retrieved at a specific point in time, which may not align with when the instruction was assessed. 

One drawback is that in online mode, you don't see the actual result of the rung at the time of evaluation. This would be resource-intensive in terms of computation and memory usage. However, one advantage is that the display remains consistent in both online and offline modes. The screenshot provided likely depicts offline mode, as the OTU input rung would appear as True otherwise.</p>
