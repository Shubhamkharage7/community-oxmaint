        <div class="bg-white p-3 rounded mb-3">
            <p>Excessive loading can cause a motor to overheat, potentially leading to burnout or even a fire hazard. A slight overload will gradually increase the temperature, while a significant overload will result in a rapid temperature rise. To prevent damage, it is crucial to disconnect the power source before the motor reaches dangerous temperatures and address the root cause of the overload before restarting the machine.

Ideally, motor temperature should be directly measured using thermistors in the motor windings, although this method can be expensive and is not commonly utilized for small motors. An alternative approach involves using a Motor Thermal Overload or Motor Breaker, which mimics the temperature increase within the motor by heating bimetallic strips in the overload mechanism.

Unlike a miniature circuit breaker, a Thermal Overload lacks protection against sudden large overcurrents but is sensitive to long-lasting overcurrents and phase discrepancies. On the other hand, a miniature circuit breaker offers protection against sudden large overcurrents but lacks the thermal component found in a motor breaker.

Motor breakers combine the features of an MCB and a Thermal Overload but are typically pricier compared to individual MCBs or Thermal Overloads. Adding an auxiliary pack allows a motor breaker to switch off the mains via a contactor for added safety. Some users may prefer a combination of thermal overload and fuses for protection against long slow overloads, single-phasing, and sudden large overloads.

Ultimately, the decision to use motor protection devices depends on the motor type and the designer's considerations regarding motor overheating. In some cases, it may be deemed acceptable to forgo protection for small motors in less critical applications where burnout consequences are minimal. However, for larger motors, it is essential to implement appropriate overload protection measures to prevent damage and ensure safe operation.</p>
