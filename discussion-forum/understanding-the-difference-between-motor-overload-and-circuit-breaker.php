            <p>As mentioned previously, in a motor control setup, the Motor Circuit Breaker (MCB) is typically located before the contactor, while the overload is located after the contactor. Overloads usually come with pins for easy attachment to the contactor. Some motor circuit breakers can be paired with a link module or kit to connect them to the contactor, creating a combination starter. It is advisable to use a coil module if the coil is located on top of the contactor, and a line-space adapter may be required for the MCB to meet the combination starter rating.

It is important to note that the AUX contacts of the overload and circuit breaker are reversed. In the event of a MCB tripping, the N.O. contact is used to turn off the contactor coil, as the tripped state is 'off'. Conversely, when an overload trips, the N.C. contact is used to deactivate the contactor coil.

It should be noted that overloads do not offer significant SCCR protection, so it is recommended to use a fuse in front of the contactor/overload. On the other hand, many MCB/combination starters are rated between 35k-65k SCCR and may not require fusing if the rating is adequate.</p>
