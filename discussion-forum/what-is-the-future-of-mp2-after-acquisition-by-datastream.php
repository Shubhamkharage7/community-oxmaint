            <p>It's possible that MP2 isn't going away anytime soon, considering its longstanding presence of over a decade. MP2 was originally built on a paradox database using Borland's embedded PAL language, similar to Access with a VB overlay. It was well-suited for small businesses. In contrast, MP5 was developed using Client-Server architecture, utilizing Database servers such as Oracle and SQL-Server, commonly found in medium to large companies. Datastream acquired CMMS Rapier in the late 1990s, released the windows version as MP5, and later introduced MP5 Datastream 7i as the web-enabled version for larger enterprises with high user traffic.

When MP5 was introduced, Datastream aimed to target a market segment dominated by Oracle Relational Database Systems. Due to a merger, Datastream is now positioned to compete with SAP, shifting away from MP2 which was once their flagship product for small businesses. E-procurement is not something we are familiar with, but one reason for upgrading from MP5 to 7i is to accommodate the growing number of users and simplify configuration for individual computers through the web-enabled version, making it easier for the IT department to provide support.

Despite the availability of newer products, many "old" systems continue to function effectively. The decision to upgrade ultimately depends on whether the current system meets business needs adequately.</p>
