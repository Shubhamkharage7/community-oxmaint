                    <p>Hello everyone, I am currently interning at a small car manufacturing company, focusing on implementing Preventive Maintenance strategies to decrease downtime. Despite my background in sensor technology, I lack experience in maintenance. My main task involves analyzing downtime records, which primarily stem from machine breakdowns, as well as potential issues like water leaks and pump noises. The company's maintenance routine consists of daily visual inspections by operators and periodic maintenance by a small team of maintenance personnel.

To address this challenge, I plan to classify equipment based on manufacturer, type, and operational status. I will calculate Mean Time Between Failures (MTBF) for similar equipment to establish appropriate maintenance intervals. Additionally, I intend to utilize Pareto analysis to identify the top 20% of equipment responsible for 80% of downtime, and apply Failure Mode and Effects Analysis (FMEA) to prioritize critical failure modes for daily inspections.

