            <p>Ali, I'm not aware of any specific standard that definitively determines the criticality of equipment. The reason for this is that the criticality of equipment is highly dependent on its function and the service it provides within a given system. As a result, it cannot be universally calculated as a standard measure, as it varies based on company policies, functions, and services.

However, there are several standards available to assist in calculating the criticality of equipment, based on three main factors: economic criticality (including loss of profit opportunities, maintenance costs, and operating costs), safety criticality, and environmental criticality. These factors are all interconnected and can be assessed through a matrix relationship to determine the likelihood of failure.

It is important to note that these factors are influenced by a multitude of variables. I hope this information has been helpful to you.</p>
