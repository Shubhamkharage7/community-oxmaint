            <p>The Risk Matrix is a widely utilized method that assesses risks based on probability and consequence. The X axis represents probability in a logarithmic scale ranging from once in 100 years to multiple times a year, while the Y axis represents consequence for HSE, Asset Loss, and Production loss. Safety scale ranges from first-aid case to multiple fatalities, and Production loss scale ranges from <$1000 to >$1000k. By evaluating each system or sub-system on this matrix, we can determine the level of risk it poses, ranging from negligible to extreme. Safety Critical Systems like Fire & Gas Detection or Emergency Shutdown Systems typically fall under the Very High or Extreme Risk category. High Risk is equated with High Criticality. This matrix is commonly used in activities such as RCM, RBI, or IPF, with a similar approach known as PRN used in FMECA analysis.</p>
