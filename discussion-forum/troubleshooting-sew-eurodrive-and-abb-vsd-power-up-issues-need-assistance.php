        <div class="bg-white p-3 rounded mb-3">
            <p>If your 3-phase power supply was functioning properly and then suddenly stopped working, there may be an issue with your incoming power supply that is causing damage to the front end of your drives as soon as power is applied. This problem is often related to the design of the drives, which are intended for solidly grounded Wye (Star) systems and not Delta power systems. Drives with CE filters typically have MOV networks to protect the rectifier components. If the resistance to ground is lower than the grounding resistor, or in the case of a delta source, the only path to ground, the MOVs can be destroyed, leading to surges that damage the rectifier.

Another potential issue could be extreme common mode noise causing zero-cross notching, leading to immediate surges through the diodes and damaging them right away. This type of notching is often caused by older DC power controllers or AC controllers with GTO thyristors, which can force an SCR into a premature off condition.

Open transition Star-Delta motor starters, particularly on high-torque motors, can also cause damage. During the transition, a brief loss of torque can result in the motor slowing down slightly. When reconnected in Delta, residual magnetism can keep the motor in Regen mode, causing it to be out of sync with the line frequency and leading to a current spike that can damage nearby electronic components.

During commissioning, it is important to investigate any potential scenarios that could be causing these issues. If the problem only arises once the machine is fully connected to the power supply, it is likely the cause of the damage to your AC drives. It is important to thoroughly review your commissioning sequence to identify and address the root cause of these issues.</p>
