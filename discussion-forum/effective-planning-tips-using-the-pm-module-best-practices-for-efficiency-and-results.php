            <p>When is a work order considered planned? A work order is considered planned when the Planner can address the 5 M's - Manpower, Material, Machines, Methods, and Management. Not all work orders require planning; for example, work orders involving troubleshooting mechanical, electrical, and instrument issues may only need a detailed problem description and a contact name. However, having a current P&ID, motor elementary, or a list of previous work orders related to the location/equipment can help complete the work order. It is essential to estimate the required crafts, personnel, and time for scheduling purposes.

When planning a work order, consider the following:
- Manpower: Determine the staffing needed for the job and whether multiple crafts, contractors, or vendor representatives are involved.
- Material: Identify the required materials and check if they are stocked or need to be purchased. Explore alternative sources like free stores, warehouses, or salvage yards for materials. Coordinate with the storeroom for excess materials.
- Machines: List the necessary equipment such as welders, cutting torches, jackhammers, and cranes needed for the repair. Ensure this information is included in the work order operation.
- Methods: Determine if printed materials like manuals, pictures, drawings, and repair procedures are necessary for the job. Attach this information to the work order as shop papers.
- Management: Apart from scheduling, manage tasks like parts delivery, material staging, vessel fire watches, contractor coordination, and vendor management. Ensure proper closure of work orders and document updates.

Remember, planning a work order requires field observation with copies of work orders, a tape measure, and a digital camera. Consider what is needed for the repair and how it should be carried out.</p>
