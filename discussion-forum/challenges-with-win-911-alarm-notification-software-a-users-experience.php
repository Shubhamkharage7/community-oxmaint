        <div class="bg-white p-3 rounded mb-3">
            <p>I recently encountered a similar issue that was resolved once I changed the login details. To reiterate, it is important to update the login information in various places where it is stored for your service account to function properly. This includes ensuring that all Win911 services are using the service account, as well as updating the login/password in IIS's application pool to match the service account.

When managing the Win911 app, always log in with the service account rather than your personal account. Additionally, enabling detailed debug logging can be helpful in troubleshooting issues. To enable detailed debug logging:
1. Stop all WIN-911 AppServers (IIS) and WIN-911 Runtime Services.
2. Navigate to c:\inetpub\wwwroot\Dispatcher in Windows Explorer.
3. Open Web.config with Notepad and locate the string "loggingFlags" using the Edit\Find tool.
4. Change the loggingFlags value from "Default" to "Debug."
5. Save the file and close Notepad.

It is essential to keep such software off IT-owned assets whenever possible, although this may not always be feasible in today's environment.</p>
