Unfortunately, I do not have access to power monitoring equipment to track incoming power fluctuations. While there is no filter currently installed, the system has operated without problems for years, indicating that this may not be the root cause of the issue. Any assistance or advice on this matter would be greatly appreciated. Thank you.</p>
