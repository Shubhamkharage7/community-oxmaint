            <p>Hello Terry,

When comparing RCM and FMEA, the key difference lies in their approach. Firstly, FMEA lacks a structured method for defining functions and functional failures. Secondly, FMEA does not inherently include a systematic problem-solving process. However, in practical terms, the importance of this point diminishes as decisions must ultimately be made during the FMEA process.

For instance, if a FMEA is being conducted to identify and eliminate design flaws, the likely solution would involve redesigning the equipment. Alternatively, if the purpose of the FMEA is to determine maintenance and spare parts requirements, incorporating RCM decision logic may be beneficial. In this scenario, the distinct advantage of RCM is its emphasis on defining functions and functional failures.

The question then arises: how crucial is it to define functions and functional failures? While some experts like Moubray suggest allocating 20% of analysis time to this aspect, I hold a different perspective. Understanding the equipment's functionality is important, but dedicating a significant portion of analysis time solely to this may not be necessary. Moreover, documenting this analysis may not always be essential.

In cases where a thorough FMEA has been conducted and appropriate decision-making logic applied, the necessity of RCM may be diminished. As for PMO, it is true that various versions exist, but a well-executed FMEA can often render PMO unnecessary.

Contrary to popular belief, I view RCM more as a design tool rather than a maintenance strategy. Its application to existing plant systems can be laborious and time-intensive. While RCM advocates may argue its merits, the reality is that companies can end up wasting resources - both time and money. Moreover, the notion that higher cost equals higher quality is not always accurate in the realm of RCM.

In line with Howard Penrose's suggestion, the approach chosen for analysis should align with the specific requirements. It is entirely plausible to integrate elements of RCM, PMO, and FMEA within a single analysis. While PMO remains our primary method, there are instances where incorporating RCM concepts becomes necessary - particularly when dealing with unknown equipment functions.

In conclusion, the key is to adapt the analysis approach to suit the unique needs of the situation. Feel free to visit our website at www.pmoptimisation.com.au for more information.

