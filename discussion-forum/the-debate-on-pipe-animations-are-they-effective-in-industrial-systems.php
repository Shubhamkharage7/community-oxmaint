            <p>Examples of these dynamic 'pipe animations' and 'phase steps' may refer to visual indicators on air and fluid pipes to show whether they are 'active' or 'inactive' through color animations. Some argue against the use of pipe animations, favoring alarming systems, interlocking indications, and textual displays to indicate the plant process's status. However, in my experience, operators find purely text-based information challenging to interpret. Many operators prefer a few simple graphical indicators, with emphasis on simplicity and clarity. Colors such as grey for inactive, bright green for active, yellow for warning, and red for faulted are commonly used to convey information efficiently. In our system, only grey and green colors are used for pipe animations to ensure clarity and simplicity.</p>
