            <p>One possible reason for a leak at the inboard versus outboard seals could be a discrepancy in the seal's fit on the shaft. This could be due to a score mark on the shaft where the seal attaches, or the shaft being slightly smaller or the seal slightly larger on one side. However, proper maintenance of the oil level should prevent this issue. Several factors can affect the level setting, with ventilation being a key consideration. It is crucial to ensure that the bearing reservoir and oiler cup are adequately vented. Check for any obstructions in the vent holes, such as paint or debris. Additionally, make sure the oil piping is correctly positioned in line with the bearing rotation to avoid creating a low-pressure area that could lead to oil leakage. Setting the pump's level accurately is essential, as relying solely on external markings on the casing can result in problems. If possible, use a boroscope to observe the bearing's operation and determine the precise oil height needed for optimal recirculation. In some cases, the difference between too little and the correct oil level can be as small as 3/32 of an inch. It is crucial to set the oiler ball at the exact point to prevent overfilling and leakage. Consider installing a level glass for better monitoring. For detailed information on setting up these units correctly, refer to Trico's website or page 10 for insights on overfilling issues. Airflow around the vent is also crucial to prevent problems. Share any additional findings with us. Rich Wurzbach, Certified Lubrication Specialist-STLE Maintenance Reliability Group, shares insights based on firsthand experience in a nuclear power plant.</p>
