                    <h3>Question:</h3>
                    <p>Experiencing a recurrent issue with an Ingersoll-Rand 12x28 LPA pump can be frustrating. The pump features shaft seals that are designed to protect from external substances but not necessarily to contain oil. Despite having an oiler for easy maintenance and an oil level indication on the outside of the pump, oil leakage has become a mystery over the past few weeks.

After diligent monitoring and replacing a defective oiler, the pump continued to leak oil heavily, especially from the inboard seal. This is perplexing since typically, if there is excess oil, leakage would occur at the outboard seal - which is not the case here. Various theories have been pondered, including the mechanism of oil transfer along the shaft and the functioning of the oiler.

Considering installing external oil level indicators may be a helpful solution for future maintenance. However, the current focus remains on understanding and resolving the ongoing oil leakage issue. Any insights or assistance would be greatly appreciated.</p>
