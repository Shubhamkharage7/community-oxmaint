- Common causes of vertical vibration issues in Overhang Fans can include improper bearing installation, misalignment, inadequate lubrication, unbalanced fan blades, or structural issues.
  
2. How can bearing replacement and coating removal impact vertical vibration issues in an Overhang Fan?
- Bearing replacement can help resolve vibration issues caused by worn-out or damaged bearings. Coating removal can eliminate any imbalance issues caused by uneven coating distribution on the fan blades, which could contribute to vertical vibrations.
  
3. Why is shaft laser alignment important in addressing vertical vibration issues in Overhang Fans?
- Shaft laser alignment ensures that the fan components are properly aligned, which can reduce vibration levels and improve overall performance. Checking alignment twice helps to confirm that alignment issues are not contributing to the vertical vibrations.</p>
