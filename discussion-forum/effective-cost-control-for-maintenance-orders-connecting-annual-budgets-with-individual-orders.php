            <p>Our project management information system (PMIS) generates MCIS reports, which are customized versions of the MCI8 cost analysis report. One notable change is the inclusion of individual orders as a key figure, as well as the conversion of monthly costs to period costs for alignment with financial reports. This user-defined analysis is conducted within the SAP platform.

By utilizing this analysis tool, users can easily compare the total actual order costs with the financial overview for the month. They can also drill down to specific details such as the top 10 orders and highest labor costs. It is important to ensure that any cost center discrepancies are journaled or linked back to a maintenance order to maintain consistency between total costs in orders and total maintenance costs in finance. The controller finalizes the blanket order following manual journal entries, and users can configure the system to automatically recalculate activity rates to balance out the cost center.

