<h4 class='text-dark'>FAQ: 2. How can fleet uptime percentage be calculated and why is it an important KPI for preventive maintenance assessment?</h4>
<p class='text-muted'><strong>Answer:</strong> - Fleet uptime percentage is calculated by dividing the total operating time by the total scheduled time. It is an important KPI as it reflects the reliability and availability of mining fleet equipment after preventive maintenance activities.</p>
</div>
<div class='faq-item bg-light p-4 rounded shadow-sm mb-3'>
<h4 class='text-dark'>FAQ: 3. What is the significance of mean time between failures (MTBF) as a KPI for preventive maintenance in mining fleets?</h4>
<p class='text-muted'><strong>Answer:</strong> - MTBF is a crucial KPI that indicates the average time elapsed between equipment failures. A higher MTBF value suggests better reliability and efficiency of preventive maintenance practices in mining fleets.</p>
</div>
