            <p>A few years back, we focused on boosting our oil refinery's production capacity by installing new pumps with un-grouted base frames. It appears that only the skid's feet are secured to the plinth, leaving the rest of the base frame empty. Despite minimal issues with vibrations, the lightweight construction of some pumps caused alignment challenges. The pedestals tend to flex significantly as the pump heats up and the pipework expands, even with proper layout and support measures in place. Investing in high-quality skids is crucial to avoid long-term costs and issues, especially when using API pumps.</p>
