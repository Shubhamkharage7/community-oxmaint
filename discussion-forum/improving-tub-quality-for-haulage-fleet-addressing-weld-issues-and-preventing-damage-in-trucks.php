The dilemma we are facing is whether to invest over 40 hours of additional labor and materials to rectify the issue and request a design change from the OEM, or focus on training our operators to handle the tubs more carefully to prevent damage caused by loaders. Our fleet is experiencing a high rate of vehicle damage, such as broken frames, mirrors, windows, steps, and rails, leading to an availability rate of only 85 percent. 

We are seeking suggestions on the best approach to address the damage problem and maintain the fleet's operational efficiency. How can we effectively tackle this challenge and prevent future issues with our tubs and trucks? Any insights or recommendations would be greatly appreciated.</p>
