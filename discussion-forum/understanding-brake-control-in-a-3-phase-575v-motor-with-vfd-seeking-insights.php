            <p>Priyank Tripathi inquired about a diagram found on the panel cover, wondering if it is related to a thermistor. The diagram is actually for detecting thermal overload using a bimetallic thermostat, not a PTC thermistor. Nord catalog page 117 provides more information on this. During normal operation, there should be continuity (< 50 mΩ) between the two leads. If the motor overheats, the thermostat will bend and open the circuit, signaling to shut off the motor to prevent further damage.

Nord drives typically come with a dedicated digital input for this purpose, unlike what is found in the PowerFlex literature. For the on/off function of the thermostat, the closest option in PowerFlex literature is configuring an input as 'SW Enable'. Alternatively, if a PLC is controlling the drive, wiring the thermostat to it for a stop command is possible. In a simple 3-wire start/stop circuit, wiring in series with the stop button is an option, but it may make troubleshooting more difficult as an overtemp is not easily distinguishable from a normal stop command.</p>
