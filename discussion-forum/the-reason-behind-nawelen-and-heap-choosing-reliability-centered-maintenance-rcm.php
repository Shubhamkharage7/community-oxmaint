            <p>It is important to note that RCM was not actually named by Stanley Nowlan and Howard Heap. Here are the details: 

In 1965, a rudimentary decision diagram technique was developed, and in June 1967, a paper was presented on its use at the AIAA Commercial Aircraft Design and Operations Meeting. This decision diagram was initially used for the maintenance steering group on the Boeing 747 aircraft, leading to the creation of the document MSG-1. 

Subsequent improvements from the use of this decision diagram were incorporated into a second document, MSG2, two years later. MSG3 was then utilized to create scheduled maintenance programs for various military aircraft, including the Lockheed 1011, Douglas DC10, Lockheed S3 and P3, and McDonnell F4J. 

The term RCM, named after author John Moubray, distinguishes industry-based (RCM2) from the original airline industry-focused RCM developed by Nowlan and Heap. Moubray, a student of Nowlan, sought to create a decision diagram applicable to land-based industries. 

For those interested, RBM (Reliability-Based Maintenance) is related to Condition-Based or Predictive Maintenance. Hopefully, this information clarifies any confusion. Rolly Angeles, Reliability Consultant.</p>
