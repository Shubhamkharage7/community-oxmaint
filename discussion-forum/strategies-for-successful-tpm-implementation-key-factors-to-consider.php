In my current implementation experience, our RCM Manager couldn't execute the program solo. After facing numerous challenges in gaining program credibility, he advocated for additional staff. He assigned individuals from different technical backgrounds who showed RCM potential and were eager to learn. This diverse team included a CMMS expert, mechanical technician, instrument technician, electrical technician, utility operator, and mechanical engineer. This approach created a self-sufficient group capable of performing Functional Failure Modes and Effects Analyses (FMEAs) and developing well-crafted job plans based on direct experience, minimizing disruptions for the technical teams. Despite some resistance from the technicians who view us as unnecessary outsiders, we have successfully supported multiple sites and buildings. However, our workload remains heavy, often being diverted by essential projects like transitioning to upgraded CMMS software versions to ensure data accuracy for operational continuity. 

