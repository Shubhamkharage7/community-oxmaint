            <p>Hello Erik, I trust that everything is going well for you. The issue of failure modes can be challenging, especially when it comes to understanding the causality of these failures. Moubray's RCM2 book provides a comprehensive overview of this topic. In this brief discussion, causality refers to the level of detail required to describe a failure mode. According to SAE JA1011, it is crucial to delve into causality to develop an effective failure management policy. For instance, ISO 14224 outlines failure modes at the highest causal levels, such as a motor failing to start. While these failures may resemble functional failures, they may not be sufficient for developing policies or conducting analyses. There is a wealth of resources available online for this topic, including commercial sources that provide data on failure modes. While some may find it off-putting to pay for this information, it holds value. Here are some cost-effective sites that may be helpful if you are willing to invest in this area: OREDA, Reliability Information Analysis Center, and EPRI. Additionally, the Hydraulic Institute offers valuable resources on failure modes, which you may find by exploring their website. Best of luck in your research!</p>
