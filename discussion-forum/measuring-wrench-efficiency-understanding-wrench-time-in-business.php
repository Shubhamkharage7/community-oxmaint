            <p>Wrench time, in our CMMS system, refers to the hands-on time spent by mechanics on a specific job assignment. This time is tracked by having mechanics report the hours they dedicate to a task, excluding time spent on housekeeping, lunch, training, and meetings. The hours logged can be connected back to a specific work order for accountability and tracking purposes.</p>
