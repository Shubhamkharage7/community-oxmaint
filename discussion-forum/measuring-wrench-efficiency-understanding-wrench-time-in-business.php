        <div class="bg-white p-3 rounded mb-3">
            <p>In maintenance management, the concept of wrench time is crucial for measuring the actual time craftsmen spend working on tasks. It excludes travel time, spare parts and tool pickup, waiting times, and discussions related to the job. This is different from the time charged on work orders in some companies. By measuring wrench time, organizations can identify inefficiencies and prioritize areas for improvement. Conducting a job study is essential to accurately measure wrench time, and there are various methods that can be used. Personally, I find that work sampling is the most cost-effective method for achieving precise results.</p>
