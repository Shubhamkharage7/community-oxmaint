        <div class="bg-white p-3 rounded mb-3">
            <p>When switching from a 60Hz to a 50Hz frequency, there will be a noticeable shift in pumping characteristics. The performance curve, which shows the relationship between flowrate and head, will move to the left and downward. This means that for a given flowrate, the head will be lower. This phenomenon is akin to reducing the size of the pump impeller. The change in pumping efficiency is expected to be minimal, although it's uncertain how it will impact motor efficiency.

One important factor to note, not mentioned in the referenced webpage, is that motor speed (measured in rpm) is directly linked to the frequency (measured in Hz) of the power supply. For further information on this topic, you can visit this useful webpage: http://www.engineeringtoolbox....nity-laws-d_408.html</p>
