            <p>The speed of a motor can be reduced by approximately 17%. For an induction motor, the synchronous speed per minute can be calculated using the formula N=(F x 120)/P, where N is the sync speed, F is the frequency, and P is the number of poles. For example, let's consider a 4-pole motor operating at 60 Hz: (120*60)/4 = 1800 RPM. Now, if the same motor operates at 50 Hz: (120*50)/4 = 1500 RPM. This demonstrates how changes in frequency can affect the speed of the motor.</p>
