            <p>1. Activate the "Class" option in the warranty category I within the image CM01. 
2. Within Plant Maintenance and Customer Service master data, navigate to Warranties and Define Warranty Types. Set the necessary indicators for warranties in notifications and work orders. 
3. Establish number range BG00 for Master Warranties. 
4. Develop a Characteristic for the Warranty Counter using T-Code CT04. Include a Description, Numeric Format, Number of Characters "5", Unit of Measure "Months", and Single Values. Utilize this Characteristic as a Warranty Counter with T-Code GM04, set it as the Default, and make it time dependent. By following these steps, the warranty configuration will be complete. - John Orris.</p>
