            <p>In the realm of AC power (specifically discussing single-phase AC for simplicity), it is important to understand that the voltage and current vectors are not in sync. Typically, the current vector lags behind by a few degrees. This results in two components - the cosine component in phase with the voltage (similar to DC power) and the sine component at a 90-degree angle to the voltage vector. The actual power utilized is a combination of the voltage and the current component in phase with it, known as VIcosPhase angle. The sine component represents reactive power, which does not involve actual energy consumption by the motor or load. A purely resistive load requires zero reactive power, while pure inductance demands 100% lagging reactive power, and pure capacitance needs 100% leading reactive power.

Despite this, the reactive component of current increases the overall current demand, which then flows through the transmission and distribution systems. These systems have inherent resistance due to materials like copper or aluminum. Essentially, the entire current - active and reactive - flows through them. The power loss incurred during transmission is calculated using the formula I squared times R, where I is the total current and R is the cable's resistance. This loss, known as copper loss, is a critical factor to consider in the efficient distribution of power.</p>
