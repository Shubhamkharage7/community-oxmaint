            <p>Thank you for your email, Dave. I apologize for the delay in responding. I have been analyzing trends in our DCS to identify the cause of the heavy N2 leak. It appears that during deep vacuum operations, there is an increase in N2 flow rate, while during normal operation, a high amount of N2 flows through the seal. After discussing with the production engineer, it seems that we are pumping multiple fluids at or near their vapor pressure, leading to off-gassing and vapor entrainment issues during deep vacuum. 

Our double seal in Tandem operation consists of two stationary rings and two rotational rings made of Tungsten on Carbon. The size of a dry gas seal is larger than a wet seal to provide enough surface area for hydrodynamic lift. The seal gland covers the connection port to the seal chamber, which is why I have requested it to be machined to accommodate a 3/8" pipe for a pressure gauge attachment.

By monitoring the stuffing box pressure, we can better understand the seal pressure correlation. While we have calculated the stuffing box pressure theoretically, there may be discrepancies. Balancing holes could help remove gas from the seal cavity, but I would need assistance from an expert in this area for our current pump. John Crane recommends maintaining N2 pressure 30-50 psig above the seal chamber pressure, although our in-house N2 pressure is limited to a maximum of 40 psig. 

Booster pumps have been installed to increase pressure, although their reliability is a concern. The nitrogen gas used is filtered with a coalescing filter to remove moisture and is suitable for the seal. Despite not seeing significant damage, it is possible that the seal faces are being pulled open, causing the leakage issue. For further details, please refer to the attached Seal Drawing document.</p>
