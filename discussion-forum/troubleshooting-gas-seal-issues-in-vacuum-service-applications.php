- Check out the impeller with "balancing holes" on Page 5 of this document: https://www.sulzer.com/en/-/me...ngleStage_E10083.pdf 
- Find out about the stuffing box pressure difference with balancing holes on Page 8 of this presentation: http://peg-az.com/wp-content/u...TAR-PRESENTATION.pdf 

Another option to consider is a throttle bushing, which is a machined flat washer that is mounted on the back side of the impeller. This component helps maintain stable stuffing box pressure by providing clearance between the rear cover shaft opening. For more details on how throttle bushings work, check out this article: http://www.fluidsealing.com/sealingsense/aug08.pdf 

In addition, if you are dealing with high temperatures, be sure to account for vapor pressure in your calculations. Based on my calculations, the Total Dynamic Head (TDH) should be around 17'. To ensure a 30-50 psi increase, you should aim for a minimum of 47-50 psi to be 30 psi higher than the discharge pressure. 

If you have any further questions or concerns, feel free to reach out. Thank you, Dave.</p>
