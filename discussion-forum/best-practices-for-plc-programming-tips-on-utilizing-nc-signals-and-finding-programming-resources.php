            <p>When selecting a limit switch, the key factor to consider is whether it is normally open or normally closed. The crucial guideline is the electrical flow needed for proper functioning. For a limit switch that is typically engaged, the normally open (NO) contact should be utilized. On the other hand, for an overtravel limit that is never reached during regular operation, the normally closed (NC) contact is preferred.

In the case of a low-level float switch, it would be normally open (NO) and activated when the liquid level rises. Conversely, a high-level float switch would be normally closed (NC) and open when the fluid reaches the specified level. Unless the high-level float switch is used to initiate a pump, in which case it would be normally open (NO) and activate the pump when current passes through it.

It is imperative to ensure that the sensor or switch is functioning properly, with no broken wires or malfunctioning PLC inputs, to enable the desired outputs to be activated.</p>
