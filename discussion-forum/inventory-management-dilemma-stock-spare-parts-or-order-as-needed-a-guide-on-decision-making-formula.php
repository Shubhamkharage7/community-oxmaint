            <p>When analyzing the 100,000 materials, the allocation of A, B, C, or D ratings will be based on the assessment of Risk Priority Number (RPN) for each specific material. This determination may vary depending on whether you analyze at the equipment level or the storeroom level. It is possible for the same material in stock to belong to different equipment with varying RPN assignments. In such cases, it is recommended to record the lowest RPN as a conservative approach.

Contrary to the popular 20-80 (Pareto) rule, the ABC technique categorizes data into classes to measure data concentration. This statistical method involves frequency analysis of a measurable property within a population. The resulting Lorenz curve or Gini index offers insight into the concentration of data, which may not always align with the 20-80 rule.

It is important to note that while the 20-80 rule might not be exact, there are instances where it closely aligns. Just like in the example provided in the attached Word document. To classify components into A, B, C, or D based on RPN, a similar approach should be taken.</p>
