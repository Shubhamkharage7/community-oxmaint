        <div class="bg-white p-3 rounded mb-3">
            <p>Hello Eugene,

It is essential to only keep spare parts in the stockroom that are prone to random failures, as these failures can happen unexpectedly. These spare parts are considered "just in case" inventory for unplanned maintenance. Other parts should be ordered "just in time" for planned maintenance purposes. If you have categorized your materials based on ABC indicators related to equipment criticality, you can utilize the discrete Poisson probability distribution to determine the optimal ordering point.

For example, if a part fails randomly once every 1,000 hours and there are 20 units of the same part running 24 hours a day, with a vendor lead time of 3 days, and a requirement for the part to be available 95% of the time, the ordering point can be calculated. By using the formula N*r*LT, where N is the number of units, r is the failure rate, and LT is the lead time, you can determine the appropriate stock level to ensure availability.

It is important to consider various iterations to find the optimal ordering point. In the given example, it was determined that ordering should be done when the stock level reaches a minimum of 4 units to meet the 95% availability requirement. Adjustments can be made based on different criticality levels to determine the most suitable ordering point.

These calculations can be easily performed using Excel with the predefined function =POISSON(k;N*r*LT;1). However, additional factors such as wear-out failure modes, preventive replacements, stockholding costs, and opportunity costs need to be considered for a more comprehensive analysis.

I hope this information is helpful for your inventory management. 

Kind regards,
Rui</p>
