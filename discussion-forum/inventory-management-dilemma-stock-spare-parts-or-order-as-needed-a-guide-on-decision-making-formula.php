            <p>Hello Josh,

When determining the criticality of a system, it is important to establish specific ranges for detectability, probability, and severity. For example, detectability can range from 1 to 5, probability from 1 to 6, and severity from 1 to 6. By multiplying the maximum scores for each dimension (5 x 6 x 6), you can calculate the Risk Priority Number, which in this case would be 180.

In accordance with Norm Mil Std 882D, Tables A-III and A-IV, different assessment intervals correspond to percentages of 20. Class A components fall within a score range of 1 to 45, Class B components range from 46 to 81, and Class C components range from 82 to 153.

