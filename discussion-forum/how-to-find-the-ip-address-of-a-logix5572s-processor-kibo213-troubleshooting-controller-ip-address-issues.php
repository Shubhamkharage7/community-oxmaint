        <div class="bg-white p-3 rounded mb-3">
            <p>When connecting through USB, you have a direct link to the CPU, which is why the CPU is easily visible under the USB driver in RSLinx. This "16" you see is due to the PLC being located in Slot 16 of a virtual USB-connected backplane object, but you are still directly connected to it. On the other hand, connecting via Ethernet involves an extra communication "hop" from the Ethernet module, through the backplane, and finally to the CPU in Slot 0. ControlLogix CPUs can be located in any slot within the chassis, with the possibility of multiple CPUs in a single chassis. This means that software tools cannot assume they are going online with the controller in that specific chassis just by connecting to the Ethernet module. An easy workaround is to browse to the Ethernet module over the TCP/IP network, then proceed to the backplane and finally to the CPU in Slot 0.

The latest generations of CompactLogix and the 1756-L8xE generation of ControlLogix feature an Ethernet module integrated directly into the CPU, similar to a USB connection where there is no additional hop across the backplane.</p>
