                    <h3>Question:</h3>
                    <p>Hello everyone, we are currently encountering an issue with excessive axial and radial float in a two-stage multistage pump. This pump features a single suction and discharge design, with a cylindrical roller bearing at the drive end (DE) and an angular contact ball bearing at the non-drive end (NDE). Upon conducting spectrum analysis due to high vibration levels, we discovered looseness and misalignment in the pump. It was determined that the pump's alignment was beyond acceptable limits, with radial and axial floats measuring 0.38mm and 0.50mm respectively. Upon further inspection, the NDE bearings were found to be loose on the shaft and the NDE bearing housing showed significant wear marks. We are seeking insights into the possible reasons for the increased radial and axial floats. Your valuable input would be greatly appreciated. Best regards, Jatan.</p>
