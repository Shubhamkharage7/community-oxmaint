            <p>Paul, not long ago, we found ourselves in a similar situation. The concept of "Urgent Work" provided immediate satisfaction to Production and the Techs, similar to receiving recognition like a Cowboy/Superhero. Currently, we are averaging less than 20% Urgent work completion, and this isn't just on paper. It required a significant cultural shift to reach this point. Some key factors that contributed to our success were: 1) Emphasizing the importance of operators acting as operators, rather than just button pushers. This led to fewer operator calls for adjustments and empowered our mechanics as "superoperators". Implementing procedures, machine centerlining, and training sessions, often led by mechanics, helped rectify this issue over time. 2) The implementation of a formal work order request system allowed Production a means to have their needs addressed. Approved work orders were planned, scheduled, and completed. We also provided requesters with weekly updates on the status of their work orders. 3) During the transitional period, we rotated techs to act as troubleshooters to address floor calls and urgent work. This allowed the remaining techs to focus on planned work. 4) Efficient planned jobs made the techs more productive. Once planners became proficient at it, kitted parts and clear work orders reduced non-productive "research" time and increased hands-on work time. 5) I suggest displaying the weekly schedule for transparency. This keeps the crew informed about upcoming tasks and helps Production understand what they are diverting techs from. 6) Lengthy tasks suffered the most when we were reactive. Breaking them into smaller, manageable jobs, ideally less than 2 hours each, helped ensure their completion. One area we overlooked was building a library of planned jobs. Instead of refining each job as it recurs, we now plan out the same job repeatedly (including steps, parts, and time estimates). As mentioned earlier, our labor is based on our backlog. We aim to maintain a "Ready Work" backlog of 2-4 weeks, exceeding this calls for overtime, while a too small backlog isn't ideal either. We utilize a priority system to move work orders forward, recently prioritizing older low-priority tasks to ensure completion. Best of luck!</p>
