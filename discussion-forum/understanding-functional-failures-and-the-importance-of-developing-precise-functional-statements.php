                    <h3>Question:</h3>
                    <p>The concept of the P-F Curve in the industry often indicates that a functional failure is very close to a catastrophic failure. However, it is important to note that this is not always the case. A precise functional statement (FS) must be developed in order to accurately determine a functional failure. Without performance standards and operating context, a functional statement lacks accuracy. By establishing performance standards within the FS, a functional failure can be identified. For example, if a pump is designed to pump 600 gpm of water but the desired performance is 500 gpm, the pump technically fails (Functional Failure) at 499 gpm according to the performance standards. In this scenario, the functional failure is far from a catastrophic failure, as the pump could continue running for months or even years before catastrophic failure occurs. This highlights the importance of developing a proper functional statement for your assets to accurately assess the loss of function.</p>
