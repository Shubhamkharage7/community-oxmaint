            <p>After encountering difficulties with establishing a direct connection between the customer OPC server and the PLC, I found success by incorporating FT Optix as an intermediary. This setup now involves communication flow from the OPC server to FT Optix to the PLC, enabling seamless messaging between the PLC client and other networked devices. To host FT Optix, options include the 1756-UM021A-EN-P (designed to fit into a control logix rack), the Factory Talk Optix HMI, or a separate PC to run the Factory Talk Optix software. The first two options include the necessary software, while the third option requires separate purchase of the software and a PC. In terms of pricing, the first two options likely offer the most cost-effective solution.</p>
