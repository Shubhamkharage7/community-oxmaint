            <p>Hello Keith,

Many organizations in the heavy equipment industry have implemented advanced whole-of-life cost management systems that require vendors to collaborate in equipment management. Most components of these machines have clear replacement dates, though condition-based maintenance can offer some benefits. Initial maintenance intervals are established based on these dates. In the past, prime movers of trucks were capable of 25,000 hours, but with the use of condition monitoring, this has been extended to around 47,000 hours. Vendors are expected to provide guarantees for availability and unit costs, with the costs of managing the assets shared between both parties. If vendors fail to meet the agreed availability and unit cost levels, the costs are shifted back to them, while exceeding expectations benefits the client company. This arrangement is seen as mutually beneficial and ensures assets can be managed within known whole-of-life costs. I trust this information proves helpful to you.</p>
