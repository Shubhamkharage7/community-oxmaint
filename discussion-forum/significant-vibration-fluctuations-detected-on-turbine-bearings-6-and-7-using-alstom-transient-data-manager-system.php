            <p>When experiencing fluctuations in vibration trends, spectrum, and orbit plots on a 340 MWe steam turbine's bearings #6 and #7 at ALSTOM's Transient Data Manager (TDM) System, it is important to address potential grounding errors. False vibration instrument readings are often caused by electrical currents induced in the system. To troubleshoot this issue, ensure that proximeters are properly grounded according to OEM guidelines. Check the junction box's main grounding wire for tight adherence and absence of oil or varnish at connections. Additionally, inspect the main grounding element going into the ground for any voltage buildup. Technicians should perform a voltage check on the junction box metal and Copper ground element at the earthing level, ensuring that the voltage does not exceed 0.01 Volts. It is also crucial to consider AC wiring induced currents by separating vibration wiring from AC conducting wires in cable trays, following instrumentation standards for proper separation distances. Lastly, investigate rotor-induced electrical currents, such as electrical shaft currents in the turbine rotor or high rotor magnetism above 5 Gauss in the shaft or casing. By conducting these checks, you can identify and address any underlying issues affecting vibration trends on the turbine bearings. For further analysis and recommendations on diagnosing the fault based on available data, reach out with your findings. Remember to consult with experts for accurate troubleshooting and maintenance strategies.</p>
