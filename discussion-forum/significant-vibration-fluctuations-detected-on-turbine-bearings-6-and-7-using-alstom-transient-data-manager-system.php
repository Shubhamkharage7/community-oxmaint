                    <h3>Question:</h3>
                    <p>After starting up on July 27, 2024, significant fluctuations in vibration trends were detected on bearings # 6 and 7 of the Turbine/Generator, along with minor fluctuations on bearings # 1, 4, and 5, using the ALSTOM Transient Data Manager/ Turbine Diagnostic System (TDM). These fluctuations were observed on the X and Y Relative probes of the TDM system, but not on the Vibro VM600 TSI system in the main control room.

The vibrations were present even at zero speed and during turning gear operation at 2~3 rpm. Major fluctuations with amplitudes between 150 µm to 240 µm were noted at regular intervals on bearings # 6 and 7, with exceptions of no or minor fluctuations during night hours. Minor vibrations between 10µm to 20 µm were also observed on bearings # 1, 4, and 5.

The vibration frequency of peaks at bearings # 6 and 7, measured by the X and Y probes, ranged from 270Hz (5.4x) to 570Hz (11.4x), with side bands visible in the waterfall plots. It is crucial to diagnose the fault based on this data, as the fluctuations are only apparent on the diagnostic system and not on the TSI system.</p>
