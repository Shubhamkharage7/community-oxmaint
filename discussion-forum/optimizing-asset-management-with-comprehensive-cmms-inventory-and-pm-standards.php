            <p>Albert, it may be impossible to register all equipment in your plant on your CMMS, based on my experience. For example, if you have a 3/4" drain valve throughout all pipelines, vessels, and other areas, do you know how many drain valves are installed in the plant? In my experience, I have had to register equipment based on an equipment list provided by project construction. This raises question 1. Once your equipment is registered, you can prioritize your equipment by defining critical ranks and setting maintenance tasks or strategies based on criticality. This step relies on information provided by discipline engineers. The final step is to input all this information back into the CMMS. While this is my own practice and not necessarily the best practice, think of it as a simple and fundamental approach.</p>
