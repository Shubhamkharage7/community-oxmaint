While you are conducting the right checks and activities, it appears that the interior of the base is in poor condition despite its outward appearance. Proper installation should have involved filling the base with grout, as the current weak base is causing misalignment during operation. Even though the machine may be aligned when at rest, once it starts running and the base begins moving, the vibration data reveals misalignment or an increased 2x rpm frequency.

To further investigate, monitor the live-time vibration spectrum while tapping or striking the pump casing/volute in the horizontal direction. Observe any peaks that rise in amplitude or any noticeable changes in vibration patterns. Measure the overall vibration at the pump base where it is bolted to concrete, taking at least 10 readings per side along the length of the base. Ideally, the vibration levels should be at or below 0.3 mm/sec. If other pumps with better bases or grouting are available, compare their vibration levels to assess the impact of installation quality.

