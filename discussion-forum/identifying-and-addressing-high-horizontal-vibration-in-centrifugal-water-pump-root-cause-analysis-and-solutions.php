            <p>Dear Luis Eugene Tibre, Your power source seems to operate at a frequency of 60Hz, indicated by the 1x of 3570 RPM. It is crucial to verify if the 2x RPM is indeed a true 2x RPM and not 2x Line Frequency. To confirm this, conduct a low Fmax Spectrum analysis (up to 500 Hz) with a high Lines of Resolution (1600 or 3200 LOR). A reliable method to check for 2x Line Frequency is to monitor the spectrum while simultaneously turning off the power. If the 2x peak disappears, it indicates an electrical issue. 

I have personally experimented with loosening individual motor hold-down bolts one at a time while monitoring the spectrum in "live mode" to determine if the 2x peak diminishes. Please note that this method should be approached with caution and at your own risk. If the problem is 2x Line Frequency, typically caused by a misaligned motor frame, the 2x peak will decrease or disappear when the issue is identified. 

