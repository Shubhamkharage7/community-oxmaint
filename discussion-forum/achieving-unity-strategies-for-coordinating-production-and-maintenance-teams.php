            <p>Hello Camrat, Being part of an organization focused on production versus maintenance, achieving a proactive environment can be quite challenging. In my experience in a similar organization based in the UK, we underwent various trendy initiatives depending on the preferences of new managers. However, what really made a difference in aligning production and maintenance was implementing a TPM (Total Productive Maintenance) program with OEE (Overall Equipment Effectiveness) as the KPI (Key Performance Indicator). This program emphasized the importance of production throughput, quality, and availability, requiring collaboration between production and maintenance teams. Despite facing some challenges, overall, the program was successful in moving us in the right direction.

We also encountered issues with production team leaders who prioritized productivity over business needs, leading to equipment breakdowns and downtime. This cycle was frustrating from a maintenance and reliability perspective. Implementing TPM could help foster a unified team environment and shift focus away from reactive problem-solving. Educating non-believers on the true costs of breakdowns and conducting cost benefit analyses could help showcase the benefits of regular maintenance practices. By demonstrating the potential savings for the business, you may be able to change their perspective. Best of luck. Gary</p>
