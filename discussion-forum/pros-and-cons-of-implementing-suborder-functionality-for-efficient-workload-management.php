        <div class="bg-white p-3 rounded mb-3">
            <p>Sub-orders are utilized in various scenarios, such as when there is a significant change in the scope of the original work order plan. In such cases, a sub-order is created for the new scope, and the job is thoroughly planned before execution. They are also used for large jobs that involve elements controlled by different main work centers or are executed in stages. Sub-orders can be created for procurement stages and execution stages as needed.

It is standard practice not to release the superior order to prevent any commitments or postings of costs. The superior order is only released and closed at the conclusion of the job. One of the advantages of using sub-orders is the ability to easily track the overall cost of the job, with clear responsibility assigned for planning and executing each part of the job.

However, there are some disadvantages to using sub-orders. For instance, it is only possible to create sub-orders and not assign existing orders as sub-orders of another. Furthermore, the superior order cannot be closed until all sub-orders are completed, which may lead to issues with schedule dates for maintenance plans if the superior order originated from such a plan. It is important to consider whether utilizing Revisions, WBS Elements, or Order Groups would be more appropriate in certain situations.</p>
