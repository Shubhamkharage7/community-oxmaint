                        <li><strong>Risk-Based Thinking:</strong> Encourages identifying risks and opportunities early.</li>
                        <li><strong>Leadership Commitment:</strong> Ensures top management is actively involved.</li>
                        <li><strong>Customer Focus:</strong> Prioritizes meeting customer expectations and satisfaction.</li>
                    </ul>
                </p>
