                <section>
                    <h2 class="text-primary">What is Preventive Maintenance?</h2>
                    <p>Preventive maintenance (PM) involves regular and planned maintenance activities to keep equipment in good working condition and to prevent unexpected failures. PM includes inspections, lubrication, adjustments, and part replacements at scheduled intervals.</p>

                    <h3>Why Conduct Preventive Maintenance?</h3>
                    <ul>
                        <li>Reduces downtime and unexpected breakdowns</li>
                        <li>Extends the lifespan of equipment</li>
                        <li>Increases efficiency and productivity</li>
                        <li>Enhances safety by preventing hazardous failures</li>
                    </ul>

                    <h3>How to Conduct Preventive Maintenance</h3>
                    <p>Preventive maintenance should be meticulously planned and documented. Key steps include:</p>
                    <ul>
                        <li>Creating a maintenance schedule based on manufacturer recommendations and equipment usage</li>
                        <li>Documenting all maintenance activities</li>
                        <li>Using checklists to ensure all necessary tasks are completed</li>
                        <li>Regularly reviewing and adjusting the maintenance plan as needed</li>
                    </ul>

                    <h3>When to Conduct Preventive Maintenance</h3>
                    <p>Preventive maintenance should be conducted based on a scheduled timeline, which can be weekly, monthly, quarterly, or annually, depending on the equipment and industry standards.</p>
                </section>

                <section>
                    <h2 class="text-primary">What is Breakdown Maintenance?</h2>
                    <p>Breakdown maintenance (also known as reactive or corrective maintenance) occurs after equipment has failed. The goal is to restore the equipment's functionality as quickly as possible to minimize downtime.</p>

                    <h3>Why Conduct Breakdown Maintenance?</h3>
                    <ul>
                        <li>Suitable for non-critical equipment where downtime does not significantly impact operations</li>
                        <li>Lower initial maintenance costs since repairs are only made after a failure</li>
                    </ul>

                    <h3>How to Conduct Breakdown Maintenance</h3>
                    <p>Breakdown maintenance involves diagnosing the cause of failure and repairing or replacing the faulty components. Key steps include:</p>
                    <ul>
                        <li>Immediate identification of the issue</li>
                        <li>Allocating necessary resources (labor, tools, parts)</li>
                        <li>Conducting the repair or replacement</li>
                        <li>Testing the equipment to ensure functionality</li>
                    </ul>

                    <h3>When to Conduct Breakdown Maintenance</h3>
                    <p>Breakdown maintenance is conducted as soon as a failure occurs. This type of maintenance is unpredictable and can incur higher costs if not managed properly.</p>
                </section>

