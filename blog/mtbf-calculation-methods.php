            <section>
                <h2 class="text-primary">What is MTBF?</h2>
                <p>MTBF stands for Mean Time Between Failures. It is a measure of how reliably a system or component operates over a specified period. It is calculated by dividing the total operational time by the number of failures during that time.</p>
                <ul>
                    <li>Total operational time: The cumulative time the equipment is operational.</li>
                    <li>Number of failures: The total number of times the equipment fails during the operational period.</li>
                </ul>
            </section>

            <section>
                <h2 class="text-primary">Why is MTBF Important?</h2>
                <p>MTBF is essential because it provides insights into the reliability and performance of equipment. It is used for:</p>
                <ul>
                    <li>Planning maintenance schedules</li>
                    <li>Improving system reliability</li>
                    <li>Redesigning systems for better performance</li>
                    <li>Budgeting maintenance costs</li>
                    <li>Ensuring safety and compliance</li>
                </ul>
            </section>

            <section>
                <h2 class="text-primary">How to Calculate MTBF</h2>
                <h3 class="text-primary">Basic Calculation Method</h3>
                <p>The basic formula for calculating MTBF is:</p>
                <p><strong>MTBF = Total Operational Time / Number of Failures</strong></p>
                <h3 class="text-primary">Example Calculation</h3>
                <p>If an equipment operated for 1000 hours and failed 4 times, the MTBF would be:</p>
                <p><strong>MTBF = 1000 hours / 4 failures = 250 hours</strong></p>
                <h3 class="text-primary">Advanced Calculation Methods</h3>
                <p>For more complex systems, advanced methodologies like Reliability Block Diagrams (RBD), Fault Tree Analysis (FTA), and statistical software are used to calculate MTBF accurately.</p>
            </section>

            <section>
                <h2 class="text-primary">When to Conduct MTBF Analysis</h2>
