                <h2 class="text-primary">What is CMMS?</h2>
                <p>A Computerized Maintenance Management System (CMMS) is software that centralizes maintenance information and facilitates the processes involved in managing maintenance operations. The primary focus of a CMMS is to ensure assets run efficiently by managing and scheduling maintenance activities.</p>
                
                <h2 class="text-primary">Why Use CMMS in Non-Manufacturing Industries?</h2>
                <p>Non-manufacturing industries such as healthcare, hospitality, education, and more can benefit from a CMMS in the following ways:</p>
