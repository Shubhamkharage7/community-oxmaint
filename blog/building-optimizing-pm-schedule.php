                <h2 class="text-primary">How to Conduct Preventive Maintenance</h2>
                <p>Conducting preventive maintenance involves several steps:</p>
                <ul>
                    <li><strong>Asset Inventory Management:</strong> Keep a detailed record of all equipment, including specifications and maintenance history.</li>
                    <li><strong>Schedule Routine Checks:</strong> Establish a maintenance calendar with regular checks based on the manufacturer's recommendations.</li>
                    <li><strong>Create Inspection Checklists:</strong> Develop comprehensive checklists for each piece of equipment to ensure thorough inspections.</li>
                    <li><strong>Documentation:</strong> Record all maintenance activities and findings for future reference.</li>
                    <li><strong>Use of CMMS:</strong> Implement a Computerized Maintenance Management System (CMMS) to automate scheduling and tracking.</li>
                </ul>
            </section>

