                <h2 class="text-primary">Why NDT is Important</h2>
                <p>
                    NDT is essential for many reasons, such as:
                </p>
                <ul>
                    <li>Ensuring the structural integrity and reliability of components and systems.</li>
                    <li>Minimizing the risk of failure and accidents by early detection of defects.</li>
                    <li>Reducing costs associated with material wastage and repairs.</li>
                    <li>Complying with industry standards and regulations.</li>
                </ul>
            </section>

            <section>
                <h2 class="text-primary">Common NDT Methods</h2>
                <p>Some of the most widely used NDT methods include:</p>
                <ul>
                    <li><strong>Ultrasonic Testing (UT):</strong> Uses high-frequency sound waves to detect internal flaws.</li>
                    <li><strong>Radiographic Testing (RT):</strong> Utilizes X-rays or gamma rays to examine the internal structure.</li>
                    <li><strong>Magnetic Particle Testing (MPT):</strong> Detects surface and slightly subsurface defects in ferromagnetic materials.</li>
                    <li><strong>Eddy Current Testing (ECT):</strong> Uses electromagnetic induction to detect surface and sub-surface flaws.</li>
                    <li><strong>Visual Inspection (VI):</strong> Involves the visual examination of materials and components.</li>
                </ul>
            </section>

            <section>
                <h2 class="text-primary">When to Conduct NDT</h2>
                <p>
                    NDT is conducted at various stages in the lifecycle of a component or system:
                </p>
                <ul>
                    <li><strong>During Production:</strong> To ensure the quality of materials and components before they are assembled.</li>
                    <li><strong>Post-Production:</strong> To verify the integrity of the final product.</li>
                    <li><strong>In-Service:</strong> For ongoing maintenance and monitoring to detect any signs of wear, damage or deterioration.</li>
                </ul>
            </section>

