                <h2 class="text-primary">How to Conduct DMAIC?</h2>
                <h3 class="text-primary">Define</h3>
                <p>In this phase, you identify the problem or project goals. Define the scope, objectives, and deliverables clearly to ensure a focused approach.</p>
                <h3 class="text-primary">Measure</h3>
                <p>Measure key aspects of the current process and collect relevant data. This data serves as the baseline for improvement.</p>
                <h3 class="text-primary">Analyze</h3>
                <p>Analyze the data to identify root causes of defects and process inefficiencies. Use tools like Pareto charts and fishbone diagrams for in-depth analysis.</p>
                <h3 class="text-primary">Improve</h3>
                <p>Develop and implement solutions to improve the process. This phase may involve brainstorming, prototyping, and pilot testing.</p>
                <h3 class="text-primary">Control</h3>
                <p>Control the improved process to ensure that improvements are sustained over time. Use control charts and regular audits to maintain the gains.</p>
            </section>
