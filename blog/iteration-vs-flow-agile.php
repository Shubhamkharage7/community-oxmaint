                <ul>
                    <li><strong>Kanban Board:</strong> Visual representation of work process.</li>
                    <li><strong>Work In Progress (WIP) Limits:</strong> Restricts number of tasks to maintain focus.</li>
                    <li><strong>Continuous Delivery:</strong> Tasks are continuously completed and moved forward.</li>
