            <section>
                <h2 class="text-primary">What is Flow-Based Agile?</h2>
                <p>Flow-Based Agile, commonly known as Kanban, is a methodology that manages work through continuous flow. It visualizes the work process using a Kanban board, helping teams to manage the flow of tasks and maximize efficiency.</p>
