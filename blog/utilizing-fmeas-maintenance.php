                <h2 class="text-primary">Top 5 FAQs</h2>
                <div id="faq1">
                    <h3>1. What is the primary objective of FMEA?</h3>
                    <p>The primary objective of FMEA is to identify potential failure modes, assess their impacts, and prioritize actions to mitigate these failures to enhance system reliability and safety.</p>
                </div>
                <div id="faq2">
                    <h3>2. How often should FMEA be updated?</h3>
                    <p>FMEA should be updated regularly, particularly when there are changes in the system, new data emerges, or after a significant failure occurs.</p>
                </div>
                <div id="faq3">
                    <h3>3. Can FMEA be used for software systems?</h3>
                    <p>Yes, FMEA can be applied to software systems to identify potential failure points and mitigate risks associated with software failures.</p>
                </div>
                <div id="faq4">
                    <h3>4. What is the difference between DFMEA and PFMEA?</h3>
                    <p>DFMEA (Design FMEA) focuses on potential failure modes during the design phase, while PFMEA (Process FMEA) focuses on potential failures during the manufacturing or maintenance process.</p>
                </div>
                <div id="faq5">
                    <h3>5. What role does FMEA play in preventive maintenance?</h3>
                    <p>FMEA helps in identifying potential failure modes early, allowing for the development of preventive maintenance strategies to address these issues before they lead to equipment breakdowns and downtime.</p>
