            <h2 class="text-primary">When to Conduct Maintenance?</h2>
            <p>Maintenance activities should be conducted based on a regular schedule or when certain conditions are met. There are two primary types of maintenance:</p>
            <h3 class="text-primary">Preventive Maintenance</h3>
            <p>Scheduled at regular intervals to prevent equipment failure. This type of maintenance includes tasks such as inspections, lubrication, and part replacements.</p>
            <h3 class="text-primary">Predictive Maintenance</h3>
            <p>Based on the actual condition of equipment, predictive maintenance uses data from sensors and other monitoring devices to determine when maintenance should be performed. This helps in preventing failures and optimizing maintenance schedules.</p>

