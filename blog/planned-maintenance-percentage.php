            <section>
                <h2 class="text-primary">How to Conduct PMP Calculations?</h2>
                <p>To calculate the Planned Maintenance Percentage, use the following formula:</p>
                <p><code>PMP = (Planned Maintenance Hours / Total Maintenance Hours) * 100%</code></p>
                <p>For example, if a factory has 80 hours of planned maintenance and 20 hours of unplanned maintenance in a given month, the PMP would be:</p>
                <p><code>PMP = (80 / (80 + 20)) * 100 = 80%</code></p>
            </section>
            
