                <h4 class="text-primary">2. How does predictive maintenance benefit the oil and gas industry?</h4>
                <p>Predictive maintenance helps by reducing unplanned downtime, optimizing maintenance schedules, and ultimately saving costs by preemptively addressing potential equipment failures.</p>

                <h4 class="text-primary">3. What are the key tools used for maintenance in the oil and gas sector?</h4>
                <p>Key tools include CMMS, condition monitoring equipment (like vibration analysis tools), non-destructive testing (NDT) equipment, and predictive analytics software.</p>

