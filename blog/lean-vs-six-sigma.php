                <h2 class="text-primary">What is Six Sigma?</h2>
                <p>Six Sigma is a set of techniques and tools for process improvement, which seeks to improve the quality of the output of a process by identifying and removing the causes of defects and minimizing variability in manufacturing and business processes. It uses a set of quality management methods, mainly empirical, statistical methods, and creates a special infrastructure of people within the organization.</p>
                <h3 class="text-primary">The DMAIC Cycle</h3>
                <ul>
                    <li>Define: Identify the problem and what is required to satisfy customers.</li>
                    <li>Measure: Collect data and determine how well the process is performing.</li>
                    <li>Analyze: Identify the cause of defects and opportunities for improvement.</li>
                    <li>Improve: Implement solutions to reduce defects and improve process performance.</li>
                    <li>Control: Maintain the improvements made.</li>
                </ul>
