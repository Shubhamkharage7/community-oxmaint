                <h2 class="text-primary">Lean vs. Six Sigma: Key Differences</h2>
                <ul>
                    <li><strong>Focus:</strong> Lean centers on eliminating waste and improving flow; Six Sigma focuses on reducing variability and defects.</li>
                    <li><strong>Tools:</strong> Lean uses tools like Value Stream Mapping and 5S; Six Sigma uses tools like DMAIC and Statistical Process Control.</li>
                    <li><strong>Approach:</strong> Lean is more intuitive and visual; Six Sigma is data-driven and statistical.</li>
                    <li><strong>Culture:</strong> Lean fosters a continuous improvement culture; Six Sigma emphasizes achieving specific benchmarks.</li>
                </ul>
