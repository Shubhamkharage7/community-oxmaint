                <p>Lean is a systematic method for waste minimization within a manufacturing system without sacrificing productivity. Lean also takes into account waste created through overburden and unevenness in workloads. Its focus is on enhancing customer value by improving processes and eliminating waste.</p>
                <h3 class="text-primary">Key Principles of Lean</h3>
                <ul>
                    <li>Value: Identifying what is valuable to the customer.</li>
                    <li>Value Stream: Mapping out all the steps in the value stream.</li>
                    <li>Flow: Ensuring a smooth flow of processes.</li>
                    <li>Pull: Producing only what is needed.</li>
                    <li>Perfection: Continually improving processes.</li>
