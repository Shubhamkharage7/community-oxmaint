                <p>Maintenance schedules can vary based on the type of equipment and operational requirements. Here are some types of maintenance to consider:</p>
                <ul>
                    <li><strong>Preventive Maintenance:</strong> Conducted on a regular basis to prevent equipment failure.</li>
                    <li><strong>Predictive Maintenance:</strong> Based on the prediction of failures using data and analytics.</li>
                    <li><strong>Corrective Maintenance:</strong> Performed after a fault is identified.</li>
                    <li><strong>Condition-Based Maintenance:</strong> Triggered by the real-time condition of the equipment.</li>
                </ul>

