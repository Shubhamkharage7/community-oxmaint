                    <li><strong>Predictive Maintenance:</strong> Using data and analytics to predict and avert potential breakdowns.</li>
                    <li><strong>Condition-Based Maintenance:</strong> Monitoring the actual condition of equipment to determine maintenance needs.</li>
                </ul>
            </section>

            <section>
                <h2 class="text-primary">When to Conduct Maintenance for Reliability?</h2>
                <p>The timing of maintenance activities is critical for ensuring reliability. Maintenance should be conducted:</p>
                <ul>
                    <li><strong>Regularly:</strong> Following a fixed schedule, such as monthly or quarterly inspections.</li>
                    <li><strong>Predictively:</strong> Based on the analysis of performance data and condition assessments.</li>
                    <li><strong>On-Demand:</strong> Whenever unexpected issues or signs of wear and tear are detected.</li>
                </ul>
            </section>

