        <h2 class="text-primary">FAQ</h2>
        <h3>1. What is the main goal of preventive maintenance?</h3>
        <p>The main goal of preventive maintenance is to maintain equipment in good working condition to prevent unexpected failures and to prolong the lifespan of the machinery.</h3>
        <h3>2. How does FMEA improve safety?</h3>
        <p>FMEA improves safety by systematically analyzing potential failure modes within a system and implementing corrective actions to mitigate these issues, thereby reducing risks and preventing accidents.</h3>
        <h3>3. When should a company implement an FMEA process?</h3>
        <p>A company should implement an FMEA process whenever a new system is being designed, an existing system undergoes modification, or following a significant failure or incident.</h3>
        <h3>4. What tools are essential for effective preventive maintenance?</h3>
        <p>Essential tools for effective preventive maintenance include CMMS software, predictive maintenance sensors, root cause analysis tools, and detailed inspection checklists.</h3>
        <h3>5. How often should preventive maintenance be conducted?</h3>
        <p>The frequency of preventive maintenance should be based on the manufacturer's recommendations, industry best practices, and the criticality of the equipment. Regular intervals can vary from daily to annually depending on these factors.</p>
