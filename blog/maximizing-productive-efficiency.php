            <h2 class="text-primary">Top 5 FAQs</h2>
            <h3 class="text-primary">1. What is the difference between productive efficiency and technical efficiency?</h3>
            <p>Productive efficiency focuses on maximizing output with minimal input, while technical efficiency refers to producing the maximum output given a fixed set of inputs. Both concepts aim to minimize waste and optimize resource utilization.</p>

            <h3 class="text-primary">2. What are the indicators of productive efficiency?</h3>
            <p>Common indicators include reduced production costs, lower waste levels, higher product quality, improved delivery times, and enhanced customer satisfaction.</p>

            <h3 class="text-primary">3. How can CMMS help in maximizing productive efficiency?</h3>
            <p>CMMS helps in scheduling maintenance tasks, tracking work orders, managing spare parts inventory, and monitoring equipment performance in real time, ensuring that maintenance activities are effectively planned and executed.</p>

            <h3 class="text-primary">4. How often should preventive maintenance be conducted?</h3>
            <p>The frequency of preventive maintenance depends on the equipment type and usage. Daily, weekly, and monthly checks are common schedules, but specific intervals should be determined based on manufacturer recommendations and operational needs.</p>
            
            <h3 class="text-primary">5. Can lean manufacturing principles be integrated with advanced technological tools?</h3>
            <p>Yes, integrating lean manufacturing principles with advanced tools such as IoT, AI, and CMMS can significantly enhance efficiency by providing real-time data, predictive insights, and streamlined processes.</p>

