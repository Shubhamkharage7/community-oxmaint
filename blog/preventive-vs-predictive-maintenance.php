            <section>
                <h2 class="text-primary">What is Preventive Maintenance?</h2>
                <p>Preventive maintenance is a proactive strategy where maintenance activities are performed regularly on equipment to prevent failures. The maintenance tasks are scheduled based on time intervals or usage thresholds.</p>
            </section>

            <section>
                <h2 class="text-primary">Why Implement Preventive Maintenance?</h2>
                <p>Preventive maintenance helps in avoiding unexpected equipment failures, extending the lifespan of machinery, and ensuring smooth operations. By maintaining equipment before a failure occurs, it minimizes downtime and optimizes resource utilization.</p>
            </section>

            <section>
                <h2 class="text-primary">How to Conduct Preventive Maintenance?</h2>
                <p>Conducting preventive maintenance involves planning and scheduling regular checks, lubrication, parts replacement, and performance testing based on the manufacturer's recommendations and historical data.</p>
            </section>

            <section>
                <h2 class="text-primary">When to Conduct Preventive Maintenance?</h2>
                <p>Preventive maintenance should be conducted at regular intervals as defined by equipment usage patterns, operating conditions, and historical maintenance data.</p>
            </section>

            <section>
                <h2 class="text-primary">Tools Used in Preventive Maintenance</h2>
                <p>Various tools are used in preventive maintenance including CMMS (Computerized Maintenance Management Systems), scheduling tools, and diagnostic equipment to monitor equipment health.</p>
            </section>

            <section>
                <h2 class="text-primary">Features of Preventive Maintenance</h2>
                <ul>
                    <li>Scheduled maintenance activities</li>
                    <li>Proactive approach to equipment care</li>
                    <li>Reduces unexpected downtime</li>
                    <li>Optimizes parts and labor usage</li>
                </ul>
            </section>

            <section>
                <h2 class="text-primary">Challenges in Preventive Maintenance</h2>
                <p>One of the main challenges of preventive maintenance is the potential for over-maintenance where unnecessary maintenance activities are performed, increasing costs without additional benefits.</p>
            </section>

            <section>
                <h2 class="text-primary">What is Predictive Maintenance?</h2>
                <p>Predictive maintenance uses data-driven techniques and condition-monitoring tools to predict when equipment failure might occur. The goal is to perform maintenance only when necessary, based on equipment condition instead of a predetermined schedule.</p>
            </section>

            <section>
                <h2 class="text-primary">Why Implement Predictive Maintenance?</h2>
                <p>Predictive maintenance helps in maximizing equipment uptime, reducing maintenance costs, and preventing catastrophic failures by addressing issues before they result in equipment downtime.</p>
            </section>

            <section>
                <h2 class="text-primary">How to Conduct Predictive Maintenance?</h2>
                <p>Predictive maintenance involves using sensors and diagnostic tools to monitor equipment conditions in real-time. Data analytics and machine learning algorithms are then used to analyze the data and predict potential failures.</p>
            </section>

            <section>
                <h2 class="text-primary">When to Conduct Predictive Maintenance?</h2>
                <p>Predictive maintenance is conducted when the data indicates that an equipment failure is imminent. This allows maintenance to be performed just in time to prevent failure.</p>
            </section>

            <section>
                <h2 class="text-primary">Tools Used in Predictive Maintenance</h2>
                <p>Tools used in predictive maintenance include sensors, IoT devices, data analytics software, and machine learning algorithms to monitor equipment health and predict failures.</p>
            </section>

            <section>
                <h2 class="text-primary">Features of Predictive Maintenance</h2>
                <ul>
                    <li>Condition-based maintenance approach</li>
                    <li>Utilizes real-time data</li>
                    <li>Reduces maintenance costs</li>
                    <li>Improves equipment reliability</li>
                </ul>
            </section>

            <section>
                <h2 class="text-primary">Challenges in Predictive Maintenance</h2>
                <p>Predictive maintenance requires significant investment in technology and skilled personnel to analyze data. Implementing an effective predictive maintenance program can be complex and resource-intensive.</p>
            </section>

            <section>
                <h2 class="text-primary">Comparing Preventive and Predictive Maintenance</h2>
                <p>Both preventive and predictive maintenance have their own advantages and challenges. Preventive maintenance is easier to implement and manage, while predictive maintenance can significantly reduce costs and improve efficiency by preventing failures just in time. Choosing the appropriate maintenance strategy depends on the specific needs and resources of your organization.</p>
            </section>

