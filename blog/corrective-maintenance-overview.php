                    <li>Focuses on restoring functionality</li>
                    <li>Requires accurate fault identification</li>
                    <li>May involve part replacement</li>
                    <li>Documentation is crucial</li>
                </ul>
            </section>

            <section>
                <h2 class="h3 text-primary">Challenges in Corrective Maintenance</h2>
                <p>Despite its importance, corrective maintenance can be challenging due to:</p>
                <ul>
                    <li>Unplanned Downtime: Can disrupt operations significantly.</li>
                    <li>Resource Allocation: May require immediate attention and resources.</li>
                    <li>Complexity: Some faults are hard to diagnose and may be time-consuming to fix.</li>
                    <li>Costs: Can be expensive due to emergency repairs.</li>
                    <li>Documentation: Requires meticulous record-keeping.</li>
                </ul>
                <p>Effective planning and the right tools can help overcome these challenges.</p>
            </section>

