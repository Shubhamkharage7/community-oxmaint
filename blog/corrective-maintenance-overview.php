                <h2 class="h3 text-primary">What is Corrective Maintenance?</h2>
                <p>Corrective maintenance is a type of maintenance task that is performed to identify, isolate, and rectify a fault, so that failed equipment, machine, or system can be restored to its original operational condition. It typically takes place after a problem has occurred, and may involve items like machinery repairs, replacements, and adjustments.</p>
            </section>

            <section>
                <h2 class="h3 text-primary">Why Conduct Corrective Maintenance?</h2>
                <p>Corrective maintenance is crucial for several reasons:</p>
                <ul>
                    <li>Restores equipment functionality</li>
                    <li>Prevents minor issues from becoming major problems</li>
                    <li>Reduces downtime</li>
                    <li>Improves safety</li>
                    <li>Increases equipment lifespan</li>
                </ul>
            </section>

            <section>
                <h2 class="h3 text-primary">How to Conduct Corrective Maintenance?</h2>
                <p>Conducting corrective maintenance involves a series of steps:</p>
                <ul>
                    <li>Identify the Problem: Use symptoms or error codes to identify the issue.</li>
                    <li>Isolate the Fault: Trace the issue to its source.</li>
                    <li>Repair or Replace: Implement the corrective action needed.</li>
                    <li>Test: Verify that the equipment is functioning correctly.</li>
                    <li>Document: Keep records of the issue and solution for future reference.</li>
                </ul>
            </section>

            <section>
                <h2 class="h3 text-primary">When to Conduct Corrective Maintenance?</h2>
                <p>Corrective maintenance is conducted when equipment shows signs of failure or during scheduled inspections where faults are identified. Regular monitoring and diagnostics can help in timely detection of issues.</p>
            </section>

            <section>
                <h2 class="h3 text-primary">Tools Used in Corrective Maintenance</h2>
                <p>Several tools are used to conduct corrective maintenance efficiently:</p>
                <ul>
                    <li>Inspection Tools: Used to identify faults (e.g. thermal cameras, ultrasonic detectors).</li>
                    <li>Diagnostic Software: Helps in detecting issues within software-managed systems.</li>
                    <li>Repair Tools: Necessary for structural adjustments (e.g., screwdrivers, wrenches).</li>
                    <li>Replacement Parts: Essential spares to replace faulty components.</li>
                    <li>Documentation Software: Used for recording issues, steps taken, and results.</li>
                </ul>
            </section>

