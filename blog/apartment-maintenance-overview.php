                </div>
                <div>
                    <h3 class="text-primary">2. What are the most common maintenance issues in apartments?</h3>
                    <p>Common issues include plumbing leaks, electrical problems, HVAC inefficiencies, structural damage, and wear and tear on appliances and fixtures.</p>
                </div>
                <div>
                    <h3 class="text-primary">3. How can residents report maintenance issues?</h3>
                    <p>Residents can report issues via a dedicated maintenance portal or mobile app, email, phone, or directly through the property manager's office.</p>
                </div>
                <div>
                    <h3 class="text-primary">4. How can maintenance costs be minimized?</h3>
                    <p>Maintenance costs can be minimized through regular inspections and preventive maintenance, use of energy-efficient systems, training staff for efficiency, and bulk purchasing of supplies.</p>
                </div>
                <div>
                    <h3 class="text-primary">5. What should be included in a maintenance checklist?</h3>
