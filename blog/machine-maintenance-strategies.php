            <h3 class="text-primary">1. What are the types of machine maintenance?</h3>
            <p>There are several types of machine maintenance, including preventive maintenance, predictive maintenance, and corrective maintenance.</p>

            <h3 class="text-primary">2. How does preventive maintenance differ from predictive maintenance?</h3>
            <p>Preventive maintenance involves regular, scheduled maintenance tasks aimed at preventing breakdowns, while predictive maintenance uses real-time data and analytics to predict potential failures before they occur.</p>

            <h3 class="text-primary">3. What are the benefits of using maintenance management software?</h3>
