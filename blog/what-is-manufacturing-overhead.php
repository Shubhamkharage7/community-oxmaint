            <h2 class="text-primary">How to Calculate Manufacturing Overhead</h2>
            <p>To calculate manufacturing overhead:</p>
            <ul>
                <li>Sum all indirect costs over a specific period.</li>
                <li>Allocate these costs to products based on a chosen allocation base, such as direct labor hours or machine hours.</li>
                <li>Use the formula: <em>Manufacturing Overhead Rate = Total Indirect Costs / Total Allocation Base</em>.</li>
            </ul>

