                <h2 class="text-primary">What is Batch Processing?</h2>
                <p>Batch processing involves manufacturing a product in separate and distinct groups or batches. Each batch goes through the entire production process before the next one begins. This method allows for precise control over quality and can be adjusted for small or large production runs.</p>
                <ul>
                    <li>Standard features: Flexibility, Control</li>
                    <li>Common industries: Pharmaceuticals, Food and Beverage</li>
                </ul>
