            <section id="what-continuous">
                <h2 class="text-primary">What is Continuous Processing?</h2>
                <p>Continuous processing, on the other hand, involves an ongoing production flow. Materials are continuously fed into the system, and products are produced without interruption. This method is ideal for high-volume, consistent product outputs.</p>
                <ul>
                    <li>Standard features: Efficiency, High output</li>
                    <li>Common industries: Chemical Manufacturing, Oil Refining</li>
                </ul>
            </section>

            <section id="why">
                <h2 class="text-primary">Why Choose One Over the Other?</h2>
                <p>The choice between batch and continuous processing depends on various factors including product type, scale of production, and specific industry requirements. Batch processing offers more flexibility and is ideal for products that require varying formulations, while continuous processing is efficient for products that need consistent quality and high output.</p>
