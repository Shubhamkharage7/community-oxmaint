                <h2 class="text-primary">Understanding Emergency Maintenance</h2>
                <p>Emergency maintenance is performed in response to an unexpected failure that needs immediate attention to prevent severe consequences. This type of maintenance is unplanned and aims to rapidly restore operational functionality.</p>

                <h3 class="text-primary">What is Emergency Maintenance?</h3>
                <p>Emergency maintenance entails urgent repairs required to address critical equipment failures. Unlike corrective maintenance, emergency tasks are carried out immediately to mitigate risks or persistent impacts on operational continuity.</p>

                <h3 class="text-primary">Why Conduct Emergency Maintenance?</h3>
                <p>Emergency maintenance is critical because it:</p>
                <ul>
                    <li>Prevents significant operational downtime</li>
                    <li>Ensures the safety of personnel and equipment</li>
                    <li>Mitigates costly damages that could arise from prolonged failures</li>
                </ul>

                <h3 class="text-primary">How to Conduct Emergency Maintenance?</h3>
                <p>The steps to conduct emergency maintenance include:</p>
                <ul>
                    <li>Rapid identification of failure</li>
                    <li>Immediate mobilization of maintenance personnel</li>
                    <li>Quick diagnostics</li>
                    <li>Urgent execution of repairs</li>
                </ul>

                <h3 class="text-primary">When to Conduct Emergency Maintenance?</h3>
                <p>Emergency maintenance is initiated immediately upon the occurrence of unexpected failures that pose a risk to operations or safety.</p>

                <h3 class="text-primary">Tools Used in Emergency Maintenance</h3>
                <p>In emergency maintenance, the tools typically used include:</p>
                <ul>
                    <li>Rapid diagnostic tools</li>
                    <li>Emergency repair kits</li>
                    <li>Communication tools</li>
                    <li>Mobile maintenance units/manpower</li>
                </ul>
