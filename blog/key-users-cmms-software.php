                <ul>
                    <li><strong>Scheduled Preventive Maintenance:</strong> As specified by the maintenance schedule in CMMS.</li>
                    <li><strong>Condition-Based Maintenance:</strong> When real-time data from monitoring systems indicates potential issues.</li>
                    <li><strong>Reactive Maintenance:</strong> In response to unexpected equipment failures or breakdowns.</li>
                </ul>
