            <h3 class="text-primary">2. How often should maintenance inspections be conducted?</h3>
            <p>The frequency depends on equipment type, usage, and regulatory requirements, typically ranging from daily to annually.</p>

            <h3 class="text-primary">3. What should be included in a maintenance checklist?</h3>
            <p>A maintenance checklist should cover all critical aspects of the equipment, including operational status, safety measures, and compliance requirements.</p>

