                <h4 class="text-primary">1. What are the most effective closing techniques in negotiation?</h4>
                <p>Effective closing techniques include the summary close (summarizing agreement points), the urgency close (highlighting deadlines), and the presumptive close (assuming the decision will be positive).</p>

                <h4 class="text-primary">2. How can I build long-term relationships with vendors?</h4>
                <p>Building long-term relationships involves consistent communication, reliability, honoring agreements, and mutual respect.</p>

                <h4 class="text-primary">3. What should I avoid during vendor negotiations?</h4>
                <p>Avoid aggressive tactics, being unprepared, making concessions too quickly, and overlooking the vendor’s needs.</p>

