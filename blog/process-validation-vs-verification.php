                <div>
                    <h3 class="text-primary">1. What is the difference between validation and verification?</h3>
                    <p>
                        Validation ensures that the process can deliver consistent results, whereas verification confirms that the process is functioning correctly on an ongoing basis.
                    </p>
                </div>
                <div>
                    <h3 class="text-primary">2. Can a process be both validated and verified?</h3>
                    <p>
                        Yes, a process can be both validated initially to establish its reliability and continuously verified to ensure ongoing compliance and performance.
                    </p>
                </div>
                <div>
                    <h3 class="text-primary">3. What industries require process validation and verification?</h3>
                    <p>
                        Industries such as pharmaceuticals, food and beverage, aerospace, and automotive require rigorous process validation and verification to ensure safety, compliance, and quality.
                    </p>
                </div>
                <div>
                    <h3 class="text-primary">4. How often should processes be revalidated?</h3>
                    <p>
                        Processes should be revalidated periodically based on regulatory requirements, significant process changes, or at predefined intervals, typically every 1-3 years.
                    </p>
                </div>
