            <section>
                <h2 class="text-primary">Types of Breakdown Maintenance</h2>
                <p>There are primarily two types of breakdown maintenance:</p>
                <ol>
                    <li><strong>Run-to-Failure Maintenance:</strong> This strategy involves allowing equipment to operate until it fails, followed by repairs. It is suitable for non-critical equipment.</li>
                    <li><strong>As-Needed Maintenance:</strong> This is characterized by performing maintenance tasks as necessary, usually triggered by a failure or malfunction that affects critical operations.</li>
                </ol>
            </section>

            <section>
                <h2 class="text-primary">How to Conduct Breakdown Maintenance</h2>
                <p>Effective breakdown maintenance involves several key steps:</p>
                <ol>
                    <li><strong>Immediate Response:</strong> Identify the failure and respond immediately to prevent further damage.</li>
                    <li><strong>Diagnosis:</strong> Analyze the failure to determine the root cause.</li>
                    <li><strong>Repair:</strong> Perform necessary repairs or part replacements.</li>
                    <li><strong>Testing:</strong> Test the equipment to ensure it has returned to optimal working condition.</li>
                </ol>
            </section>

            <section>
                <h2 class="text-primary">When to Conduct Breakdown Maintenance</h2>
                <p>Breakdown maintenance should be conducted when:</p>
                <ul>
                    <li>Equipment stops working unexpectedly.</li>
                    <li>There is an indication of malfunctioning which affects operations significantly.</li>
                    <li>Pre-scheduled preventive maintenance has not been performed, leading to unexpected failures.</li>
                </ul>
            </section>

