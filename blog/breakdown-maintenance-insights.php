                <h2 class="text-primary">What is Breakdown Maintenance?</h2>
                <p>Breakdown maintenance refers to the remedial work that is performed after equipment has failed, with the goal to restore it to operational condition. Unlike preventive maintenance, which is performed at regular intervals, breakdown maintenance is reactive and occurs only when a failure or malfunction happens.</p>
            </section>

