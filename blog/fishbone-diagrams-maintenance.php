                        <li><strong>Root Cause Identification:</strong> Facilitates in identifying the root causes of frequent or recurring problems.</li>
                    </ul>
                </section>
                <section>
                    <h2 class="text-primary">How to Conduct a Fishbone Diagram Analysis?</h2>
                    <p>Follow these steps to efficiently conduct a Fishbone Diagram analysis:</p>
                    <ol>
                        <li><strong>Define the Problem:</strong> Clearly state the problem at the head of the diagram.</li>
                        <li><strong>Identify Major Categories:</strong> Determine the major categories of possible causes. These are typically labeled as Methods, Machines, Materials, Measurements, Environment, and People.</li>
                        <li><strong>Brainstorm Causes:</strong> For each category, brainstorm all possible causes and link them to the respective category on the diagram.</li>
                        <li><strong>Analyze the Diagram:</strong> Examine the diagram to identify the most likely root causes.</li>
                        <li><strong>Develop Solutions:</strong> Based on the analysis, devise solutions to address the identified root causes.</li>
                    </ol>
                </section>
